package com.micsig.base; // 定义基础工具类包

/**
 * ┌──────────────────────────────────────────────────────────────────────────────┐
 * │                    RxBooleanWithSelect 布尔值选择封装                        │
 * │                       带选择状态的布尔值响应式封装                            │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【模块定位】                                                                 │
 * │   MHO示波器基础工具模块 - RxJava扩展组件                                      │
 * │   封装布尔值与选择状态，用于开关类参数的UI交互                                 │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【核心职责】                                                                 │
 * │   1. 封装布尔值（value）                                                   │
 * │   2. 继承选择状态（rxMsgSelect）                                           │
 * │   3. 提供组合判断方法（isCurSelectTrue）                                    │
 * │   4. 支持toString调试输出                                                 │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【架构设计】                                                                 │
 * │   继承RxMsgSelect基类，扩展布尔值封装                                        │
 * │   用于表示开关状态（如：使能/禁用、开/关）                                    │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【数据流向】                                                                 │
 * │   布尔值 + 选择状态 → UI显示/交互响应                                       │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【依赖关系】                                                               │
 * │   继承: RxMsgSelect (选择状态基类)                                          │
 * │   被依赖: 开关设置、使能控制、触发模式选择等场景                              │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【使用示例】                                                                 │
 * │   RxBooleanWithSelect item = new RxBooleanWithSelect(true);               │
 * │   item.setRxMsgSelect(true);  // 设置为当前选中项                          │
 * │   if (item.isCurSelectTrue()) {                                           │
 * │       // 当前选中且值为true                                                │
 * │   }                                                                       │
 * └──────────────────────────────────────────────────────────────────────────────┘
 * 
 * @author yangj
 * @version 1.0
 * @since 2017/5/22
 */
public class RxBooleanWithSelect extends RxMsgSelect { // 继承RxMsgSelect基类
    
    // ==================== 成员变量 ====================
    private boolean value; // 布尔值，表示开关状态

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 获取布尔值                                                              │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   返回封装的布尔值                                                      │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值】                                                              │
     * │   @return 布尔值，true/false                                           │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    public boolean isValue() { // 获取布尔值方法
        return value; // 返回布尔值
    }

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 设置布尔值                                                              │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   设置封装的布尔值                                                      │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                            │
     * │   @param value 布尔值                                                  │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    public void setValue(boolean value) { // 设置布尔值方法
        this.value = value; // 更新布尔值
    }

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 构造函数                                                               │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   创建带初始布尔值的实例                                                │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                            │
     * │   @param value 初始布尔值                                              │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【使用示例】                                                            │
     * │   RxBooleanWithSelect item = new RxBooleanWithSelect(true);            │
     * │   RxBooleanWithSelect item2 = new RxBooleanWithSelect(false);          │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    public RxBooleanWithSelect(boolean value) { // 构造函数
        this.value = value; // 初始化布尔值
    }

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 判断是否当前选中且值为true                                             │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   组合判断：既是当前选择项，又是选择的打开状态                             │
     * │   用于判断某个选项是否被激活                                            │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值】                                                              │
     * │   @return true表示当前选中且值为true                                    │
     * │           false表示未选中或值为false                                    │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【使用示例】                                                            │
     * │   // 判断某个通道是否被激活                                             │
     * │   if (channelItem.isCurSelectTrue()) {                                 │
     * │       // 该通道被选中且处于开启状态                                      │
     * │       enableChannel();                                                 │
     * │   }                                                                    │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    public boolean isCurSelectTrue() { // 组合判断方法
        return value && rxMsgSelect; // 返回布尔值与选择状态的逻辑与结果
    }

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 转换为字符串                                                            │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   返回对象的字符串表示，用于调试输出                                      │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值】                                                              │
     * │   @return 包含value和rxMsgSelect的字符串                                │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    @Override
    public String toString() { // 重写toString方法
        return "RxBooleanWithSelect{" + // 返回类名
                "value=" + value + // 包含布尔值
                "rxMsgSelect=" + rxMsgSelect + // 包含选择状态
                '}'; // 结束括号
    }
}
