package com.micsig.base; // 定义基础工具类包

/**
 * ┌──────────────────────────────────────────────────────────────────────────────┐
 * │                         RxMsgSelect 消息选择基类                             │
 * │                          RxJava响应式选择状态封装                            │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【模块定位】                                                                 │
 * │   MHO示波器基础工具模块 - RxJava扩展组件                                      │
 * │   提供响应式选择状态封装，支持UI交互状态管理                                   │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【核心职责】                                                                 │
 * │   1. 封装选择状态（rxMsgSelect）                                            │
 * │   2. 提供选择状态的getter/setter方法                                        │
 * │   3. 作为RxBooleanWithSelect/RxIntWithSelect/RxStringWithSelect的基类       │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【架构设计】                                                                 │
 * │   采用继承模式，作为各种类型选择器的基类                                       │
 * │   protected成员变量允许子类直接访问                                          │
 * │   配合RxJava实现响应式UI更新                                                │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【数据流向】                                                                 │
 * │   用户交互 → setRxMsgSelect() → 状态更新 → UI响应                           │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【依赖关系】                                                                 │
 * │   被继承: RxBooleanWithSelect, RxIntWithSelect, RxStringWithSelect         │
 * │   被依赖: 菜单选择、参数选择、通道选择等UI交互场景                            │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【使用示例】                                                                 │
 * │   // 通常不直接使用，而是使用其子类                                          │
 * │   RxBooleanWithSelect item = new RxBooleanWithSelect(true);                │
 * │   item.setRxMsgSelect(true);  // 设置选中状态                               │
 * │   boolean selected = item.isRxMsgSelect();  // 获取选中状态                 │
 * └──────────────────────────────────────────────────────────────────────────────┘
 * 
 * @author yangj
 * @version 1.0
 * @since 2017/5/22
 */
public class RxMsgSelect { // RxJava消息选择基类
    
    // ==================== 成员变量 ====================
    protected boolean rxMsgSelect = false; // 选择状态标志，protected允许子类直接访问

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 获取选择状态                                                            │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   返回当前的选择状态                                                    │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值】                                                              │
     * │   @return true表示已选中，false表示未选中                                │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    public boolean isRxMsgSelect() { // 获取选择状态方法
        return rxMsgSelect; // 返回选择状态
    }

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 设置选择状态                                                            │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   设置当前的选择状态                                                    │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                            │
     * │   @param rxMsgSelect 选择状态，true表示选中，false表示未选中             │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    public void setRxMsgSelect(boolean rxMsgSelect) { // 设置选择状态方法
        this.rxMsgSelect = rxMsgSelect; // 更新选择状态
    }
}
