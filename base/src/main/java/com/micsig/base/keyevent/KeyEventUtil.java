package com.micsig.base.keyevent; // 按键事件工具包

import android.view.KeyEvent; // Android按键事件类

/**
 * ┌─────────────────────────────────────────────────────────────────────────────┐
 * │                               KeyEventUtil                                  │
 * │                            按键事件工具类                                     │
 * ├─────────────────────────────────────────────────────────────────────────────┤
 * │ 【模块定位】                                                                 │
 * │   base模块 → keyevent子包 → 工具类组件                                        │
 * │   MHO系列示波器软件按键事件判断与识别工具                                       │
 * ├─────────────────────────────────────────────────────────────────────────────┤
 * │ 【核心职责】                                                                 │
 * │   1. 提供按键类型的静态判断方法                                               │
 * │   2. 封装常用按键码的识别逻辑                                                 │
 * │   3. 支持确认键、返回键、方向键、正负号等特殊按键判断                           │
 * │   4. 提供标签输入场景的按键过滤判断                                           │
 * ├─────────────────────────────────────────────────────────────────────────────┤
 * │ 【架构设计】                                                                 │
 * │   ┌─────────────────────────────────────────────────────────────────────┐   │
 * │   │                        工具类模式(静态方法)                          │   │
 * │   │  ┌──────────────────────────────────────────────────────────────┐  │   │
 * │   │  │                     KeyEventUtil                             │  │   │
 * │   │  │  ┌─────────────────┐  ┌─────────────────┐                   │  │   │
 * │   │  │  │ isConfirmKey()  │  │  isBackKey()    │                   │  │   │
 * │   │  │  └─────────────────┘  └─────────────────┘                   │  │   │
 * │   │  │  ┌─────────────────┐  ┌─────────────────┐                   │  │   │
 * │   │  │  │ isIgnoreKey()   │  │  isLeft/Right() │                   │  │   │
 * │   │  │  └─────────────────┘  └─────────────────┘                   │  │   │
 * │   │  │  ┌─────────────────┐  ┌─────────────────┐                   │  │   │
 * │   │  │  │ignoreKeyForLabel│  │ isNegative/Pos()│                   │  │   │
 * │   │  │  └─────────────────┘  └─────────────────┘                   │  │   │
 * │   │  └──────────────────────────────────────────────────────────────┘  │   │
 * │   └─────────────────────────────────────────────────────────────────────┘   │
 * ├─────────────────────────────────────────────────────────────────────────────┤
 * │ 【按键分类】                                                                 │
 * │   ┌───────────────┬─────────────────────────────────────────────────────┐   │
 * │   │  按键类型      │  对应按键码                                          │   │
 * │   ├───────────────┼─────────────────────────────────────────────────────┤   │
 * │   │  确认键        │  KEYCODE_ENTER, KEYCODE_NUMPAD_ENTER               │   │
 * │   │  返回/删除键   │  KEYCODE_DEL                                         │   │
 * │   │  前向删除键    │  KEYCODE_FORWARD_DEL                                 │   │
 * │   │  方向键        │  TAB, DPAD_UP/DOWN/LEFT/RIGHT                       │   │
 * │   │  正号键        │  Shift+=, NUMPAD_ADD                                 │   │
 * │   │  负号键        │  NUMPAD_SUBTRACT, MINUS                              │   │
 * │   │  标签过滤键    │  分号、冒号、逗号                                     │   │
 * │   └───────────────┴─────────────────────────────────────────────────────┘   │
 * ├─────────────────────────────────────────────────────────────────────────────┤
 * │ 【数据流向】                                                                 │
 * │   KeyEvent事件对象 → KeyEventUtil静态方法 → boolean判断结果                   │
 * │        ↓                    ↓                                               │
 * │   提取keyCode          匹配预定义按键码                                       │
 * ├─────────────────────────────────────────────────────────────────────────────┤
 * │ 【依赖关系】                                                                 │
 * │   依赖:                                                                      │
 * │     - android.view.KeyEvent: 按键事件数据类，提供按键码常量                   │
 * │   被依赖:                                                                    │
 * │     - KeyEventFilter: 用于判断特定按键类型                                   │
 * │     - 各UI组件: 用于按键事件处理逻辑                                         │
 * ├─────────────────────────────────────────────────────────────────────────────┤
 * │ 【使用示例】                                                                  │
 * │   @Override                                                                 │
 * │   public boolean onKey(View v, int keyCode, KeyEvent event) {               │
 * │       // 判断是否为确认键                                                    │
 * │       if (KeyEventUtil.isConfirmKey(event)) {                               │
 * │           performAction();                                                  │
 * │           return true;                                                      │
 * │       }                                                                     │
 * │       // 判断是否为需要忽略的按键                                            │
 * │       if (KeyEventUtil.isIgnoreKey(event)) {                                │
 * │           return false;                                                     │
 * │       }                                                                     │
 * │       return false;                                                         │
 * │   }                                                                         │
 * ├─────────────────────────────────────────────────────────────────────────────┤
 * │ 【注意事项】                                                                  │
 * │   1. 所有方法均为静态方法，可直接调用                                         │
 * │   2. 方法不修改KeyEvent对象，仅做判断                                        │
 * │   3. 部分方法需要检查组合键状态(如Shift)                                      │
 * │   4. 按键码参考KeyEvent常量定义                                              │
 * └─────────────────────────────────────────────────────────────────────────────┘
 *
 * @author MICSIG开发团队
 * @version 1.0.0
 * @since 2024-01-01
 */
public class KeyEventUtil { // 按键事件工具类


    /**
     * ┌─────────────────────────────────────────────────────────────────────────┐
     * │ 方法：isConfirmKey - 判断是否为确认键                                     │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                              │
     * │   判断当前按键事件是否为确认键(回车键)                                      │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                              │
     * │   @param event 按键事件对象                                               │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值说明】                                                            │
     * │   @return true表示是确认键，false表示不是                                  │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【判断逻辑】                                                              │
     * │   检查按键码是否为:                                                        │
     * │   - KEYCODE_ENTER: 主回车键                                               │
     * │   - KEYCODE_NUMPAD_ENTER: 小键盘回车键                                    │
     * └─────────────────────────────────────────────────────────────────────────┘
     */
    public static boolean isConfirmKey(KeyEvent event) { // 判断确认键方法开始
        int keyCode = event.getKeyCode(); // 获取按键码
        return keyCode == KeyEvent.KEYCODE_ENTER || keyCode == KeyEvent.KEYCODE_NUMPAD_ENTER; // 判断是否为回车键或小键盘回车键
    } // 判断确认键方法结束

    /**
     * ┌─────────────────────────────────────────────────────────────────────────┐
     * │ 方法：isBackKey - 判断是否为返回/删除键                                   │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                              │
     * │   判断当前按键事件是否为返回键(退格删除键)                                  │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                              │
     * │   @param event 按键事件对象                                               │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值说明】                                                            │
     * │   @return true表示是返回键，false表示不是                                  │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【判断逻辑】                                                              │
     * │   检查按键码是否为KEYCODE_DEL(退格删除键)                                  │
     * └─────────────────────────────────────────────────────────────────────────┘
     */
    public static boolean isBackKey(KeyEvent event) { // 判断返回键方法开始
        int keyCode = event.getKeyCode(); // 获取按键码
        return keyCode == KeyEvent.KEYCODE_DEL; // 判断是否为退格删除键
    } // 判断返回键方法结束

    /**
     * ┌─────────────────────────────────────────────────────────────────────────┐
     * │ 方法：isForwardDel - 判断是否为前向删除键                                 │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                              │
     * │   判断当前按键事件是否为前向删除键(Delete键)                                │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                              │
     * │   @param event 按键事件对象                                               │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值说明】                                                            │
     * │   @return true表示是前向删除键，false表示不是                              │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【判断逻辑】                                                              │
     * │   检查按键码是否为KEYCODE_FORWARD_DEL(Delete键)                           │
     * │   前向删除: 删除光标后的字符，与退格键(删除光标前字符)相反                   │
     * └─────────────────────────────────────────────────────────────────────────┘
     */
    public static boolean isForwardDel(KeyEvent event) { // 判断前向删除键方法开始
        int keyCode = event.getKeyCode(); // 获取按键码
        return keyCode == KeyEvent.KEYCODE_FORWARD_DEL; // 判断是否为前向删除键
    } // 判断前向删除键方法结束


    /**
     * ┌─────────────────────────────────────────────────────────────────────────┐
     * │ 方法：isIgnoreKey - 判断是否为忽略键                                      │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                              │
     * │   判断当前按键事件是否为需要忽略的导航键                                    │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                              │
     * │   @param event 按键事件对象                                               │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值说明】                                                            │
     * │   @return true表示是忽略键，false表示不是                                  │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【判断逻辑】                                                              │
     * │   检查按键码是否为以下导航键之一:                                          │
     * │   - KEYCODE_TAB: Tab键                                                   │
     * │   - KEYCODE_DPAD_UP: 方向键上                                             │
     * │   - KEYCODE_DPAD_DOWN: 方向键下                                           │
     * │   - KEYCODE_DPAD_LEFT: 方向键左                                           │
     * │   - KEYCODE_DPAD_RIGHT: 方向键右                                          │
     * └─────────────────────────────────────────────────────────────────────────┘
     */
    //tab ↑ ↓ ← → // 注释说明支持的忽略键类型
    public static boolean isIgnoreKey(KeyEvent event) { // 判断忽略键方法开始
        int keyCode = event.getKeyCode(); // 获取按键码
        return keyCode == KeyEvent.KEYCODE_TAB // 判断是否为Tab键
                || keyCode == KeyEvent.KEYCODE_DPAD_UP // 或方向键上
                || keyCode == KeyEvent.KEYCODE_DPAD_DOWN // 或方向键下
                || keyCode == KeyEvent.KEYCODE_DPAD_LEFT // 或方向键左
                || keyCode == KeyEvent.KEYCODE_DPAD_RIGHT; // 或方向键右
    } // 判断忽略键方法结束

    /**
     * ┌─────────────────────────────────────────────────────────────────────────┐
     * │ 方法：isLeft - 判断是否为左方向键                                         │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                              │
     * │   判断当前按键事件是否为左方向键                                           │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                              │
     * │   @param event 按键事件对象                                               │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值说明】                                                            │
     * │   @return true表示是左方向键，false表示不是                                │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【判断逻辑】                                                              │
     * │   检查按键码是否为KEYCODE_DPAD_LEFT                                       │
     * └─────────────────────────────────────────────────────────────────────────┘
     */
    public static boolean isLeft(KeyEvent event) { // 判断左方向键方法开始
        int keyCode = event.getKeyCode(); // 获取按键码
        return keyCode == KeyEvent.KEYCODE_DPAD_LEFT; // 判断是否为左方向键
    } // 判断左方向键方法结束

    /**
     * ┌─────────────────────────────────────────────────────────────────────────┐
     * │ 方法：isRight - 判断是否为右方向键                                        │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                              │
     * │   判断当前按键事件是否为右方向键                                           │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                              │
     * │   @param event 按键事件对象                                               │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值说明】                                                            │
     * │   @return true表示是右方向键，false表示不是                                │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【判断逻辑】                                                              │
     * │   检查按键码是否为KEYCODE_DPAD_RIGHT                                      │
     * └─────────────────────────────────────────────────────────────────────────┘
     */
    public static boolean isRight(KeyEvent event) { // 判断右方向键方法开始
        int keyCode = event.getKeyCode(); // 获取按键码
        return keyCode == KeyEvent.KEYCODE_DPAD_RIGHT; // 判断是否为右方向键
    } // 判断右方向键方法结束

    /**
     * ┌─────────────────────────────────────────────────────────────────────────┐
     * │ 方法：ignoreKeyForLabel - 判断是否为标签输入过滤键                        │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                              │
     * │   判断当前按键事件是否为标签输入场景需要过滤的按键                          │
     * │   用于限制标签输入中不允许的特殊字符                                        │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                              │
     * │   @param event 按键事件对象                                               │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值说明】                                                            │
     * │   @return true表示是需要过滤的按键，false表示不是                          │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【判断逻辑】                                                              │
     * │   检查按键码是否为以下需要过滤的字符:                                      │
     * │   - Shift+分号: 冒号(:)                                                  │
     * │   - 分号: (;)                                                            │
     * │   - 逗号: (,)                                                            │
     * │   这些字符在标签输入中可能造成解析问题，需要过滤                            │
     * └─────────────────────────────────────────────────────────────────────────┘
     */
    /**
     * 标签限制输入屏幕  冒号 分号 逗号 // 功能说明注释
     */
    public static boolean ignoreKeyForLabel(KeyEvent event) { // 标签过滤键判断方法开始
        int keyCode = event.getKeyCode(); // 获取按键码
        return (event.isShiftPressed() && keyCode == KeyEvent.KEYCODE_SEMICOLON) // Shift+分键=冒号
                || keyCode == KeyEvent.KEYCODE_SEMICOLON // 或分号键
                || keyCode == KeyEvent.KEYCODE_COMMA; // 或逗号键
    } // 标签过滤键判断方法结束

    /**
     * ┌─────────────────────────────────────────────────────────────────────────┐
     * │ 方法：isNegative - 判断是否为负号键                                       │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                              │
     * │   判断当前按键事件是否为负号键                                             │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                              │
     * │   @param event 按键事件对象                                               │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值说明】                                                            │
     * │   @return true表示是负号键，false表示不是                                  │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【判断逻辑】                                                              │
     * │   检查按键码是否为:                                                        │
     * │   - KEYCODE_NUMPAD_SUBTRACT: 小键盘减号键                                 │
     * │   - KEYCODE_MINUS: 主键盘减号键                                           │
     * └─────────────────────────────────────────────────────────────────────────┘
     */
    public static boolean isNegative(KeyEvent event) { // 判断负号键方法开始
        int keyCode = event.getKeyCode(); // 获取按键码
        return keyCode == KeyEvent.KEYCODE_NUMPAD_SUBTRACT // 小键盘减号
                || keyCode == KeyEvent.KEYCODE_MINUS; // 主键盘减号
    } // 判断负号键方法结束


    /**
     * ┌─────────────────────────────────────────────────────────────────────────┐
     * │ 方法：isPositive - 判断是否为正号键                                       │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                              │
     * │   判断当前按键事件是否为正号键                                             │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                              │
     * │   @param event 按键事件对象                                               │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值说明】                                                            │
     * │   @return true表示是正号键，false表示不是                                  │
     * ├─────────────────────────────────────────────────────────────────────────┤
     * │ 【判断逻辑】                                                              │
     * │   检查按键码是否为:                                                        │
     * │   - Shift+等号键: 主键盘输入正号(+)                                       │
     * │   - KEYCODE_NUMPAD_ADD: 小键盘加号键                                      │
     * └─────────────────────────────────────────────────────────────────────────┘
     */
    public static boolean isPositive(KeyEvent event) { // 判断正号键方法开始
        int keyCode = event.getKeyCode(); // 获取按键码
        return (event.isShiftPressed() && keyCode == KeyEvent.KEYCODE_EQUALS) // Shift+等号=正号
                        || keyCode == KeyEvent.KEYCODE_NUMPAD_ADD; // 或小键盘加号
    } // 判断正号键方法结束

} // KeyEventUtil类结束
