package com.micsig.base; // 定义基础工具类包

/**
 * ┌──────────────────────────────────────────────────────────────────────────────┐
 * │                      RxIntWithSelect 整数值选择封装                          │
 * │                        带选择状态的整数值响应式封装                           │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【模块定位】                                                                 │
 * │   MHO示波器基础工具模块 - RxJava扩展组件                                      │
 * │   封装整数值与选择状态，用于枚举类参数的UI交互                                 │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【核心职责】                                                                 │
 * │   1. 封装整数值（value）                                                  │
 * │   2. 继承选择状态（rxMsgSelect）                                           │
 * │   3. 支持toString调试输出                                                 │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【架构设计】                                                                 │
 * │   继承RxMsgSelect基类，扩展整数值封装                                        │
 * │   用于表示枚举索引、通道号、档位等整数参数                                    │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【数据流向】                                                                 │
 * │   整数值 + 选择状态 → UI显示/交互响应                                       │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【依赖关系】                                                                 │
 * │   继承: RxMsgSelect (选择状态基类)                                          │
 * │   被依赖: 通道选择、时基档位、电压档位、触发模式等场景                        │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【使用示例】                                                                 │
 * │   RxIntWithSelect item = new RxIntWithSelect(1);  // 通道1                │
 * │   item.setRxMsgSelect(true);  // 设置为当前选中项                          │
 * │   int channel = item.getValue();  // 获取通道号                           │
 * └──────────────────────────────────────────────────────────────────────────────┘
 * 
 * @author yangj
 * @version 1.0
 * @since 2017/5/22
 */
public class RxIntWithSelect extends RxMsgSelect { // 继承RxMsgSelect基类
    
    // ==================== 成员变量 ====================
    private int value; // 整数值，表示参数值

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 获取整数值                                                              │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   返回封装的整数值                                                      │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值】                                                              │
     * │   @return 整数值                                                       │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    public int getValue() { // 获取整数值方法
        return value; // 返回整数值
    }

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 设置整数值                                                              │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   设置封装的整数值                                                      │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                            │
     * │   @param value 整数值                                                  │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    public void setValue(int value) { // 设置整数值方法
        this.value = value; // 更新整数值
    }

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 构造函数                                                               │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   创建带初始整数值的实例                                                │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                            │
     * │   @param value 初始整数值                                              │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【使用示例】                                                            │
     * │   RxIntWithSelect item = new RxIntWithSelect(0);  // 默认值0            │
     * │   RxIntWithSelect channel = new RxIntWithSelect(1);  // 通道1           │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    public RxIntWithSelect(int value) { // 构造函数
        this.value = value; // 初始化整数值
    }

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 转换为字符串                                                            │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   返回对象的字符串表示，用于调试输出                                      │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值】                                                              │
     * │   @return 包含value和rxMsgSelect的字符串                                │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    @Override
    public String toString() { // 重写toString方法
        return "RxIntWithSelect{" + // 返回类名
                "value=" + value + // 包含整数值
                "rxMsgSelect=" + rxMsgSelect + // 包含选择状态
                '}'; // 结束括号
    }
}
