package com.micsig.base; // 定义基础工具类包

/**
 * ┌──────────────────────────────────────────────────────────────────────────────┐
 * │                    RxStringWithSelect 字符串选择封装                         │
 * │                       带选择状态的字符串响应式封装                            │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【模块定位】                                                                 │
 * │   MHO示波器基础工具模块 - RxJava扩展组件                                      │
 * │   封装字符串与选择状态，用于文本类参数的UI交互                                 │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【核心职责】                                                                 │
 * │   1. 封装字符串值（value）                                                │
 * │   2. 继承选择状态（rxMsgSelect）                                           │
 * │   3. 支持克隆操作（Cloneable）                                             │
 * │   4. 支持toString调试输出                                                 │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【架构设计】                                                                 │
 * │   继承RxMsgSelect基类，实现Cloneable接口                                    │
 * │   用于表示标签、名称、路径等字符串参数                                       │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【数据流向】                                                                 │
 * │   字符串 + 选择状态 → UI显示/交互响应                                       │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【依赖关系】                                                                 │
 * │   继承: RxMsgSelect (选择状态基类)                                          │
 * │   实现: Cloneable (克隆接口)                                               │
 * │   被依赖: 文件名选择、标签输入、路径配置等场景                               │
 * ├──────────────────────────────────────────────────────────────────────────────┤
 * │ 【使用示例】                                                                 │
 * │   RxStringWithSelect item = new RxStringWithSelect("CH1");               │
 * │   item.setRxMsgSelect(true);  // 设置为当前选中项                          │
 * │   String name = item.getValue();  // 获取字符串值                          │
 * │   RxStringWithSelect copy = (RxStringWithSelect) item.clone();  // 克隆   │
 * └──────────────────────────────────────────────────────────────────────────────┘
 * 
 * @author yangj
 * @version 1.0
 * @since 2017/5/22
 */
public class RxStringWithSelect extends RxMsgSelect implements Cloneable { // 继承RxMsgSelect基类，实现Cloneable接口
    
    // ==================== 成员变量 ====================
    private String value; // 字符串值，表示参数内容

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 克隆对象                                                               │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   创建当前对象的副本                                                    │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值】                                                              │
     * │   @return 克隆的对象副本                                                │
     * │   @throws CloneNotSupportedException 不支持克隆时抛出                    │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【使用示例】                                                            │
     * │   RxStringWithSelect original = new RxStringWithSelect("test");        │
     * │   RxStringWithSelect copy = (RxStringWithSelect) original.clone();     │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    @Override
    public Object clone() throws CloneNotSupportedException { // 重写clone方法
        return super.clone(); // 调用父类克隆方法
    }

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 获取字符串值                                                            │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   返回封装的字符串值                                                    │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值】                                                              │
     * │   @return 字符串值                                                     │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    public String getValue() { // 获取字符串值方法
        return value; // 返回字符串值
    }

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 设置字符串值                                                            │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   设置封装的字符串值                                                    │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                            │
     * │   @param value 字符串值                                                │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    public void setValue(String value) { // 设置字符串值方法
        this.value = value; // 更新字符串值
    }

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 构造函数                                                               │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   创建带初始字符串值的实例                                              │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【参数说明】                                                            │
     * │   @param value 初始字符串值                                            │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【使用示例】                                                            │
     * │   RxStringWithSelect item = new RxStringWithSelect("CH1");             │
     * │   RxStringWithSelect file = new RxStringWithSelect("waveform.dat");    │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    public RxStringWithSelect(String value) { // 构造函数
        this.value = value; // 初始化字符串值
    }

    /**
     * ┌────────────────────────────────────────────────────────────────────────┐
     * │ 转换为字符串                                                            │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【功能说明】                                                            │
     * │   返回对象的字符串表示，用于调试输出                                      │
     * ├────────────────────────────────────────────────────────────────────────┤
     * │ 【返回值】                                                              │
     * │   @return 包含value和rxMsgSelect的字符串                                │
     * └────────────────────────────────────────────────────────────────────────┘
     */
    @Override
    public String toString() { // 重写toString方法
        return "RxStringWithSelect{" + // 返回类名
                "value='" + value + '\'' + // 包含字符串值
                "rxMsgSelect='" + rxMsgSelect + '\'' + // 包含选择状态
                '}'; // 结束括号
    }
}
