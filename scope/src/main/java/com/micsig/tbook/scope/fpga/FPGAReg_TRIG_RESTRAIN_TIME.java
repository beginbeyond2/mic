/**
 * 示波器FPGA触发抑制时间寄存器类
 * 继承自FPGAReg基类，负责管理FPGA触发抑制时间参数
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>设置触发抑制时间</li>
 *   <li>控制触发后的抑制周期</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建触发抑制时间寄存器对象
 * FPGAReg_TRIG_RESTRAIN_TIME reg = new FPGAReg_TRIG_RESTRAIN_TIME();
 *
 * // 设置抑制时间（单次触发模式）
 * reg.setRestrainTime(Integer.MAX_VALUE);  // 设置为最大值，相当于单次触发
 *
 * // 设置抑制时间（正常触发模式）
 * reg.setRestrainTime(1000000);  // 设置为1000000个时钟周期
 *
 * // 设置抑制时间（自动触发模式）
 * reg.setRestrainTime(0);  // 设置为0，允许连续触发
 *
 * // 发送命令到FPGA
 * FPGACommand.getInstance().sendCommand(reg);
 * </pre>
 *
 * @see FPGAReg
 * @see FPGACommand
 * @see Trigger
 */
public class FPGAReg_TRIG_RESTRAIN_TIME extends FPGAReg {                                // 类声明：触发抑制时间寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化触发抑制时间寄存器
     * 
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_TRIG_RESTRAIN_TIME</li>
     *   <li>数据长度：4字节（32位）</li>
     * </ul>
     */
    public FPGAReg_TRIG_RESTRAIN_TIME() {                                                // 构造方法：初始化触发抑制时间寄存器
        super(FPGA_TRIG_RESTRAIN_TIME, 4);                                               // 调用父类构造函数，设置寄存器ID和数据长度
    }                                                                                    // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置触发抑制时间
     * 设置触发后的抑制周期，防止重复触发
     * 
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>抑制时间以时钟周期数为单位</li>
     *   <li>设置为0表示不抑制，可以连续触发</li>
     *   <li>设置较大的值可以确保触发间隔足够长</li>
     *   <li>单次触发模式下设置为最大值</li>
     * </ul>
     * 
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>最小值：0（不抑制）</li>
     *   <li>最大值：Integer.MAX_VALUE（无限抑制）</li>
     *   <li>典型值：根据触发模式和应用场景确定</li>
     * </ul>
     * 
     * <p><b>典型值示例：</b></p>
     * <ul>
     *   <li>自动触发：0（连续触发）</li>
     *   <li>正常触发：1000000（约1ms抑制时间）</li>
     *   <li>单次触发：Integer.MAX_VALUE（无限抑制）</li>
     * </ul>
     * 
     * @param val 抑制时间值（时钟周期数）
     */
    public void setRestrainTime(int val){                                                // 公有方法：设置触发抑制时间
        setVal(0,val);                                                                   // 在偏移量0处设置抑制时间值
    }                                                                                    // 方法结束

}                                                                                        // 类结束