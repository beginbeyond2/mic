/**
 * 示波器FPGA显示波形寄存器类
 * 继承自FPGAReg基类，负责管理FPGA波形显示参数
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>设置插值Y值</li>
 *   <li>设置是否需要插值Y值</li>
 *   <li>设置波形显示长度</li>
 *   <li>设置波形显示位置</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建显示波形寄存器对象
 * FPGAReg_DISP_WAVE reg = new FPGAReg_DISP_WAVE();
 *
 * // 设置插值Y值
 * reg.setCouY(100);     // 设置插值Y值为100
 *
 * // 设置波形显示长度
 * reg.setLen(1000);     // 设置波形显示长度为1000
 *
 * // 设置波形显示位置
 * reg.setWavePlace(0);  // 设置波形显示位置为0
 *
 * // 发送命令到FPGA
 * FPGACommand.getInstance().sendCommand(reg);
 * </pre>
 *
 * @see FPGAReg
 * @see FPGACommand
 * @see Display
 * @see WaveRender
 * @author zhuzh
 * @version 1.0
 * @since 2018/3/21
 */
public class FPGAReg_DISP_WAVE extends FPGAReg {                                           // 类声明：显示波形寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化显示波形寄存器
     *
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_DISP_WAVE</li>
     *   <li>数据长度：8字节（2个字，每个字32位）</li>
     * </ul>
     *
     * <p><b>寄存器地址说明：</b></p>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器地址映射                         │
     * ├─────────────────────────────────────────┤
     * │  FPGA_DISP_WAVE                         │
     * │  功能: 配置波形显示参数                 │
     * │  大小: 8字节（2个字）                   │
     * │  访问: 只写 (由FPGA读取)                │
     * │                                         │
     * │  数据结构:                              │
     * │  字0: 插值Y值和波形显示长度             │
     * │    位0-12: 插值Y值                      │
     * │    位13-31: 波形显示长度                │
     * │  字1: 波形显示位置                      │
     * └─────────────────────────────────────────┘
     * </pre>
     */
    public FPGAReg_DISP_WAVE() {                                                           // 构造方法：初始化显示波形寄存器
        // 调用父类构造函数，设置寄存器ID和数据长度
        // FPGA_DISP_WAVE：显示波形寄存器地址
        // 8：数据长度为8字节（2个32位字）
        super(FPGAReg.FPGA_DISP_WAVE,8);                                                    // 调用父类构造函数，设置寄存器ID为FPGA_DISP_WAVE，长度8字节
    }                                                                                      // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法 - 插值参数设置
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置插值Y值
     * 设置波形插值显示的Y值参数
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>插值Y值用于波形插值显示</li>
     *   <li>决定插值算法的Y值参数</li>
     *   <li>影响波形显示的平滑度</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>val：插值Y值（13位，范围0到8191）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <ul>
     *   <li>存储位置：字0的位0-12（13位）</li>
     * </ul>
     *
     * @param val 插值Y值（13位）
     */
    public void setCouY(int val){                                                          // 公有方法：设置插值Y值
        // 调用父类setVal方法，设置字0的插值Y值
        // 参数1：起始位=0（从位0开始）
        // 参数2：位宽度=13（13位）
        // 参数3：插值Y值
        setVal(0,13,val);                                                                  // 设置字0的位0-12，值为val（插值Y值）
    }                                                                                      // 方法结束

    /**
     * 设置是否需要插值Y值（已注释）
     * 此方法已被注释，不再使用
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>原功能：设置是否需要插值Y值标志</li>
     *   <li>已被注释，不再使用</li>
     * </ul>
     *
     * @param val 是否需要插值Y值标志（1位，0=不需要，1=需要）
     */
    public void setNeedCY(int val){                                                        // 公有方法：设置是否需要插值Y值（已注释）
        // 此方法已被注释，不再使用
        // 原功能：设置字0的位13，值为val（是否需要插值Y值标志）
        //setVal(13,1,val);                                                                 // 已注释：设置字0的位13，值为val（是否需要插值Y值标志）
    }                                                                                      // 方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法 - 波形显示参数设置
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置波形显示长度
     * 设置波形显示的长度值
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>波形显示长度决定了波形显示的长度</li>
     *   <li>用于控制波形显示的范围</li>
     *   <li>值越大，显示的波形越长</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>val：波形显示长度值（19位，范围0到524287）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <ul>
     *   <li>存储位置：字0的位13-31（19位）</li>
     * </ul>
     *
     * @param val 波形显示长度值（19位）
     */
    public void setLen(int val){                                                           // 公有方法：设置波形显示长度
        // 调用父类setVal方法，设置字0的波形显示长度
        // 参数1：起始位=13（从位13开始）
        // 参数2：位宽度=19（19位）
        // 参数3：波形显示长度值
        setVal(13,19,val);                                                                 // 设置字0的位13-31，值为val（波形显示长度）
    }                                                                                      // 方法结束

    /**
     * 设置波形显示位置
     * 设置波形在屏幕上的显示位置
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>波形显示位置决定了波形在屏幕上的显示位置</li>
     *   <li>用于控制波形的显示位置</li>
     *   <li>值越大，显示位置越靠后</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>val：波形显示位置值（32位）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <ul>
     *   <li>存储位置：字1（32位）</li>
     * </ul>
     *
     * @param val 波形显示位置值
     */
    public  void setWavePlace(int val){                                                    // 公有方法：设置波形显示位置
        // 调用父类setVal方法，设置字1的波形显示位置
        setVal(1,val);                                                                     // 设置字1，值为val（波形显示位置）
    }                                                                                      // 方法结束

}                                                                                          // 类结束