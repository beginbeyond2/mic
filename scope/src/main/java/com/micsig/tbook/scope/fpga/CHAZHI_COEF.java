/**
 * 示波器FPGA插值系数管理类
 * 负责管理和提供FPGA插值计算所需的系数数据
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>管理单点插值系数数组</li>
 *   <li>管理双点插值系数数组</li>
 *   <li>管理线性插值系数数组</li>
 *   <li>提供系数初始化接口</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 使用默认系数（MHO68V2）
 * int[] sglCoef = CHAZHI_COEF.chazhiCoef_sgl;
 * int[] dubCoef = CHAZHI_COEF.chazhiCoef_dub;
 * int[] lineCoef = CHAZHI_COEF.chazhiCoef_Line;
 *
 * // 初始化自定义系数（适配其他硬件）
 * CHAZHI_COEF.Init(customSgl, customDub, customLine);
 *
 * // 获取初始化后的系数
 * int[] newSglCoef = CHAZHI_COEF.chazhiCoef_sgl;
 * </pre>
 *
 * @see MHO68V2_CHAZHI_COEF
 * @see FPGABoot
 */
public class CHAZHI_COEF {                                                               // 类声明：插值系数管理类

    // ═══════════════════════════════════════════════════════════════════════════════
    // 插值系数静态变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 单点插值系数数组
     * 用于单点插值算法的权重系数
     * 默认值来自 MHO68V2_CHAZHI_COEF
     */
    public static int [] chazhiCoef_sgl = MHO68V2_CHAZHI_COEF.chazhiCoef_sgl;             // 静态变量：单点插值系数数组，默认使用MHO68V2的系数

    /**
     * 双点插值系数数组
     * 用于双点插值算法的权重系数
     * 默认值来自 MHO68V2_CHAZHI_COEF
     */
    public static int [] chazhiCoef_dub = MHO68V2_CHAZHI_COEF.chazhiCoef_dub;             // 静态变量：双点插值系数数组，默认使用MHO68V2的系数

    /**
     * 线性插值系数数组
     * 用于线性插值算法的权重系数
     * 默认值来自 MHO68V2_CHAZHI_COEF
     */
    public static int [] chazhiCoef_Line = MHO68V2_CHAZHI_COEF.chazhiCoef_Line;           // 静态变量：线性插值系数数组，默认使用MHO68V2的系数

    // ═══════════════════════════════════════════════════════════════════════════════
    // 初始化方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 初始化插值系数
     * 用于适配不同硬件型号的插值系数
     *
     * <p><b>使用场景：</b></p>
     * <ul>
     *   <li>系统启动时根据硬件型号初始化系数</li>
     *   <li>切换硬件型号时更新系数</li>
     *   <li>测试调试时使用自定义系数</li>
     * </ul>
     *
     * <p><b>参数说明：</b></p>
     * <ul>
     *   <li>sgl: 单点插值系数数组，用于单点插值算法</li>
     *   <li>dub: 双点插值系数数组，用于双点插值算法</li>
     *   <li>line: 线性插值系数数组，用于线性插值算法</li>
     * </ul>
     *
     * @param sgl 单点插值系数数组
     * @param dub 双点插值系数数组
     * @param line 线性插值系数数组
     */
    public static void Init(int[] sgl,int [] dub,int [] line){                            // 静态公有方法：初始化插值系数
        chazhiCoef_sgl = sgl;                                                            // 设置单点插值系数数组
        chazhiCoef_dub = dub;                                                            // 设置双点插值系数数组
        chazhiCoef_Line = line;                                                          // 设置线性插值系数数组
    }                                                                                       // 方法结束

}                                                                                           // 类结束
