package com.micsig.tbook.scope.fpga;  // 定义包名：示波器FPGA寄存器管理模块

/**
 * FPGA AD零点校准寄存器类 - 用于配置示波器AD转换器的零点校准值
 *
 * <p><b>模块定位：</b>
 * <ul>
 *   <li>所属模块：scope.fpga（示波器FPGA寄存器管理模块）</li>
 *   <li>架构层级：硬件抽象层 - FPGA寄存器配置</li>
 *   <li>设计模式：继承模式（继承FPGAReg基类）</li>
 *   <li>职责类型：硬件校准配置器</li>
 * </ul>
 *
 * <p><b>核心职责：</b>
 * <ul>
 *   <li>配置AD转换器的零点校准值</li>
 *   <li>支持多通道AD零点校准</li>
 *   <li>提供A/B两组校准值配置</li>
 *   <li>封装FPGA校准寄存器的位操作</li>
 * </ul>
 *
 * <p><b>设计目的：</b>
 * <ul>
 *   <li>消除AD转换器的零点偏移误差</li>
 *   <li>提高测量精度和准确性</li>
 *   <li>支持不同量程下的零点校准</li>
 *   <li>支持多通道独立校准</li>
 * </ul>
 *
 * <p><b>寄存器结构：</b>
 * <pre>
 * FPGA_AD_ZERO寄存器（32字节，8个4字节字段）
 * │
 * ├── 位0-31：D1A - 通道1校准值A（4字节）
 * │     └── 用于通道1的零点校准值A
 * │
 * ├── 位32-63：D1B - 通道1校准值B（4字节）
 * │     └── 用于通道1的零点校准值B
 * │
 * ├── 位64-95：D2A - 通道2校准值A（4字节）
 * │     └── 用于通道2的零点校准值A
 * │
 * ├── 位96-127：D2B - 通道2校准值B（4字节）
 * │     └── 用于通道2的零点校准值B
 * │
 * ├── 位128-159：D3A - 通道3校准值A（4字节）
 * │     └── 用于通道3的零点校准值A
 * │
 * ├── 位160-191：D3B - 通道3校准值B（4字节）
 * │     └── 用于通道3的零点校准值B
 * │
 * ├── 位192-223：D4A - 通道4校准值A（4字节）
 * │     └── 用于通道4的零点校准值A
 * │
 * └── 位224-255：D4B - 通道4校准值B（4字节）
 *       └── 用于通道4的零点校准值B
 * </pre>
 *
 * <p><b>校准值说明：</b>
 * <ul>
 *   <li>D1-D4：对应示波器的4个输入通道</li>
 *   <li>A/B校准值：可能对应不同量程或不同增益下的零点校准</li>
 *   <li>校准值在出厂校准时写入，存储在非易失性存储器中</li>
 *   <li>用户也可以进行手动校准更新这些值</li>
 * </ul>
 *
 * <p><b>使用场景：</b>
 * <ul>
 *   <li>出厂校准：生产线上进行零点校准</li>
 *   <li>用户校准：用户手动执行自校准操作</li>
 *   <li>周期校准：定期校准以补偿器件老化</li>
 *   <li>温度补偿：温度变化后重新校准零点</li>
 * </ul>
 *
 * <p><b>依赖关系：</b>
 * <ul>
 *   <li>继承：FPGAReg（FPGA寄存器基类）</li>
 *   <li>依赖：FPGACommand（FPGA命令处理类）</li>
 *   <li>依赖：CalibrationManager（校准管理类）</li>
 * </ul>
 *
 * <p><b>线程安全：</b>
 * <ul>
 *   <li>寄存器操作在FPGACommand中同步执行</li>
 *   <li>校准过程需要确保硬件稳定</li>
 * </ul>
 *
 * <p><b>使用示例：</b>
 * <pre>
 * // 创建AD零点校准寄存器实例
 * FPGAReg_AD_ZERO regADZero = new FPGAReg_AD_ZERO();
 *
 * // 设置通道1的校准值
 * regADZero.setD1A(100);  // 设置通道1校准值A
 * regADZero.setD1B(200);  // 设置通道1校准值B
 *
 * // 设置通道2的校准值
 * regADZero.setD2A(150);
 * regADZero.setD2B(250);
 *
 * // 发送寄存器配置到FPGA
 * fpgaCommand.sendCmd(regADZero);
 * </pre>
 *
 * @author zhuzh
 * @version 1.0
 * @since 2018/3/22
 * @see FPGAReg FPGA寄存器基类
 * @see FPGACommand FPGA命令处理类
 */
public class FPGAReg_AD_ZERO extends FPGAReg {  // 继承FPGAReg基类，复用寄存器操作方法

    /**
     * 构造函数 - 初始化AD零点校准寄存器
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>调用父类构造函数，设置寄存器地址和大小</li>
     *   <li>寄存器地址：FPGA_AD_ZERO（AD零点校准寄存器）</li>
     *   <li>寄存器大小：4字节（注意：实际数据为32字节，但基类按最小单元管理）</li>
     * </ul>
     *
     * <p><b>寄存器地址说明：</b>
     * <ul>
     *   <li>FPGA_AD_ZERO：用于配置AD转换器的零点校准值</li>
     *   <li>地址值由FPGAReg基类定义</li>
     * </ul>
     *
     * <p><b>寄存器大小说明：</b>
     * <ul>
     *   <li>构造函数传入4字节，但实际使用setVal方法设置32字节数据</li>
     *   <li>每个校准值占用4字节，共8个校准值</li>
     * </ul>
     */
    public FPGAReg_AD_ZERO(){  // 构造函数
        // 调用父类构造函数，传入寄存器地址和大小
        // FPGA_AD_ZERO：AD零点校准寄存器地址
        // 4：寄存器基础大小为4字节
        super(FPGA_AD_ZERO, 4);  // 初始化AD零点校准寄存器，地址为FPGA_AD_ZERO，基础大小为4字节
    }

    /**
     * 设置通道1校准值A
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>设置通道1的零点校准值A</li>
     *   <li>数据位置：位0-31（4字节）</li>
     *   <li>用于补偿通道1的零点偏移</li>
     * </ul>
     *
     * <p><b>校准值说明：</b>
     * <ul>
     *   <li>校准值A可能对应某一量程或增益下的零点校准</li>
     *   <li>值的具体含义由硬件设计决定</li>
     * </ul>
     *
     * @param val 校准值A（4字节整数）
     */
    public void setD1A(int val){  // 设置通道1校准值A方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=0（位0开始）
        // 参数2：位宽度=4字节（32位）
        // 参数3：校准值A
        setVal(0, 4, val);  // 设置位0-31的值，宽度为4字节，值为val
    }

    /**
     * 设置通道1校准值B
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>设置通道1的零点校准值B</li>
     *   <li>数据位置：位32-63（4字节）</li>
     *   <li>用于补偿通道1的零点偏移</li>
     * </ul>
     *
     * <p><b>校准值说明：</b>
     * <ul>
     *   <li>校准值B可能对应另一量程或增益下的零点校准</li>
     *   <li>值的具体含义由硬件设计决定</li>
     * </ul>
     *
     * @param val 校准值B（4字节整数）
     */
    public void setD1B(int val){  // 设置通道1校准值B方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=4字节（位32开始）
        // 参数2：位宽度=4字节（32位）
        // 参数3：校准值B
        setVal(4, 4, val);  // 设置位32-63的值，宽度为4字节，值为val
    }

    /**
     * 设置通道2校准值A
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>设置通道2的零点校准值A</li>
     *   <li>数据位置：位64-95（4字节）</li>
     *   <li>用于补偿通道2的零点偏移</li>
     * </ul>
     *
     * @param val 校准值A（4字节整数）
     */
    public void setD2A(int val){  // 设置通道2校准值A方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=8字节（位64开始）
        // 参数2：位宽度=4字节（32位）
        // 参数3：校准值A
        setVal(8, 4, val);  // 设置位64-95的值，宽度为4字节，值为val
    }

    /**
     * 设置通道2校准值B
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>设置通道2的零点校准值B</li>
     *   <li>数据位置：位96-127（4字节）</li>
     *   <li>用于补偿通道2的零点偏移</li>
     * </ul>
     *
     * @param val 校准值B（4字节整数）
     */
    public void setD2B(int val){  // 设置通道2校准值B方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=12字节（位96开始）
        // 参数2：位宽度=4字节（32位）
        // 参数3：校准值B
        setVal(12, 4, val);  // 设置位96-127的值，宽度为4字节，值为val
    }

    /**
     * 设置通道3校准值A
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>设置通道3的零点校准值A</li>
     *   <li>数据位置：位128-159（4字节）</li>
     *   <li>用于补偿通道3的零点偏移</li>
     * </ul>
     *
     * @param val 校准值A（4字节整数）
     */
    public void setD3A(int val){  // 设置通道3校准值A方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=16字节（位128开始）
        // 参数2：位宽度=4字节（32位）
        // 参数3：校准值A
        setVal(16, 4, val);  // 设置位128-159的值，宽度为4字节，值为val
    }

    /**
     * 设置通道3校准值B
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>设置通道3的零点校准值B</li>
     *   <li>数据位置：位160-191（4字节）</li>
     *   <li>用于补偿通道3的零点偏移</li>
     * </ul>
     *
     * @param val 校准值B（4字节整数）
     */
    public void setD3B(int val){  // 设置通道3校准值B方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=20字节（位160开始）
        // 参数2：位宽度=4字节（32位）
        // 参数3：校准值B
        setVal(20, 4, val);  // 设置位160-191的值，宽度为4字节，值为val
    }

    /**
     * 设置通道4校准值A
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>设置通道4的零点校准值A</li>
     *   <li>数据位置：位192-223（4字节）</li>
     *   <li>用于补偿通道4的零点偏移</li>
     * </ul>
     *
     * @param val 校准值A（4字节整数）
     */
    public void setD4A(int val){  // 设置通道4校准值A方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=24字节（位192开始）
        // 参数2：位宽度=4字节（32位）
        // 参数3：校准值A
        setVal(24, 4, val);  // 设置位192-223的值，宽度为4字节，值为val
    }

    /**
     * 设置通道4校准值B
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>设置通道4的零点校准值B</li>
     *   <li>数据位置：位224-255（4字节）</li>
     *   <li>用于补偿通道4的零点偏移</li>
     * </ul>
     *
     * @param val 校准值B（4字节整数）
     */
    public void setD4B(int val){  // 设置通道4校准值B方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=28字节（位224开始）
        // 参数2：位宽度=4字节（32位）
        // 参数3：校准值B
        setVal(28, 4, val);  // 设置位224-255的值，宽度为4字节，值为val
    }
}
