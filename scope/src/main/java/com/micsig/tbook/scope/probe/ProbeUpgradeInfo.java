package com.micsig.tbook.scope.probe;


import androidx.annotation.NonNull;

/**
 * ╔══════════════════════════════════════════════════════════════════════════════╗
 * ║                ProbeUpgradeInfo - 探头升级信息类                              ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【模块定位】                                                                 ║
 * ║   Probe模块的探头升级信息类，位于probe包下，                                    ║
 * ║   用于封装探头固件升级的状态和进度信息。                                        ║
 * ║                                                                              ║
 * ║ 【核心职责】                                                                 ║
 * ║   1. 定义升级状态常量（开始、数据传输、结束）                                  ║
 * ║   2. 封装升级进度数据（通道索引、状态、进度值）                                ║
 * ║   3. 提供线程安全的访问方法                                                   ║
 * ║   4. 提供字符串表示方法                                                       ║
 * ║                                                                              ║
 * ║ 【升级状态定义】                                                             ║
 * ║   ┌──────────────────────────────────────────────────────────────────────┐ ║
 * ║   │                        升级状态定义                                   │ ║
 * ║   │                                                                      │ ║
 * ║   │   UPGRADE_BEGIN (0)  - 升级开始                                      │ ║
 * ║   │   UPGRADE_DATA (1)   - 升级数据传输中                                │ ║
 * ║   │   UPGRADE_END (2)    - 升级结束                                      │ ║
 * ║   │                                                                      │ ║
 * ║   │   【状态转换流程】                                                    │ ║
 * ║   │                                                                      │ ║
 * ║   │   UPGRADE_BEGIN ──▶ UPGRADE_DATA ──▶ UPGRADE_END                    │ ║
 * ║   │       (0)              (1)              (2)                          │ ║
 * ║   │                                                                      │ ║
 * ║   │   【进度值含义】                                                      │ ║
 * ║   │   - UPGRADE_BEGIN: progress = 0（固定）                             │ ║
 * ║   │   - UPGRADE_DATA:  progress = 0~100（百分比）                       │ ║
 * ║   │   - UPGRADE_END:   progress = 100（成功）或 -1（失败）              │ ║
 * ║   └──────────────────────────────────────────────────────────────────────┘ ║
 * ║                                                                              ║
 * ║ 【数据结构】                                                                 ║
 * ║   ┌─────────────────────────────────────────────────────────────────────┐ ║
 * ║   │                         ProbeUpgradeInfo                            │ ║
 * ║   │                                                                     │ ║
 * ║   │   ┌──────────────────────────────────────────────────────────┐     │ ║
 * ║   │   │                    升级信息字段                            │     │ ║
 * ║   │   │                                                          │     │ ║
 * ║   │   │   chIdx      通道索引（0-3）                             │     │ ║
 * ║   │   │   state      升级状态（0=开始, 1=数据, 2=结束）          │     │ ║
 * ║   │   │   progress   升级进度（0-100或-1）                       │     │ ║
 * ║   │   └──────────────────────────────────────────────────────────┘     │ ║
 * ║   └─────────────────────────────────────────────────────────────────────┘   ║
 * ║                                                                              ║
 * ║ 【使用流程】                                                                 ║
 * ║   ┌─────────────┐    ┌─────────────┐    ┌─────────────┐                   ║
 * ║   │ ProbeUpgrade │───▶│ 创建信息对象 │───▶│ 发送事件通知 │                   ║
 * ║   │   线程       │    │ProbeUpgradeInfo│   │EventFactory │                   ║
 * ║   └─────────────┘    └─────────────┘    └─────────────┘                   ║
 * ║                                                                              ║
 * ║ 【应用场景】                                                                 ║
 * ║   1. ProbeUpgrade发送升级进度事件                                           ║
 * ║   2. UI层接收并显示升级进度                                                  ║
 * ║   3. 升级状态监控和日志记录                                                  ║
 * ║                                                                              ║
 * ║ 【线程安全】                                                                 ║
 * ║   - 所有getter方法使用synchronized保护                                      ║
 * ║   - 不可变对象，创建后不应修改                                                ║
 * ║                                                                              ║
 * ║ 【依赖关系】                                                                 ║
 * ║   - ProbeUpgrade: 创建ProbeUpgradeInfo实例                                 ║
 * ║   - EventFactory: 使用ProbeUpgradeInfo作为事件数据                         ║
 * ║   - EventBase: 封装ProbeUpgradeInfo作为事件参数                            ║
 * ║                                                                              ║
 * ║ 【作者】 MHO项目组                                                           ║
 * ╚══════════════════════════════════════════════════════════════════════════════╝
 */
public class ProbeUpgradeInfo {

    // ═══════════════════════════════════════════════════════════════════════════════
    // 升级状态常量定义
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 升级开始状态
     * 表示升级流程已启动，准备开始传输数据
     * 此时progress值固定为0
     */
    public static final int UPGRADE_BEGIN = 0;

    /**
     * 升级数据传输状态
     * 表示正在传输固件数据包
     * 此时progress值为0~100的百分比
     */
    public static final int UPGRADE_DATA = 1;

    /**
     * 升级结束状态
     * 表示升级流程已完成
     * progress值：100=成功，-1=失败
     */
    public static final int UPGRADE_END = 2;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 通道索引
     * 标识待升级探头所属的通道（0-3）
     */
    private int chIdx;

    /**
     * 升级状态
     * 取值范围：UPGRADE_BEGIN(0)、UPGRADE_DATA(1)、UPGRADE_END(2)
     */
    private int state;

    /**
     * 升级进度
     * 取值范围：
     * - 0~100: 升级进度百分比
     * - -1: 升级失败
     * - 100: 升级成功
     */
    private int progress;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造ProbeUpgradeInfo实例
     * 创建升级信息对象，包含通道索引、状态和进度
     *
     * @param chIdx 通道索引（0-3）
     * @param state 升级状态（UPGRADE_BEGIN、UPGRADE_DATA、UPGRADE_END）
     * @param progress 升级进度（0-100或-1）
     */
    public ProbeUpgradeInfo(int chIdx,int state,int progress){

        this.chIdx = chIdx;                                                         // 设置通道索引
        this.state = state;                                                         // 设置升级状态
        this.progress = progress;                                                   // 设置升级进度
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // Getter方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取通道索引
     * 线程安全方法
     *
     * @return 通道索引（0-3）
     */
    public synchronized int getChIdx() {
        return chIdx;                                                               // 返回通道索引
    }

    /**
     * 获取升级状态
     * 线程安全方法
     *
     * @return 升级状态（UPGRADE_BEGIN、UPGRADE_DATA、UPGRADE_END）
     */
    public synchronized int getState() {
        return state;                                                               // 返回升级状态
    }

    /**
     * 获取升级进度
     * 线程安全方法
     *
     * @return 升级进度（0-100或-1）
     */
    public synchronized int getProgress() {
        return progress;                                                            // 返回升级进度
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 对象方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 返回对象的字符串表示
     * 用于调试和日志输出
     * 
     * <p><b>输出格式：</b></p>
     * <pre>
     * chIdx:0,sate:1,progress:50
     * </pre>
     *
     * @return 包含所有字段信息的字符串
     */
    @NonNull
    @Override
    public String toString() {
        return "chIdx:" + chIdx + ",sate:" + state + ",progress:" + progress;       // 返回字符串表示
    }
}
