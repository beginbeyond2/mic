package com.micsig.tbook.scope.Trigger;

/**
 * ╔══════════════════════════════════════════════════════════════════════════════╗
 * ║                    TriggerEdge - 边沿触发器类                                ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【模块定位】                                                                 ║
 * ║   Trigger模块的边沿触发器实现类，是最常用、最基础的触发方式。                 ║
 * ║   边沿触发通过检测信号的上升沿、下降沿或双边沿来实现波形捕获。               ║
 * ║                                                                              ║
 * ║ 【核心职责】                                                                 ║
 * ║   1. 管理触发边沿类型（上升沿/下降沿/双边沿）                                ║
 * ║   2. 管理触发耦合方式（直流/交流/高频抑制/低频抑制/噪声抑制）                ║
 * ║   3. 提供单触发源访问接口                                                    ║
 * ║   4. 触发参数变更通知                                                        ║
 * ║                                                                              ║
 * ║ 【继承体系】                                                                 ║
 * ║                          ┌─────────────────┐                                 ║
 * ║                          │   Trigger       │ ← 抽象基类                      ║
 * ║                          │   (abstract)    │                                 ║
 * ║                          └────────┬────────┘                                 ║
 * ║                                   │                                          ║
 * ║         ┌───────────┬─────────────┼─────────────┬───────────┐               ║
 * ║         │           │             │             │           │               ║
 * ║         ▼           ▼             ▼             ▼           ▼               ║
 * ║   ┌──────────┐ ┌──────────┐ ┌──────────┐ ┌──────────┐ ┌──────────┐         ║
 * ║   │Edge      │ │PulseWidth│ │Logic     │ │Slope     │ │TimeOut   │         ║
 * ║   │边沿触发  │ │脉宽触发  │ │逻辑触发  │ │斜率触发  │ │超时触发  │         ║
 * ║   │(单源)    │ │(单源)    │ │(多源)    │ │(单源)    │ │(单源)    │         ║
 * ║   └──────────┘ └──────────┘ └──────────┘ └──────────┘ └──────────┘         ║
 * ║        ↑                                                                   ║
 * ║      本类                                                                   ║
 * ║                                                                              ║
 * ║ 【边沿触发说明】                                                             ║
 * ║   边沿触发是最常用的触发方式，通过检测信号的边沿来实现波形捕获。             ║
 * ║                                                                              ║
 * ║   ┌────────────────┬────────────────────────────────────────────────────┐  ║
 * ║   │ 边沿类型       │ 说明                                                │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 上升沿(ASC)    │ 信号从低电平跳变到高电平时触发                      │  ║
 * ║   │                │ 适用于检测脉冲的起始位置                            │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 下降沿(DSC)    │ 信号从高电平跳变到低电平时触发                      │  ║
 * ║   │                │ 适用于检测脉冲的结束位置                            │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 双边沿(DUAL)   │ 上升沿和下降沿都触发                                │  ║
 * ║   │                │ 适用于检测所有边沿，如时钟信号                      │  ║
 * ║   └────────────────┴────────────────────────────────────────────────────┘  ║
 * ║                                                                              ║
 * ║ 【触发耦合说明】                                                             ║
 * ║   触发耦合决定了触发系统如何处理输入信号。                                   ║
 * ║                                                                              ║
 * ║   ┌────────────────┬────────────────────────────────────────────────────┐  ║
 * ║   │ 耦合方式       │ 说明                                                │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 直流(DIRECT)   │ 直接耦合，通过所有频率分量                          │  ║
 * ║   │                │ 适用于大多数信号                                    │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 交流(AC)       │ 阻断直流分量，只通过交流分量                        │  ║
 * ║   │                │ 适用于叠加在直流上的小信号                          │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 高频抑制(HFRS) │ 滤除高频噪声，只通过低频分量                        │  ║
 * ║   │                │ 适用于高频噪声干扰的信号                            │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 低频抑制(LFRS) │ 滤除低频分量，只通过高频分量                        │  ║
 * ║   │                │ 适用于低频干扰的信号                                │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 噪声抑制       │ 增加迟滞范围，抑制噪声误触发                        │  ║
 * ║   │ (NOISERS)      │ 适用于噪声较大的信号                                │  ║
 * ║   └────────────────┴────────────────────────────────────────────────────┘  ║
 * ║                                                                              ║
 * ║ 【依赖关系】                                                                 ║
 * ║   → Trigger: 继承触发器基类                                                 ║
 * ║   → TriggerEdgeAction: 边沿触发器动作代理                                   ║
 * ║   → TriggerFactory: 工厂类，管理触发器实例                                  ║
 * ║                                                                              ║
 * ║ 【使用场景】                                                                 ║
 * ║   1. 用户选择边沿触发类型时，TriggerFactory创建TriggerEdge实例             ║
 * ║   2. 用户切换触发边沿（上升沿/下降沿/双边沿）时                              ║
 * ║   3. 用户切换触发耦合方式时                                                 ║
 * ║   4. 用户选择触发源通道时                                                   ║
 * ║                                                                              ║
 * ║ 【设计模式】                                                                 ║
 * ║   - 模板方法模式（Template Method）：继承Trigger的抽象方法                 ║
 * ║   - 代理模式（Proxy）：通过TriggerEdgeAction发送消息                        ║
 * ║                                                                              ║
 * ║ 【线程安全】                                                                 ║
 * ║   触发边沿和耦合方式参数未加同步保护，建议在UI线程操作。                     ║
 * ║                                                                              ║
 * ║ 【作者】 Created by zhuzh on 2018/3/20                                       ║
 * ╚══════════════════════════════════════════════════════════════════════════════╝
 */
public class TriggerEdge extends Trigger{

    // ═══════════════════════════════════════════════════════════════════════════════
    // 触发边沿类型常量定义
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 上升沿触发
     * 当信号从低电平跳变到高电平时触发
     * 适用于检测脉冲的起始位置
     *
     * <p><b>波形示例：</b>
     * <pre>
     *      ┌──────
     *      │
     * ─────┘
     *      ↑
     *    触发点
     * </pre>
     */
    public static final int TET_ASC = 0;

    /**
     * 下降沿触发
     * 当信号从高电平跳变到低电平时触发
     * 适用于检测脉冲的结束位置
     *
     * <p><b>波形示例：</b>
     * <pre>
     * ─────┐
     *      │
     *      └──────
     *      ↓
     *    触发点
     * </pre>
     */
    public static final int TET_DSC = 1;

    /**
     * 双边沿触发
     * 上升沿和下降沿都触发
     * 适用于检测所有边沿，如时钟信号
     *
     * <p><b>波形示例：</b>
     * <pre>
     *      ┌──────┐      ┌──────
     *      │      │      │
     * ─────┘      └──────┘
     *      ↑      ↑      ↑
     *    触发点  触发点  触发点
     * </pre>
     */
    public static final int TET_DUAL = 2;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 触发耦合方式常量定义
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 直流耦合
     * 直接耦合，通过所有频率分量
     * 适用于大多数信号测量
     *
     * <p><b>特点：</b>
     * <ul>
     *   <li>通过直流和交流分量</li>
     *   <li>信号无衰减</li>
     *   <li>最常用的耦合方式</li>
     * </ul>
     */
    public static final int COUPLING_DIRECT = 0;

    /**
     * 交流耦合
     * 阻断直流分量，只通过交流分量
     * 适用于叠加在直流上的小信号
     *
     * <p><b>特点：</b>
     * <ul>
     *   <li>阻断直流分量</li>
     *   <li>只通过交流分量</li>
     *   <li>适用于观察叠加在直流上的小信号</li>
     * </ul>
     *
     * <p><b>示例：</b>
     * <pre>
     * 原始信号：直流5V + 交流0.1V
     * 交流耦合后：只显示0.1V的交流信号
     * </pre>
     */
    public static final int COUPLING_AC = 1;

    /**
     * 高频抑制耦合
     * 滤除高频噪声，只通过低频分量
     * 适用于高频噪声干扰的信号
     *
     * <p><b>特点：</b>
     * <ul>
     *   <li>滤除高频噪声</li>
     *   <li>只通过低频分量</li>
     *   <li>适用于高频噪声干扰的信号</li>
     * </ul>
     */
    public static final int COUPLING_HFRS = 2;

    /**
     * 低频抑制耦合
     * 滤除低频分量，只通过高频分量
     * 适用于低频干扰的信号
     *
     * <p><b>特点：</b>
     * <ul>
     *   <li>滤除低频分量</li>
     *   <li>只通过高频分量</li>
     *   <li>适用于低频干扰的信号</li>
     * </ul>
     */
    public static final int COUPLING_LFRS = 3;

    /**
     * 噪声抑制耦合
     * 增加迟滞范围，抑制噪声误触发
     * 适用于噪声较大的信号
     *
     * <p><b>特点：</b>
     * <ul>
     *   <li>增加迟滞范围</li>
     *   <li>抑制噪声误触发</li>
     *   <li>适用于噪声较大的信号</li>
     * </ul>
     */
    public static final int COUPLING_NOISERS = 4;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 当前触发边沿类型
     * 取值：TET_ASC、TET_DSC 或 TET_DUAL
     * 默认值：TET_ASC（上升沿触发）
     */
    private int triggerEdge = TET_ASC;

    /**
     * 当前触发耦合方式
     * 取值：COUPLING_DIRECT、COUPLING_AC、COUPLING_HFRS、COUPLING_LFRS 或 COUPLING_NOISERS
     * 默认值：COUPLING_DIRECT（直流耦合）
     */
    private int triggerCouple = COUPLING_DIRECT;

    /**
     * 边沿触发器动作代理
     * 负责触发参数变更时发送FPGA消息和事件通知
     */
    private TriggerEdgeAction triggerEdgeAction;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造边沿触发器实例
     * 调用父类Trigger构造方法设置触发类型为边沿触发
     *
     * <p><b>初始化流程：</b>
     * <ol>
     *   <li>调用父类Trigger构造方法，设置触发类型为TRIG_TYPE_EDGE</li>
     *   <li>创建TriggerEdgeAction动作代理</li>
     * </ol>
     *
     * @example
     *   TriggerEdge trigger = new TriggerEdge();
     */
    public TriggerEdge() {
        super(Trigger.TRIG_TYPE_EDGE);
        triggerEdgeAction = new TriggerEdgeAction(this);
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 触发边沿类型访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取当前触发边沿类型
     *
     * @return 触发边沿类型
     *         TET_ASC (0): 上升沿触发
     *         TET_DSC (1): 下降沿触发
     *         TET_DUAL (2): 双边沿触发
     */
    public int getTriggerEdge() {
        return triggerEdge;
    }

    /**
     * 设置触发边沿类型
     * 更新触发边沿并发送FPGA消息
     *
     * @param triggerEdge 触发边沿类型
     *                    TET_ASC: 上升沿触发
     *                    TET_DSC: 下降沿触发
     *                    TET_DUAL: 双边沿触发
     *
     * @note 设置后会发送以下FPGA命令：
     *       - FPGA_CMD_TRIG: 触发参数
     *       - FPGA_CMD_TRIG_HUICHA: 触发回差
     *       - FPGA_CMD_TRIG_LEVEL: 触发电平
     *       - FPGA_CMD_TRIG_COUPLE: 触发耦合
     *
     * @see TriggerEdgeAction#TriggerEdgeChange()
     */
    public void setTriggerEdge(int triggerEdge) {
        this.triggerEdge = triggerEdge;
        triggerEdgeAction.TriggerEdgeChange();

    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 触发耦合方式访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取当前触发耦合方式
     *
     * @return 触发耦合方式
     *         COUPLING_DIRECT (0): 直流耦合
     *         COUPLING_AC (1): 交流耦合
     *         COUPLING_HFRS (2): 高频抑制耦合
     *         COUPLING_LFRS (3): 低频抑制耦合
     *         COUPLING_NOISERS (4): 噪声抑制耦合
     */
    public int getTriggerCouple() {
        return triggerCouple;
    }

    /**
     * 设置触发耦合方式
     * 更新触发耦合并发送FPGA消息
     *
     * @param triggerCouple 触发耦合方式
     *                      COUPLING_DIRECT: 直流耦合
     *                      COUPLING_AC: 交流耦合
     *                      COUPLING_HFRS: 高频抑制耦合
     *                      COUPLING_LFRS: 低频抑制耦合
     *                      COUPLING_NOISERS: 噪声抑制耦合
     *
     * @note 设置后会发送以下FPGA命令：
     *       - FPGA_CMD_TRIG_COUPLE: 触发耦合
     *       - FPGA_CMD_TRIG_LEVEL: 触发电平
     *
     * @see TriggerEdgeAction#TriggerCoupleChange()
     */
    public void setTriggerCouple(int triggerCouple) {
        this.triggerCouple = triggerCouple;
        triggerEdgeAction.TriggerCoupleChange();
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 抽象方法实现 - 继承自Trigger
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取触发源数量
     * 边沿触发只支持单触发源
     *
     * @return 始终返回1，表示单触发源
     */
    @Override
    public int getTriggerSourceCnt() {
        return 1;
    }

    /**
     * 触发源变更通知
     * 调用父类方法发送FPGA消息，并通过TriggerEdgeAction发送额外的FPGA消息
     *
     * <p><b>发送的消息：</b>
     * <ol>
     *   <li>父类方法发送：
     *     <ul>
     *       <li>sendChSample(): 更新通道采样状态</li>
     *       <li>FPGA_CMD_TRIG: 触发参数</li>
     *       <li>FPGA_CMD_TRIG_OFFSET: 触发偏移</li>
     *       <li>FPGA_CMD_TRIG_HUICHA: 触发回差</li>
     *       <li>FPGA_CMD_TRIG_LEVEL: 触发电平</li>
     *     </ul>
     *   </li>
     *   <li>TriggerEdgeAction发送：
     *     <ul>
     *       <li>FPGA_CMD_TRIG: 触发参数</li>
     *       <li>FPGA_CMD_TRIG_OFFSET: 触发偏移</li>
     *       <li>FPGA_CMD_TRIG_HUICHA: 触发回差</li>
     *       <li>FPGA_CMD_TRIG_LEVEL: 触发电平</li>
     *     </ul>
     *   </li>
     * </ol>
     *
     * @see Trigger#triggerSourceChange()
     * @see TriggerEdgeAction#TriggerSourceChange()
     */
    @Override
    public void triggerSourceChange() {
        super.triggerSourceChange();
        triggerEdgeAction.TriggerSourceChange();
    }

    /**
     * 判断是否支持多触发源
     * 边沿触发只支持单触发源
     *
     * @return 始终返回false，表示只支持单触发源
     */
    @Override
    protected boolean isMultitriggerSource() {
        return false;
    }
}
