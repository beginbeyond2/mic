package com.micsig.tbook.scope.Trigger;

/**
 * ╔══════════════════════════════════════════════════════════════════════════════╗
 * ║                 TriggerPulseWidth - 脉宽触发器类                             ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【模块定位】                                                                 ║
 * ║   Trigger模块的脉宽触发器实现类，根据脉冲宽度条件触发采集。                   ║
 * ║   脉宽触发是一种高级触发方式，适用于检测特定宽度的脉冲信号。                  ║
 * ║                                                                              ║
 * ║ 【核心职责】                                                                 ║
 * ║   1. 管理脉冲极性（正脉冲/负脉冲/任意脉冲）                                  ║
 * ║   2. 管理脉宽条件（小于/大于/等于/不等于）                                   ║
 * ║   3. 管理脉宽时间参数                                                        ║
 * ║   4. 提供单触发源访问接口                                                    ║
 * ║                                                                              ║
 * ║ 【继承体系】                                                                 ║
 * ║                          ┌─────────────────┐                                 ║
 * ║                          │   Trigger       │ ← 抽象基类                      ║
 * ║                          │   (abstract)    │                                 ║
 * ║                          └────────┬────────┘                                 ║
 * ║                                   │                                          ║
 * ║         ┌───────────┬─────────────┼─────────────┬───────────┐               ║
 * ║         │           │             │             │           │               ║
 * ║         ▼           ▼             ▼             ▼           ▼               ║
 * ║   ┌──────────┐ ┌──────────┐ ┌──────────┐ ┌──────────┐ ┌──────────┐         ║
 * ║   │Edge      │ │PulseWidth│ │Logic     │ │Slope     │ │TimeOut   │         ║
 * ║   │边沿触发  │ │脉宽触发  │ │逻辑触发  │ │斜率触发  │ │超时触发  │         ║
 * ║   │(单源)    │ │(单源)    │ │(多源)    │ │(单源)    │ │(单源)    │         ║
 * ║   └──────────┘ └──────────┘ └──────────┘ └──────────┘ └──────────┘         ║
 * ║                      ↑                                                      ║
 * ║                    本类                                                     ║
 * ║                                                                              ║
 * ║ 【脉宽触发说明】                                                             ║
 * ║   脉宽触发是一种高级触发方式，根据脉冲宽度条件触发采集。                      ║
 * ║   适用于检测特定宽度的脉冲信号，如通信协议、脉冲编码等。                      ║
 * ║                                                                              ║
 * ║   ┌─────────────────────────────────────────────────────────────────────┐   ║
 * ║   │                     脉宽触发示意图                                   │   ║
 * ║   │                                                                     │   ║
 * ║   │  正脉冲：                                                           │   ║
 * ║   │      ┌──────────┐                                                  │   ║
 * ║   │      │          │                                                  │   ║
 * ║   │      │  脉宽    │                                                  │   ║
 * ║   │ ─────┘          └──────                                            │   ║
 * ║   │      ←── pwTime ──→                                                 │   ║
 * ║   │                                                                     │   ║
 * ║   │  负脉冲：                                                           │   ║
 * ║   │                  ┌──────────┐                                      │   ║
 * ║   │                  │          │                                      │   ║
 * ║   │                  │  脉宽    │                                      │   ║
 * ║   │ ─────────────────┘          └──────                                │   ║
 * ║   │                  ←── pwTime ──→                                     │   ║
 * ║   │                                                                     │   ║
 * ║   │  当脉冲宽度满足设定条件时，触发采集                                  │   ║
 * ║   └─────────────────────────────────────────────────────────────────────┘   ║
 * ║                                                                              ║
 * ║ 【脉冲极性说明】                                                             ║
 * ║   ┌────────────────┬────────────────────────────────────────────────────┐  ║
 * ║   │ 极性           │ 说明                                                │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 正脉冲(POS)    │ 检测从低电平到高电平的脉冲                          │  ║
 * ║   │                │ 适用于检测正脉冲信号                                │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 负脉冲(NEG)    │ 检测从高电平到低电平的脉冲                          │  ║
 * ║   │                │ 适用于检测负脉冲信号                                │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 任意脉冲(EITHER│ 检测正脉冲和负脉冲                                  │  ║
 * ║   │                │ 适用于检测任意极性的脉冲                            │  ║
 * ║   └────────────────┴────────────────────────────────────────────────────┘  ║
 * ║                                                                              ║
 * ║ 【脉宽条件说明】                                                             ║
 * ║   ┌────────────────┬────────────────────────────────────────────────────┐  ║
 * ║   │ 条件           │ 说明                                                │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 小于(LESSER)   │ 脉冲宽度小于设定值时触发                            │  ║
 * ║   │                │ 适用于检测窄脉冲                                    │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 大于(GREATER)  │ 脉冲宽度大于设定值时触发                            │  ║
 * ║   │                │ 适用于检测宽脉冲                                    │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 等于(EQUAL)    │ 脉冲宽度等于设定值时触发                            │  ║
 * ║   │                │ 适用于检测特定宽度的脉冲                            │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 不等于(NOT_EQUAL)│ 脉冲宽度不等于设定值时触发                        │  ║
 * ║   │                │ 适用于排除特定宽度的脉冲                            │  ║
 * ║   └────────────────┴────────────────────────────────────────────────────┘  ║
 * ║                                                                              ║
 * ║ 【依赖关系】                                                                 ║
 * ║   → Trigger: 继承触发器基类                                                 ║
 * ║   → TriggerFactory: 工厂类，管理触发器实例                                  ║
 * ║                                                                              ║
 * ║ 【使用场景】                                                                 ║
 * ║   1. 用户选择脉宽触发类型时，TriggerFactory创建TriggerPulseWidth实例       ║
 * ║   2. 用户设置脉冲极性（正脉冲/负脉冲/任意脉冲）时                            ║
 * ║   3. 用户设置脉宽条件（小于/大于/等于/不等于）时                             ║
 * ║   4. 用户设置脉宽时间参数时                                                 ║
 * ║                                                                              ║
 * ║ 【设计模式】                                                                 ║
 * ║   - 模板方法模式（Template Method）：继承Trigger的抽象方法                 ║
 * ║                                                                              ║
 * ║ 【线程安全】                                                                 ║
 * ║   触发参数未加同步保护，建议在UI线程操作。                                   ║
 * ║                                                                              ║
 * ║ 【作者】 Created by zhuzh on 2018-6-1                                        ║
 * ╚══════════════════════════════════════════════════════════════════════════════╝
 */
public class TriggerPulseWidth extends Trigger {

    // ═══════════════════════════════════════════════════════════════════════════════
    // 脉冲极性常量定义
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 正脉冲极性
     * 检测从低电平到高电平的脉冲
     * 适用于检测正脉冲信号
     *
     * <p><b>波形示例：</b>
     * <pre>
     *      ┌──────────┐
     *      │          │
     *      │  脉宽    │
     * ─────┘          └──────
     * </pre>
     */
    public static final int PW_POLARITY_POSITIVE = 0;

    /**
     * 负脉冲极性
     * 检测从高电平到低电平的脉冲
     * 适用于检测负脉冲信号
     *
     * <p><b>波形示例：</b>
     * <pre>
     *                  ┌──────────┐
     *                  │          │
     *                  │  脉宽    │
     * ─────────────────┘          └──────
     * </pre>
     */
    public static final int PW_POLARITY_NEGATIVE = 1;

    /**
     * 任意脉冲极性
     * 检测正脉冲和负脉冲
     * 适用于检测任意极性的脉冲
     */
    public static final int PW_POLARITY_EITHER = 2;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 脉宽条件常量定义
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 脉宽条件：小于
     * 脉冲宽度小于设定值时触发
     * 适用于检测窄脉冲
     */
    public static final int PW_RELATION_LESSER = TRIGGER_RELATION_LESS_THAN;

    /**
     * 脉宽条件：大于
     * 脉冲宽度大于设定值时触发
     * 适用于检测宽脉冲
     */
    public static final int PW_RELATION_GREATER = TRIGGER_RELATION_MORE_THAN;

    /**
     * 脉宽条件：等于
     * 脉冲宽度等于设定值时触发
     * 适用于检测特定宽度的脉冲
     */
    public static final int PW_RELATION_EQUAL = TRIGGER_RELATION_EQUAL;

    /**
     * 脉宽条件：不等于
     * 脉冲宽度不等于设定值时触发
     * 适用于排除特定宽度的脉冲
     */
    public static final int PW_RELATION_NOT_EQUAL = TRIGGER_RELATION_NOT_EQUAL;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 当前脉冲极性
     * 取值：PW_POLARITY_POSITIVE、PW_POLARITY_NEGATIVE 或 PW_POLARITY_EITHER
     * 默认值：PW_POLARITY_POSITIVE（正脉冲）
     */
    private int polarity = PW_POLARITY_POSITIVE;

    /**
     * 当前脉宽条件
     * 取值：PW_RELATION_LESSER、PW_RELATION_GREATER、PW_RELATION_EQUAL 或 PW_RELATION_NOT_EQUAL
     * 默认值：PW_RELATION_LESSER（小于）
     */
    private int condition = PW_RELATION_LESSER;

    /**
     * 脉宽时间（FPGA单位）
     * 单位：纳秒
     * 默认值：8ns
     *
     * 用于设置脉宽条件的比较值
     * 当脉冲宽度满足条件时触发采集
     */
    private long pwTime = 8;

    /**
     * 高电平时间（FPGA单位）
     * 单位：纳秒
     * 默认值：8ns
     *
     * 用于设置高电平持续时间的阈值
     */
    private long timeHigh = 8;

    /**
     * 低电平时间（FPGA单位）
     * 单位：纳秒
     * 默认值：8ns
     *
     * 用于设置低电平持续时间的阈值
     */
    private long timeLow = 8;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造脉宽触发器实例
     * 调用父类Trigger构造方法设置触发类型为脉宽触发
     *
     * <p><b>初始化流程：</b>
     * <ol>
     *   <li>调用父类Trigger构造方法，设置触发类型为TRIG_TYPE_PULSE</li>
     * </ol>
     *
     * @example
     *   TriggerPulseWidth trigger = new TriggerPulseWidth();
     */
    public TriggerPulseWidth() {
        super(Trigger.TRIG_TYPE_PULSE);
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 脉冲极性访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取当前脉冲极性
     *
     * @return 脉冲极性
     *         PW_POLARITY_POSITIVE (0): 正脉冲
     *         PW_POLARITY_NEGATIVE (1): 负脉冲
     *         PW_POLARITY_EITHER (2): 任意脉冲
     */
    public int getPolarity() {
        return polarity;
    }

    /**
     * 设置脉冲极性
     * 更新脉冲极性并发送FPGA消息
     *
     * @param polarity 脉冲极性
     *                 PW_POLARITY_POSITIVE: 正脉冲
     *                 PW_POLARITY_NEGATIVE: 负脉冲
     *                 PW_POLARITY_EITHER: 任意脉冲
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setPolarity(int polarity) {
        this.polarity = polarity;
        triggerParamChange();
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 脉宽条件访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取当前脉宽条件
     *
     * @return 脉宽条件
     *         PW_RELATION_LESSER: 小于
     *         PW_RELATION_GREATER: 大于
     *         PW_RELATION_EQUAL: 等于
     *         PW_RELATION_NOT_EQUAL: 不等于
     */
    public int getCondition() {
        return condition;
    }

    /**
     * 设置脉宽条件
     * 更新脉宽条件并发送FPGA消息
     *
     * @param condition 脉宽条件
     *                  PW_RELATION_LESSER: 小于
     *                  PW_RELATION_GREATER: 大于
     *                  PW_RELATION_EQUAL: 等于
     *                  PW_RELATION_NOT_EQUAL: 不等于
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setCondition(int condition) {
        this.condition = condition;
        triggerParamChange();
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 脉宽时间访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取脉宽时间
     *
     * @return 脉宽时间（纳秒）
     */
    public long getPwTime() {
        return pwTime;
    }

    /**
     * 设置脉宽时间
     * 更新脉宽时间并发送FPGA消息
     *
     * @param pwTime 脉宽时间（纳秒）
     *               用于设置脉宽条件的比较值
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setPwTime(long pwTime) {
        this.pwTime = pwTime;
        triggerParamChange();
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 高低电平时间访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取高电平时间
     *
     * @return 高电平时间（纳秒）
     */
    public long getTimeHigh() {
        return timeHigh;
    }

    /**
     * 设置高电平时间
     * 更新高电平时间并发送FPGA消息
     *
     * @param timeHigh 高电平时间（纳秒）
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setTimeHigh(long timeHigh) {
        this.timeHigh = timeHigh;
        triggerParamChange();
    }

    /**
     * 获取低电平时间
     *
     * @return 低电平时间（纳秒）
     */
    public long getTimeLow() {
        return timeLow;
    }

    /**
     * 设置低电平时间
     * 更新低电平时间并发送FPGA消息
     *
     * @param timeLow 低电平时间（纳秒）
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setTimeLow(long timeLow) {
        this.timeLow = timeLow;
        triggerParamChange();
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 抽象方法实现 - 继承自Trigger
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取触发源数量
     * 脉宽触发只支持单触发源
     *
     * @return 始终返回1，表示单触发源
     */
    @Override
    public int getTriggerSourceCnt() {
        return 1;
    }

    /**
     * 判断是否支持多触发源
     * 脉宽触发只支持单触发源
     *
     * @return 始终返回false，表示只支持单触发源
     */
    @Override
    protected boolean isMultitriggerSource() {
        return false;
    }

}
