/**
 * 示波器FPGA版本寄存器类
 * 继承自FPGAReg基类，负责读取FPGA固件版本号
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>读取FPGA固件版本号</li>
 *   <li>提供版本号访问接口</li>
 *   <li>管理接收寄存器对象</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建版本寄存器对象
 * FPGAReg_VER reg = new FPGAReg_VER();
 *
 * // 重置寄存器（准备读取）
 * reg.reset(true);
 *
 * // 设置FPGA索引
 * reg.setFpgaIdx(0);
 *
 * // 发送读取命令
 * FPGACommand.getInstance().recvCommand(reg, reg.getRecvReg());
 *
 * // 获取版本号
 * int version = reg.getVersion();
 * Log.d(TAG, "FPGA version = " + Integer.toHexString(version));
 * </pre>
 *
 * @see FPGAReg
 * @see FPGACommand
 * @see FPGABoot
 */
public class FPGAReg_VER extends FPGAReg {                                               // 类声明：版本寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 接收寄存器对象
     * 用于存储从FPGA读取的版本号数据
     * 与发送寄存器分离，实现读写分离
     */
    private FPGAReg recvReg;                                                             // 成员变量：接收寄存器对象

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化版本寄存器
     * 
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_STATUS_VER</li>
     *   <li>数据长度：4字节（32位）</li>
     *   <li>操作类型：读取操作（true）</li>
     * </ul>
     * 
     * <p><b>双寄存器设计：</b></p>
     * <ul>
     *   <li>发送寄存器：用于发送读取命令</li>
     *   <li>接收寄存器：用于存储读取的数据</li>
     * </ul>
     */
    public FPGAReg_VER() {                                                               // 构造方法：初始化版本寄存器
        super(FPGA_STATUS_VER, 4, true);                                                 // 调用父类构造函数，设置寄存器ID、数据长度和读取标志
        recvReg = new FPGAReg(FPGA_STATUS_VER,4);                                        // 创建接收寄存器对象，用于存储读取的数据
    }                                                                                    // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取FPGA版本号
     * 从接收寄存器中读取版本号数据
     * 
     * <p><b>返回值说明：</b></p>
     * <ul>
     *   <li>类型：32位整数</li>
     *   <li>格式：由固件定义，通常包含主版本号、次版本号、修订号</li>
     *   <li>示例：0x00010002 表示版本1.0.2</li>
     * </ul>
     * 
     * @return FPGA固件版本号（32位整数）
     */
    public int getVersion(){                                                             // 公有方法：获取FPGA版本号
        return recvReg.getVal(0);                                                        // 从接收寄存器的偏移量0处读取版本号
    }                                                                                    // 方法结束

    /**
     * 获取接收寄存器对象
     * 用于接收FPGA返回的版本号数据
     * 
     * <p><b>使用场景：</b></p>
     * <ul>
     *   <li>调用recvCommand时需要传入接收寄存器</li>
     *   <li>接收寄存器用于存储FPGA返回的数据</li>
     * </ul>
     * 
     * @return 接收寄存器对象
     */
    public FPGAReg getRecvReg(){                                                         // 公有方法：获取接收寄存器对象
        return recvReg;                                                                  // 返回接收寄存器对象
    }                                                                                    // 方法结束

}                                                                                        // 类结束