/**
 * 示波器FPGA触发偏移寄存器类
 * 继承自FPGAReg基类，负责管理FPGA触发偏移参数
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>设置触发偏移值</li>
 *   <li>控制触发点相对于采样数据的位置</li>
 *   <li>支持多通道的偏移设置</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建触发偏移寄存器对象
 * FPGAReg_TRIG_OFFSET reg = new FPGAReg_TRIG_OFFSET(FPGAReg.FPGA_TRIG_OFFSET_CH1);
 *
 * // 设置通道偏移（触发点居中）
 * reg.setChOffset(0, 0, 0);  // val=0, val1=0
 *
 * // 设置通道偏移（预触发，查看触发前波形）
 * reg.setChOffset(0, 100, 50);  // val=100, val1=50
 *
 * // 发送命令到FPGA
 * FPGACommand.getInstance().sendCommand(reg);
 * </pre>
 *
 * @see FPGAReg
 * @see FPGACommand
 * @see Trigger
 */
public class FPGAReg_TRIG_OFFSET extends FPGAReg{                                        // 类声明：触发偏移寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 日志标签
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 日志标签
     * 用于日志输出时标识此类
     */
    private static final String TAG = "FPGAReg_TRIG_OFFSET";                             // 静态常量：日志标签

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化触发偏移寄存器
     * 
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：由参数addr传入</li>
     *   <li>数据长度：16字节（128位）</li>
     * </ul>
     * 
     * <p><b>参数说明：</b></p>
     * <ul>
     *   <li>addr：寄存器地址，用于区分不同通道的触发偏移寄存器</li>
     *   <li>例如：FPGA_TRIG_OFFSET_CH1、FPGA_TRIG_OFFSET_CH2等</li>
     * </ul>
     * 
     * @param addr 寄存器地址
     */
    public FPGAReg_TRIG_OFFSET(int addr) {                                               // 构造方法：初始化触发偏移寄存器
        super(addr, 16);                                                                 // 调用父类构造函数，设置寄存器ID和数据长度
    }                                                                                    // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置通道偏移
     * 设置触发点相对于采样数据的位置
     * 
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>val：主要偏移值（24位），用于粗调触发位置</li>
     *   <li>val1：次要偏移值（8位），用于精调触发位置</li>
     *   <li>组合使用可以实现精确的偏移控制</li>
     * </ul>
     * 
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>chIdx：通道索引（0-3或其他有效通道索引）</li>
     *   <li>val：主要偏移值（24位，范围0到16777215）</li>
     *   <li>val1：次要偏移值（8位，范围0到255）</li>
     * </ul>
     * 
     * <p><b>典型值示例：</b></p>
     * <ul>
     *   <li>居中显示：val=0, val1=0（触发点在屏幕中央）</li>
     *   <li>预触发：val>0（触发点在屏幕右侧，查看触发前波形）</li>
     *   <li>后触发：val&lt;屏幕宽度（触发点在屏幕左侧，查看触发后波形）</li>
     * </ul>
     * 
     * @param chIdx 通道索引
     * @param val 主要偏移值（24位）
     * @param val1 次要偏移值（8位）
     */
    public void setChOffset(int chIdx,int val,int val1){                                 // 公有方法：设置通道偏移
//        Log.d(TAG, "setChOffset() called with: chIdx = [" + chIdx + "], val = [" + val + "], val1 = [" + val1 + "]");  // 已注释：调试日志
        setVal(8, 24, val);                                                              // 在偏移量8处设置24位的主要偏移值
        setVal(0, 8, val1);                                                              // 在偏移量0处设置8位的次要偏移值
    }                                                                                    // 方法结束

}                                                                                        // 类结束