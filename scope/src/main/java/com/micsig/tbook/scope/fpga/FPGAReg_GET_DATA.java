package com.micsig.tbook.scope.fpga;  // 定义包名：示波器FPGA寄存器管理模块

/**
 * FPGA数据获取寄存器类 - 用于配置示波器从FPGA读取波形数据的参数
 *
 * <p><b>模块定位：</b>
 * <ul>
 *   <li>所属模块：scope.fpga（示波器FPGA寄存器管理模块）</li>
 *   <li>架构层级：硬件抽象层 - FPGA数据读取配置</li>
 *   <li>设计模式：继承模式（继承FPGAReg基类）</li>
 *   <li>职责类型：数据读取配置器</li>
 * </ul>
 *
 * <p><b>核心职责：</b>
 * <ul>
 *   <li>配置数据读取的通道选择</li>
 *   <li>配置数据读取的状态标志</li>
 *   <li>配置数据读取的数量</li>
 *   <li>封装FPGA数据获取寄存器的位操作</li>
 * </ul>
 *
 * <p><b>设计目的：</b>
 * <ul>
 *   <li>提供波形数据读取的配置接口</li>
 *   <li>支持多通道数据读取</li>
 *   <li>支持不同状态下的数据读取</li>
 *   <li>支持指定数量的数据读取</li>
 * </ul>
 *
 * <p><b>寄存器结构：</b>
 * <pre>
 * FPGA_GET_DATA寄存器（16字节，4个4字节字段）
 * │
 * ├── 字段0（位0-31）：配置字段
 * │     ├── 位0-1：状态标志（Status）
 * │     │     └── 数据读取状态（0-3）
 * │     │
 * │     └── 位4-7：通道选择位（Channel）
 * │           └── 每位对应一个通道，(1 << ch) << 4
 * │           └── 例如：ch=0时，位4=1（CH1）
 * │           └── 例如：ch=1时，位5=1（CH2）
 * │
 * ├── 字段1（位32-63）：保留字段1
 * │     └── 设置为0
 * │
 * ├── 字段2（位64-95）：保留字段2
 * │     └── 设置为0
 * │
 * └── 字段3（位96-127）：数据数量（Num）
 *       └── 要读取的数据点数（20位，0-0xFFFFF）
 * </pre>
 *
 * <p><b>数据读取说明：</b>
 * <ul>
 *   <li>通道选择：指定要读取哪个通道的波形数据</li>
 *   <li>状态标志：指定数据读取的状态（如正常、溢出等）</li>
 *   <li>数据数量：指定要读取的数据点数</li>
 *   <li>保留字段：用于扩展或对齐</li>
 * </ul>
 *
 * <p><b>使用场景：</b>
 * <ul>
 *   <li>波形数据读取：读取采集的波形数据</li>
 *   <li>多通道读取：同时读取多个通道的数据</li>
 *   <li>分段读取：分段读取大量波形数据</li>
 *   <li>状态检查：读取特定状态下的数据</li>
 * </ul>
 *
 * <p><b>依赖关系：</b>
 * <ul>
 *   <li>继承：FPGAReg（FPGA寄存器基类）</li>
 *   <li>依赖：FPGACommand（FPGA命令处理类）</li>
 *   <li>依赖：Channel（通道类）</li>
 * </ul>
 *
 * <p><b>线程安全：</b>
 * <ul>
 *   <li>寄存器操作在FPGACommand中同步执行</li>
 *   <li>数据读取配置需要确保硬件稳定</li>
 * </ul>
 *
 * <p><b>使用示例：</b>
 * <pre>
 * // 创建数据获取寄存器实例
 * FPGAReg_GET_DATA regGetData = new FPGAReg_GET_DATA();
 *
 * // 配置读取通道1的1000个数据点
 * regGetData.setReg(0, 0, 1000);  // ch=0（CH1），status=0，num=1000
 *
 * // 发送寄存器配置到FPGA
 * fpgaCommand.sendCmd(regGetData);
 *
 * // 配置读取通道2的500个数据点
 * regGetData.setReg(1, 0, 500);  // ch=1（CH2），status=0，num=500
 * </pre>
 *
 * @author zhuzh
 * @version 1.0
 * @since 2018/10/25
 * @see FPGAReg FPGA寄存器基类
 * @see FPGACommand FPGA命令处理类
 */
public class FPGAReg_GET_DATA extends FPGAReg {  // 继承FPGAReg基类，复用寄存器操作方法

    /**
     * 构造函数 - 初始化数据获取寄存器
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>调用父类构造函数，设置寄存器地址和大小</li>
     *   <li>寄存器地址：FPGA_GET_DATA（数据获取寄存器）</li>
     *   <li>寄存器大小：16字节（4个4字节字段）</li>
     * </ul>
     *
     * <p><b>寄存器地址说明：</b>
     * <ul>
     *   <li>FPGA_GET_DATA：用于配置从FPGA读取波形数据的参数</li>
     *   <li>地址值由FPGAReg基类定义</li>
     * </ul>
     *
     * <p><b>寄存器大小说明：</b>
     * <ul>
     *   <li>16字节（128位）用于存储配置参数</li>
     *   <li>包含通道选择、状态标志、数据数量等字段</li>
     * </ul>
     */
    public FPGAReg_GET_DATA() {  // 构造函数
        // 调用父类构造函数，传入寄存器地址和大小
        // FPGA_GET_DATA：数据获取寄存器地址
        // 16：寄存器大小为16字节（4个4字节字段）
        super(FPGAReg.FPGA_GET_DATA, 16);  // 初始化数据获取寄存器，地址为FPGA_GET_DATA，大小为16字节
    }

    /**
     * 设置数据读取配置 - 配置通道、状态和数据数量
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>配置数据读取的通道选择</li>
     *   <li>配置数据读取的状态标志</li>
     *   <li>配置数据读取的数量</li>
     *   <li>设置保留字段为0</li>
     * </ul>
     *
     * <p><b>参数说明：</b>
     * <ul>
     *   <li>ch：通道索引（0=CH1，1=CH2，2=CH3，3=CH4）</li>
     *   <li>status：状态标志（0-3）</li>
     *   <li>num：数据数量（0-0xFFFFF，最多1048575个数据点）</li>
     * </ul>
     *
     * <p><b>配置字段计算：</b>
     * <ul>
     *   <li>字段0 = (status & 0x03) | ((1 << ch) << 4)</li>
     *   <li>status & 0x03：提取状态标志的低2位</li>
     *   <li>(1 << ch) << 4：计算通道选择位的位置</li>
     *   <li>例如：ch=0时，位4=1；ch=1时，位5=1；ch=2时，位6=1；ch=3时，位7=1</li>
     * </ul>
     *
     * <p><b>使用场景：</b>
     * <ul>
     *   <li>读取单通道数据：指定一个通道索引</li>
     *   <li>读取特定状态数据：指定状态标志</li>
     *   <li>读取指定数量数据：指定数据数量</li>
     * </ul>
     *
     * @param ch 通道索引（0=CH1，1=CH2，2=CH3，3=CH4）
     * @param status 状态标志（0-3）
     * @param num 数据数量（0-0xFFFFF，最多1048575个数据点）
     */
    public void setReg(int ch, int status, int num){  // 设置数据读取配置方法
        // 设置字段0：通道选择和状态标志
        // (status & 0x03)：提取状态标志的低2位（位0-1）
        // ((1 << ch) << 4)：计算通道选择位的位置（位4-7）
        // 例如：ch=0时，(1<<0)<<4 = 16（位4=1）
        // 例如：ch=1时，(1<<1)<<4 = 32（位5=1）
        setVal(0, (status & 0x03) | ((1 << ch) << 4));  // 设置字段0：状态标志（位0-1）+ 通道选择（位4-7）
        // 设置字段1：保留字段，设置为0
        setVal(1, 0);  // 设置字段1为0（保留字段）
        // 设置字段2：保留字段，设置为0
        setVal(2, 0);  // 设置字段2为0（保留字段）
        // 设置字段3：数据数量
        // (num & 0xFFFFF)：提取数据数量的低20位（最多1048575个数据点）
        setVal(3, (num & 0xFFFFF));  // 设置字段3：数据数量（20位，最多1048575）
    }
}
