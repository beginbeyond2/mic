/**
 * 示波器FPGA显示串行解码寄存器类
 * 继承自FPGAReg基类，负责管理FPGA串行解码显示参数
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>设置解码码位置</li>
 *   <li>设置解码码长度</li>
 *   <li>设置压缩参数</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建显示串行解码寄存器对象
 * FPGAReg_DISP_SERI reg = new FPGAReg_DISP_SERI();
 *
 * // 设置解码码位置
 * reg.setCodepos(100);  // 设置解码码位置为100
 *
 * // 设置解码码长度
 * reg.setCodelen(50);   // 设置解码码长度为50
 *
 * // 设置压缩参数
 * reg.setZipparam(1);   // 设置压缩参数为1
 *
 * // 发送命令到FPGA
 * FPGACommand.getInstance().sendCommand(reg);
 * </pre>
 *
 * @see FPGAReg
 * @see FPGACommand
 * @see SerialDecode
 * @see ProtocolDecode
 * @author zhuzh
 * @version 1.0
 * @since 2018-3-22
 */
public class FPGAReg_DISP_SERI extends FPGAReg {                                           // 类声明：显示串行解码寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化显示串行解码寄存器
     *
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_DISP_SERI</li>
     *   <li>数据长度：12字节（3个字，每个字32位）</li>
     * </ul>
     *
     * <p><b>寄存器地址说明：</b></p>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器地址映射                         │
     * ├─────────────────────────────────────────┤
     * │  FPGA_DISP_SERI                         │
     * │  功能: 配置串行解码显示参数             │
     * │  大小: 12字节（3个字）                  │
     * │  访问: 只写 (由FPGA读取)                │
     * │                                         │
     * │  数据结构:                              │
     * │  字0: 解码码位置                        │
     * │  字1: 解码码长度                        │
     * │  字2: 压缩参数                          │
     * └─────────────────────────────────────────┘
     * </pre>
     */
    public FPGAReg_DISP_SERI(){                                                            // 构造方法：初始化显示串行解码寄存器
        // 调用父类构造函数，设置寄存器ID和数据长度
        // FPGA_DISP_SERI：显示串行解码寄存器地址
        // 12：数据长度为12字节（3个32位字）
        super(FPGA_DISP_SERI,12);                                                           // 调用父类构造函数，设置寄存器ID为FPGA_DISP_SERI，长度12字节
    }                                                                                      // 构造方法结束

    /**
     * 构造函数（自定义地址和长度）
     * 初始化显示串行解码寄存器，使用自定义地址和长度
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>支持自定义寄存器地址和数据长度</li>
     *   <li>用于特殊场景或扩展功能</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>addr：寄存器地址</li>
     *   <li>len：数据长度（字节）</li>
     * </ul>
     *
     * @param addr 寄存器地址
     * @param len 数据长度（字节）
     */
    public FPGAReg_DISP_SERI(int addr, int len) {                                          // 构造方法：初始化显示串行解码寄存器（自定义地址和长度）
        // 调用父类构造函数，设置自定义寄存器地址和数据长度
        super(addr, len);                                                                  // 调用父类构造函数，设置寄存器地址为addr，长度为len字节
    }                                                                                      // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法 - 解码码参数设置
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置解码码位置
     * 设置解码码在屏幕上的显示位置
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>解码码位置决定了解码码在屏幕上的显示位置</li>
     *   <li>用于控制解码结果的显示位置</li>
     *   <li>值越大，显示位置越靠后</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>val：解码码位置值（32位）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <ul>
     *   <li>存储位置：字0（32位）</li>
     * </ul>
     *
     * @param val 解码码位置值
     */
    public void setCodepos(int val){                                                       // 公有方法：设置解码码位置
        // 调用父类setVal方法，设置字0的值
        setVal(0,val);                                                                     // 设置字0，值为val（解码码位置）
    }                                                                                      // 方法结束

    /**
     * 设置解码码长度
     * 设置解码码的显示长度
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>解码码长度决定了解码码的显示长度</li>
     *   <li>用于控制解码结果的显示范围</li>
     *   <li>值越大，显示范围越宽</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>val：解码码长度值（32位）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <ul>
     *   <li>存储位置：字1（32位）</li>
     * </ul>
     *
     * @param val 解码码长度值
     */
    public void setCodelen(int val){                                                       // 公有方法：设置解码码长度
        // 调用父类setVal方法，设置字1的值
        setVal(1,val);                                                                     // 设置字1，值为val（解码码长度）
    }                                                                                      // 方法结束

    /**
     * 设置压缩参数
     * 设置显示压缩参数
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>压缩参数用于控制显示压缩</li>
     *   <li>用于优化显示性能</li>
     *   <li>具体功能取决于FPGA实现</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>val：压缩参数值（32位）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <ul>
     *   <li>存储位置：字2（32位）</li>
     * </ul>
     *
     * @param val 压缩参数值
     */
    public void setZipparam(int val){                                                      // 公有方法：设置压缩参数
        // 调用父类setVal方法，设置字2的值
        setVal(2,val);                                                                     // 设置字2，值为val（压缩参数）
    }                                                                                      // 方法结束

}                                                                                          // 类结束