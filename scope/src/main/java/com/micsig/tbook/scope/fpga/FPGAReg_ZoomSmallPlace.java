/**
 * 示波器FPGA缩放小窗口位置寄存器类
 * 继承自FPGAReg基类，负责管理FPGA缩放小窗口的位置和数量参数
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>设置缩放小窗口的位置</li>
 *   <li>设置缩放小窗口的数量</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建缩放小窗口位置寄存器对象
 * FPGAReg_ZoomSmallPlace reg = new FPGAReg_ZoomSmallPlace();
 *
 * // 设置小窗口数量
 * reg.setNums(2);  // 显示2个小窗口
 *
 * // 设置小窗口位置
 * reg.setPlace(100);  // 位置在100像素处
 *
 * // 发送命令到FPGA
 * FPGACommand.getInstance().sendCommand(reg);
 * </pre>
 *
 * @see FPGAReg
 * @see FPGACommand
 */
public class FPGAReg_ZoomSmallPlace extends FPGAReg{                                     // 类声明：缩放小窗口位置寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化缩放小窗口位置寄存器
     * 
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_ZOOM_SMALL_PLACE</li>
     *   <li>数据长度：8字节（2个32位字）</li>
     * </ul>
     */
    public FPGAReg_ZoomSmallPlace() {                                                    // 构造方法：初始化缩放小窗口位置寄存器
        super(FPGAReg.FPGA_ZOOM_SMALL_PLACE, 8);                                         // 调用父类构造函数，设置寄存器ID和数据长度
    }                                                                                    // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置小窗口位置
     * 设置缩放小窗口在屏幕上的水平位置
     * 
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>位置以像素或采样点为单位</li>
     *   <li>位置范围：0到屏幕宽度</li>
     *   <li>用于控制小窗口的显示位置</li>
     * </ul>
     * 
     * @param place 小窗口位置（像素或采样点）
     */
    public void setPlace(int place){                                                     // 公有方法：设置小窗口位置
        setVal(1,place);                                                                 // 在偏移量1（第2个字）处设置位置值
    }                                                                                    // 方法结束

    /**
     * 设置小窗口数量
     * 设置同时显示的缩放小窗口个数
     * 
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>数量通常为1-4个</li>
     *   <li>多个小窗口可以同时查看不同区域</li>
     *   <li>数量为0表示不显示小窗口</li>
     * </ul>
     * 
     * @param nums 小窗口数量（通常为1-4）
     */
    public void setNums(int nums){                                                       // 公有方法：设置小窗口数量
        setVal(0,nums);                                                                  // 在偏移量0（第1个字）处设置数量值
    }                                                                                    // 方法结束

}                                                                                        // 类结束
