    // ═══════════════════════════════════════════════════════════════════════════════

    /** X方向偏移量（像素），默认值：0 */
    private int xOffset = 0;

    /** Y方向偏移量（像素），默认值：0 */
    private int yOffset = 0;

    /** 是否启用缩放，默认值：false */
    private boolean bZoom = false;

