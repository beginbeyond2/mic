package com.micsig.tbook.scope.fpga;  // 定义包名：示波器FPGA寄存器管理模块

import com.micsig.tbook.scope.Bus.IBus;  // 导入总线接口类，定义总线通用接口
import com.micsig.tbook.scope.Bus.LinBus;  // 导入LIN总线类，提供LIN总线配置参数

/**
 * FPGA LIN总线寄存器类 - 用于配置LIN总线解码和触发参数
 *
 * <p><b>模块定位：</b>
 * <ul>
 *   <li>所属模块：scope.fpga（示波器FPGA寄存器管理模块）</li>
 *   <li>架构层级：硬件抽象层 - FPGA寄存器配置</li>
 *   <li>设计模式：继承模式（继承FPGAReg_BUS基类）</li>
 *   <li>职责类型：LIN总线配置管理器</li>
 * </ul>
 *
 * <p><b>核心职责：</b>
 * <ul>
 *   <li>配置LIN总线的信号源</li>
 *   <li>配置LIN总线波特率</li>
 *   <li>配置LIN总线时钟源</li>
 *   <li>配置LIN帧ID和数据长度</li>
 *   <li>配置LIN触发模式和触发条件</li>
 *   <li>配置LIN总线空闲电平</li>
 * </ul>
 *
 * <p><b>设计目的：</b>
 * <ul>
 *   <li>提供LIN总线解码的硬件配置接口</li>
 *   <li>支持LIN协议的帧ID触发和数据触发</li>
 *   <li>支持不同波特率的LIN总线解码</li>
 *   <li>封装FPGA寄存器的位操作细节</li>
 * </ul>
 *
 * <p><b>寄存器结构：</b>
 * <pre>
 * LIN总线寄存器（160位，20字节）
 * │
 * ├── 字节0：信号源配置
 * │     └── 位2-3：LIN信号源选择（0-3）
 * │
 * ├── 字节1：帧ID配置
 * │     └── 位0-5：LIN帧ID（6位，0-63）
 * │
 * ├── 字节2-3：数据配置
 * │     ├── 字节2：数据高位
 * │     └── 字节3：数据低位
 * │
 * ├── 字节4：波特率配置
 * │     └── 位4-16：波特率分频值（13位）
 * │
 * ├── 字节5：时钟源配置
 * │     └── 位20：时钟源选择（0=低速时钟，1=高速时钟）
 * │
 * ├── 字节6：数据长度配置
 * │     └── 位21-23：数据长度（3位，0-7代表1-8字节）
 * │
 * └── 字节7：触发配置
 *       └── 位28-31：触发模式（4位）
 * </pre>
 *
 * <p><b>LIN总线说明：</b>
 * <ul>
 *   <li>LIN（Local Interconnect Network）是汽车电子中常用的低成本串行通信协议</li>
 *   <li>波特率范围：1-20kbps（低速模式）或20kbps以上（高速模式）</li>
 *   <li>帧结构：包含帧头（同步字段、ID字段）和帧响应（数据、校验）</li>
 *   <li>触发方式：帧ID触发、数据触发等</li>
 * </ul>
 *
 * <p><b>使用场景：</b>
 * <ul>
 *   <li>LIN总线解码：配置LIN总线解码参数</li>
 *   <li>LIN总线触发：配置LIN总线触发条件</li>
 *   <li>波特率设置：设置LIN总线波特率</li>
 *   <li>帧ID过滤：设置帧ID触发条件</li>
 * </ul>
 *
 * <p><b>依赖关系：</b>
 * <ul>
 *   <li>继承：FPGAReg_BUS（总线寄存器基类）</li>
 *   <li>依赖：LinBus（LIN总线配置类）</li>
 *   <li>依赖：FPGACommand（FPGA命令处理类）</li>
 *   <li>依赖：FPGAReg_BUS_LEVEL（总线电平寄存器）</li>
 * </ul>
 *
 * <p><b>使用示例：</b>
 * <pre>
 * // 创建LIN总线寄存器实例
 * FPGAReg_BUS_LIN reg = new FPGAReg_BUS_LIN(FPGA_BUS_LIN);
 *
 * // 配置LIN总线参数
 * reg.setSrc(0);           // 设置信号源为通道0
 * reg.setRate(7812);       // 设置波特率分频值
 * reg.setClk(0);           // 设置低速时钟
 * reg.setDataLength(3);    // 设置数据长度为4字节
 * reg.setTriggerMode(1);   // 设置触发模式为帧ID触发
 * reg.setId(0x10);         // 设置帧ID为0x10
 * reg.setDataL(0x12345678);// 设置数据低位
 * reg.setDataH(0x9ABCDEF0);// 设置数据高位
 *
 * // 发送寄存器配置到FPGA
 * FPGACommand.sendCmd(reg);
 * </pre>
 *
 * @author zhuzh
 * @version 1.0
 * @since 2018-5-30
 * @see FPGAReg_BUS 总线寄存器基类
 * @see LinBus LIN总线配置类
 * @see IBus 总线接口类
 */
public class FPGAReg_BUS_LIN extends FPGAReg_BUS {  // 继承FPGAReg_BUS基类，复用总线寄存器操作方法
    
    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════
    
    /**
     * 构造函数 - 初始化LIN总线寄存器
     *
     * <p><b>初始化参数：</b>
     * <ul>
     *   <li>寄存器地址：由参数addr指定</li>
     *   <li>寄存器大小：20字节（160位）</li>
     * </ul>
     *
     * <p><b>初始化操作：</b>
     * <ul>
     *   <li>调用父类构造函数，设置寄存器地址和大小</li>
     *   <li>寄存器大小固定为20字节</li>
     * </ul>
     *
     * @param addr 寄存器地址（FPGA总线寄存器地址）
     */
    public FPGAReg_BUS_LIN(int addr) {  // 构造方法：初始化LIN总线寄存器
        // 调用父类构造函数，设置寄存器地址和大小
        // addr：寄存器地址（由调用者指定）
        // 20：寄存器大小（20字节，160位）
        super(addr, 20);  // 调用父类构造函数，设置寄存器地址和大小
    }  // 构造方法结束
    
    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法 - 配置参数设置
    // ═══════════════════════════════════════════════════════════════════════════════
    
    /**
     * 设置信号源
     * 设置LIN总线的信号源通道
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>控制FPGA从哪个通道获取LIN总线信号</li>
     *   <li>信号源值：0-3（对应通道0-3）</li>
     *   <li>寄存器位：位2-3（2位）</li>
     * </ul>
     *
     * <p><b>信号源说明：</b>
     * <ul>
     *   <li>信号源决定了LIN总线信号的输入通道</li>
     *   <li>通常使用示波器的通道作为信号源</li>
     *   <li>支持多通道信号源选择</li>
     * </ul>
     *
     * @param val 信号源值（0-3，对应通道0-3）
     */
    public void setSrc(int val){  // 公有方法：设置信号源
        // 调用父类setVal方法，设置寄存器值
        // 参数1：字索引=0（第一个字）
        // 参数2：起始位=2（位2）
        // 参数3：位宽度=2（2位）
        // 参数4：信号源值
        setVal(0,2,val);  // 设置位2-3的值，宽度为2位，值为val（信号源）
    }  // 方法结束
    
    /**
     * 设置波特率分频值
     * 设置LIN总线的波特率分频参数
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>控制LIN总线的通信波特率</li>
     *   <li>波特率通过分频值计算</li>
     *   <li>寄存器位：位4-16（13位）</li>
     * </ul>
     *
     * <p><b>波特率计算：</b>
     * <ul>
     *   <li>低速模式（波特率≤20kbps）：波特率 = 7812500 / (分频值 + 1)</li>
     *   <li>高速模式（波特率>20kbps）：波特率 = 250000000 / (分频值 + 1)</li>
     *   <li>分频值范围：0-8191（13位）</li>
     * </ul>
     *
     * @param val 波特率分频值（0-8191）
     */
    public void setRate(int val){  // 公有方法：设置波特率分频值
        // 调用父类setVal方法，设置寄存器值
        // 参数1：字索引=0（第一个字）
        // 参数2：起始位=4（位4）
        // 参数3：位宽度=13（13位）
        // 参数4：波特率分频值
        setVal(4,13,val);  // 设置位4-16的值，宽度为13位，值为val（波特率分频值）
    }  // 方法结束
    
    /**
     * 设置时钟源
     * 设置LIN总线的时钟源选择
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>控制LIN总线使用的时钟源</li>
     *   <li>时钟源值：0=低速时钟，1=高速时钟</li>
     *   <li>寄存器位：位20（1位）</li>
     * </ul>
     *
     * <p><b>时钟源说明：</b>
     * <ul>
     *   <li>低速时钟：用于波特率≤20kbps的LIN总线</li>
     *   <li>高速时钟：用于波特率>20kbps的LIN总线</li>
     *   <li>时钟源选择影响波特率计算公式</li>
     * </ul>
     *
     * @param val 时钟源值（0=低速时钟，1=高速时钟）
     */
    public void setClk(int val){  // 公有方法：设置时钟源
        // 调用父类setVal方法，设置寄存器值
        // 参数1：字索引=0（第一个字）
        // 参数2：起始位=20（位20）
        // 参数3：位宽度=1（1位）
        // 参数4：时钟源值
        setVal(20,1,val);  // 设置位20的值，宽度为1位，值为val（时钟源）
    }  // 方法结束
    
    /**
     * 设置数据长度
     * 设置LIN帧的数据长度
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>控制LIN帧的数据字段长度</li>
     *   <li>数据长度值：0-7代表1-8字节</li>
     *   <li>寄存器位：位21-23（3位）</li>
     * </ul>
     *
     * <p><b>数据长度说明：</b>
     * <ul>
     *   <li>LIN帧数据长度为1-8字节</li>
     *   <li>寄存器值需要转换：寄存器值 = (数据长度 >> 1) - 1</li>
     *   <li>数据长度影响数据字段的解析</li>
     * </ul>
     *
     * @param length 数据长度寄存器值（0-7，代表1-8字节）
     */
    public void setDataLength(int length) {  // 公有方法：设置数据长度
        // 调用父类setVal方法，设置寄存器值
        // 参数1：字索引=0（第一个字）
        // 参数2：起始位=21（位21）
        // 参数3：位宽度=3（3位）
        // 参数4：数据长度值
        setVal(21, 3, length);  // 设置位21-23的值，宽度为3位，值为length（数据长度）
    }  // 方法结束
    
    /**
     * 设置触发模式
     * 设置LIN总线的触发模式
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>控制LIN总线的触发方式</li>
     *   <li>触发模式：帧ID触发、数据触发等</li>
     *   <li>寄存器位：位28-31（4位）</li>
     * </ul>
     *
     * <p><b>触发模式说明：</b>
     * <ul>
     *   <li>帧ID触发：在特定帧ID时触发</li>
     *   <li>数据触发：在特定数据时触发</li>
     *   <li>触发模式决定了触发条件</li>
     * </ul>
     *
     * @param val 触发模式值（0-15，对应不同触发模式）
     */
    public void setTriggerMode(int val){  // 公有方法：设置触发模式
        // 调用父类setVal方法，设置寄存器值
        // 参数1：字索引=0（第一个字）
        // 参数2：起始位=28（位28）
        // 参数3：位宽度=4（4位）
        // 参数4：触发模式值
        setVal(28,4,val);  // 设置位28-31的值，宽度为4位，值为val（触发模式）
    }  // 方法结束
    
    /**
     * 设置帧ID
     * 设置LIN帧的ID字段值
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>控制LIN帧的ID字段值</li>
     *   <li>帧ID值：0-63（6位）</li>
     *   <li>寄存器位：位0-5（6位）</li>
     * </ul>
     *
     * <p><b>帧ID说明：</b>
     * <ul>
     *   <li>LIN帧ID用于标识帧的类型和目的</li>
     *   <li>帧ID范围：0-63（6位）</li>
     *   <li>帧ID触发时使用此值作为触发条件</li>
     * </ul>
     *
     * @param val 帧ID值（0-63）
     */
    public void setId(int val){  // 公有方法：设置帧ID
        // 调用父类setVal方法，设置寄存器值
        // 参数1：字索引=1（第二个字）
        // 参数2：起始位=0（位0）
        // 参数3：位宽度=6（6位）
        // 参数4：帧ID值
        setVal(1,0,6,val);  // 设置位0-5的值，宽度为6位，值为val（帧ID）
    }  // 方法结束
    
    /**
     * 设置数据字段
     * 设置LIN帧的数据字段值
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>控制LIN帧的数据字段值</li>
     *   <li>数据字段索引：0-7（对应字节0-7）</li>
     *   <li>寄存器位置：字索引 = idx + 2</li>
     * </ul>
     *
     * <p><b>数据字段说明：</b>
     * <ul>
     *   <li>LIN帧数据字段最多8字节</li>
     *   <li>每个字节对应一个寄存器字</li>
     *   <li>数据触发时使用此值作为触发条件</li>
     * </ul>
     *
     * @param idx 数据字段索引（0-7，对应字节0-7）
     * @param val 数据字段值（0-255，8位）
     */
    public void setData(int idx,int val){  // 公有方法：设置数据字段
        // 调用父类setVal方法，设置寄存器值
        // 参数1：字索引=idx + 2（数据字段对应的字）
        // 参数2：数据字段值
        setVal(idx + 2,val);  // 设置字idx+2的值，值为val（数据字段）
    }  // 方法结束
    
    /**
     * 设置数据低位
     * 设置LIN帧数据的低位部分（字节4-7）
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>控制LIN帧数据的低位部分</li>
     *   <li>数据低位：字节4-7（32位）</li>
     *   <li>用于设置数据触发条件</li>
     * </ul>
     *
     * <p><b>数据低位说明：</b>
     * <ul>
     *   <li>数据低位对应字节4-7</li>
     *   <li>32位数据值</li>
     *   <li>需要根据数据长度进行字节序转换</li>
     * </ul>
     *
     * @param val 数据低位值（32位）
     */
    public void setDataL(int val){  // 公有方法：设置数据低位
        // 调用setData方法，设置数据字段索引为1的数据
        setData(1,val);  // 设置数据字段索引1的值（数据低位）
    }  // 方法结束
    
    /**
     * 设置数据高位
     * 设置LIN帧数据的高位部分（字节0-3）
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>控制LIN帧数据的高位部分</li>
     *   <li>数据高位：字节0-3（32位）</li>
     *   <li>用于设置数据触发条件</li>
     * </ul>
     *
     * <p><b>数据高位说明：</b>
     * <ul>
     *   <li>数据高位对应字节0-3</li>
     *   <li>32位数据值</li>
     *   <li>需要根据数据长度进行字节序转换</li>
     * </ul>
     *
     * @param val 数据高位值（32位）
     */
    public void setDataH(int val){  // 公有方法：设置数据高位
        // 调用setData方法，设置数据字段索引为0的数据
        setData(0,val);  // 设置数据字段索引0的值（数据高位）
    }  // 方法结束
    
    // ═══════════════════════════════════════════════════════════════════════════════
    // 私有方法 - 辅助计算
    // ═══════════════════════════════════════════════════════════════════════════════
    
    /**
     * 32位字节序交换
     * 将32位整数的字节序从大端转换为小端或反之
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>交换32位整数的字节序</li>
     *   <li>用于LIN总线数据的字节序转换</li>
     *   <li>大端序和小端序互换</li>
     * </ul>
     *
     * <p><b>字节序交换逻辑：</b>
     * <pre>
     * 原始值：[字节3][字节2][字节1][字节0]
     * 交换后：[字节0][字节1][字节2][字节3]
     *
     * 交换公式：
     * ((val & 0xFF)<<24) | ((val&0xff0000)>>8) | ((val&0xff00)<<8) | (val>>>24)
     * </pre>
     *
     * @param val 需要交换字节序的32位整数
     * @return 字节序交换后的32位整数
     */
    private int swap32(int val) {  // 私有方法：32位字节序交换
        // 字节序交换公式：
        // ((val & 0xFF)<<24)：字节0移到字节3位置
        // ((val&0xff0000)>>8)：字节2移到字节1位置
        // ((val&0xff00)<<8)：字节1移到字节2位置
        // (val>>>24)：字节3移到字节0位置
        return ((val & 0xFF)<<24) | ((val&0xff0000)>>8) | ((val&0xff00)<<8) | (val>>>24);  // 返回字节序交换后的值
    }  // 方法结束
    
    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法 - 总线配置
    // ═══════════════════════════════════════════════════════════════════════════════
    
    /**
     * 配置LIN总线参数
     * 根据LinBus配置对象设置FPGA寄存器参数
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>从LinBus对象获取配置参数</li>
     *   <li>计算波特率分频值和时钟源</li>
     *   <li>设置帧ID、数据长度、触发模式等参数</li>
     *   <li>处理数据字节序转换</li>
     *   <li>配置总线电平</li>
     *   <li>发送寄存器配置到FPGA</li>
     * </ul>
     *
     * <p><b>配置流程：</b>
     * <ol>
     *   <li>获取LinBus配置参数（波特率、帧ID、数据等）</li>
     *   <li>根据波特率选择时钟源（低速或高速）</li>
     *   <li>计算波特率分频值</li>
     *   <li>设置信号源、波特率、时钟源等参数</li>
     *   <li>设置帧ID、数据长度、触发模式</li>
     *   <li>处理数据字节序转换</li>
     *   <li>设置数据高位和低位</li>
     *   <li>配置总线电平</li>
     *   <li>发送寄存器配置到FPGA</li>
     * </ol>
     *
     * <p><b>波特率计算：</b>
     * <pre>
     * 低速模式（波特率≤20kbps）：
     *   分频值 = 7812500 / 波特率 - 1
     *   时钟源 = 0
     *
     * 高速模式（波特率>20kbps）：
     *   分频值 = 250000000 / 波特率 - 1
     *   时钟源 = 1
     * </pre>
     *
     * <p><b>数据字节序转换：</b>
     * <ul>
     *   <li>数据长度≤4字节：16位字节序交换</li>
     *   <li>数据长度≤8字节：32位字节序交换</li>
     *   <li>数据长度>8字节：64位字节序交换</li>
     *   <li>根据数据长度进行移位调整</li>
     * </ul>
     *
     * @param fpgaIdx FPGA索引（用于多FPGA系统）
     * @param iBus LIN总线配置对象（LinBus类型）
     */
    @Override
    public void configBus(int fpgaIdx,IBus iBus) {  // 公有方法：配置LIN总线参数
        
        // 将IBus接口转换为LinBus类型
        LinBus bus = (LinBus)iBus;  // 类型转换：获取LinBus配置对象
        
        // 获取LIN总线配置参数
        int baudRate = bus.getBaudRate();  // 获取波特率
        int clk = 0;  // 初始化时钟源变量
        int triggerType = bus.getTriggerType();  // 获取触发类型
        int id = bus.getFrameId(triggerType);  // 获取帧ID
        long data = bus.getData();  // 获取数据（64位）
        int dataLength = bus.getDataLength();  // 获取数据长度（字节）
        int src = bus.getSrcChIdx();  // 获取信号源通道索引
        
        // 获取总线电平寄存器
        FPGAReg_BUS_LEVEL busLevel = (FPGAReg_BUS_LEVEL) FPGACommand.getReg(fpgaIdx,FPGA_BUS_LEVEL);  // 获取总线电平寄存器
        
        // 设置信号源
        setSrc(chIdx2BusSrc(src));  // 设置信号源（通道索引转换为总线信号源）
        
        // 设置总线信号源扩展
        busLevel.setBusSrcExt(getBusIdx(),((src>>2)&0x1));  // 设置总线信号源扩展（高位通道选择）
        
        // 根据波特率选择时钟源和计算分频值
        if(baudRate <= 20*1000){  // 条件判断：低速模式（波特率≤20kbps）
            // 低速模式：使用7.8125MHz时钟
            baudRate = 7812500/baudRate - 1;  // 计算波特率分频值：7812500 / 波特率 - 1
            clk = 0;  // 设置时钟源为低速时钟（0）
        }else{  // 条件分支：高速模式（波特率>20kbps）
            // 高速模式：使用250MHz时钟
            baudRate = 250000000/baudRate - 1;  // 计算波特率分频值：250000000 / 波特率 - 1
            clk = 1;  // 设置时钟源为高速时钟（1）
        }  // 条件判断结束
        
        // 设置波特率分频值
        setRate(baudRate);  // 设置波特率分频值
        
        // 设置时钟源
        setClk(clk);  // 设置时钟源
        
        // 设置数据长度（寄存器值 = (数据长度 >> 1) - 1）
        setDataLength((dataLength >> 1) - 1);  // 设置数据长度（用数0-7代表1-8个字节）
        
        // 设置触发模式
        setTriggerMode(triggerType);  // 设置触发模式
        
        // 设置帧ID
        setId(id);  // 设置帧ID
        
        // 定义最大数据大小变量
        int maxSize = 4;  // 初始化最大数据大小为4字节
        
        // 根据数据实际有效长度进行匹配转换
        if (dataLength <= 4) {  // 条件判断：数据长度≤4字节
            // 16位字节序交换
            data = ((data & 0xff00) >> 8) | ((data & 0x00ff) << 8);  // 交换字节序：字节0和字节1互换
        } else if (dataLength <= 8) {  // 条件分支：数据长度≤8字节
            // 32位字节序交换
            data = swap32((int) (data & 0xFFFFFFFFL)) & 0xFFFFFFFFL;  // 交换低32位的字节序
            maxSize = 8;  // 设置最大数据大小为8字节
        } else {  // 条件分支：数据长度>8字节（最大16字节）
            // 64位字节序交换（分两次交换）
            long l = swap32((int) (data & 0xFFFFFFFFL)) & 0xFFFFFFFFL;  // 交换低32位的字节序
            long h = swap32((int) (data >>> 32)) & 0xFFFFFFFFL;  // 交换高32位的字节序
            data = l << 32 | h;  // 组合交换后的高低32位
            maxSize = 16;  // 设置最大数据大小为16字节
        }  // 条件判断结束
        
        // 根据数据长度进行移位调整
        data = data >>> (maxSize - dataLength) * 4;  // 移位调整：右移无效数据位
        
        // 设置数据低位（高32位）
        setDataL((int) (data >>> 32));  // 设置数据低位（高32位）
        
        // 设置数据高位（低32位）
        setDataH((int) (data & 0xFFFFFFFFL));  // 设置数据高位（低32位）
        
        // 配置总线电平
        setLevel(busLevel,src,bus.getIdleLevel());  // 设置总线空闲电平
        
        // 发送总线电平寄存器配置到FPGA
        FPGACommand.sendCmd(busLevel);  // 发送总线电平寄存器配置
    }  // 方法结束
}  // FPGAReg_BUS_LIN类结束