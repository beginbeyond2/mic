package com.micsig.tbook.scope.fpga;   // FPGA寄存器包，包含所有FPGA通信相关的寄存器类

/**
 * FPGA滚动行寄存器类 - 控制滚动显示模式的刷新参数
 * 
 * ┌─────────────────────────────────────────────────────────────────┐
 * │                    FPGA滚动行寄存器架构                          │
 * ├─────────────────────────────────────────────────────────────────┤
 * │                                                                 │
 * │  ┌─────────────────┐                                           │
 * │  │  FPGACommand    │                                           │
 * │  │  (命令管理)      │                                           │
 * │  └────────┬────────┘                                           │
 * │           │                                                     │
 * │           │ gntR_samp_place()                                   │
 * │           │ isInScrollMode()                                    │
 * │           │ getColsPerRefresh_scollMode()                       │
 * │           ▼                                                     │
 * │  ┌─────────────────────────────────────────────┐               │
 * │  │        FPGAReg_SCROLL_ROW                   │               │
 * │  │         (滚动行寄存器)                       │               │
 * │  │                                             │               │
 * │  │  ┌─────────────────────────────────────┐   │               │
 * │  │  │  寄存器数据 (4字节)                  │   │               │
 * │  │  │  ┌───────────────────────────────┐  │   │               │
 * │  │  │  │  位0-15: 每次刷新列数(16位)   │  │   │               │
 * │  │  │  │  位16-23: 无效列数(8位)       │  │   │               │
 * │  │  │  │  位24-31: 保留                │  │   │               │
 * │  │  │  └───────────────────────────────┘  │   │               │
 * │  │  └─────────────────────────────────────┘   │               │
 * │  └─────────────────────────────────────────────┘               │
 * │                       │                                       │
 * │                       ▼                                       │
 * │           ┌─────────────────────┐                             │
 * │           │   FPGA硬件 (0x0A)   │                             │
 * │           │  滚动显示刷新控制   │                             │
 * │           └─────────────────────┘                             │
 * │                                                                 │
 * └─────────────────────────────────────────────────────────────────┘
 * 
 * <h3>模块定位</h3>
 * <p>本类是FPGA滚动显示系统的核心组件，负责配置滚动模式下的刷新参数。
 * 滚动模式是示波器在慢时基下的一种显示方式，波形从右向左滚动显示，
 * 类似于纸带记录仪的效果，适合观察长时间变化的信号。</p>
 * 
 * <h3>核心职责</h3>
 * <ul>
 *   <li><b>刷新列数配置</b>：设置每次刷新显示的列数</li>
 *   <li><b>无效列数配置</b>：设置最后一次刷新的无效列数</li>
 *   <li><b>滚动显示优化</b>：配合慢时基模式实现流畅的滚动效果</li>
 * </ul>
 * 
 * <h3>滚动显示模式说明</h3>
 * <pre>
 * ┌───────────────────────────────────────────────────────────────┐
 * │                    滚动显示模式原理                            │
 * ├───────────────────────────────────────────────────────────────┤
 * │                                                               │
 * │  滚动模式特点:                                                │
 * │    - 波形从右向左滚动显示                                     │
 * │    - 新数据从右侧进入，旧数据从左侧移出                       │
 * │    - 类似纸带记录仪的效果                                     │
 * │    - 适合观察长时间变化的信号                                 │
 * │                                                               │
 * │  显示效果示意:                                                │
 * │                                                               │
 * │    ┌─────────────────────────────────────────────┐           │
 * │    │  ←←←←←←←←←←←←←←←←←←←←←←←←←←←←←←←←←←←←←←←←← │           │
 * │    │  [旧数据] [中间数据] [新数据]               │           │
 * │    │  └────────────────────────────────────────  │           │
 * │    │         ↑                                   │           │
 * │    │    新数据入口                               │           │
 * │    └─────────────────────────────────────────────┘           │
 * │                                                               │
 * │  刷新机制:                                                    │
 * │    ┌─────────────────────────────────────────────┐           │
 * │    │  刷新次数: n = 总列数 / 每次刷新列数         │           │
 * │    │  每次刷新列数: colsPerRefresh                │           │
 * │    │  最后无效列数: lastInvalidCols               │           │
 * │    │                                             │           │
 * │    │  示例: 总列数=100, colsPerRefresh=16         │           │
 * │    │    n = 100 / 16 = 6 次刷新                  │           │
 * │    │    最后无效列数 = 100 - 16*6 = 4 列          │           │
 * │    │                                             │           │
 * │    │  刷新序列:                                  │           │
 * │    │    刷新1: 列0-15 (16列)                     │           │
 * │    │    刷新2: 列16-31 (16列)                    │           │
 * │    │    刷新3: 列32-47 (16列)                    │           │
 * │    │    刷新4: 列48-63 (16列)                    │           │
 * │    │    刷新5: 列64-79 (16列)                    │           │
 * │    │    刷新6: 列80-99 (16列，其中4列无效)       │           │
 * │    └─────────────────────────────────────────────┘           │
 * │                                                               │
 * └───────────────────────────────────────────────────────────────┘
 * </pre>
 * 
 * <h3>寄存器数据格式</h3>
 * <pre>
 * ┌────────────────────────────────────────────────────────────┐
 * │              寄存器数据结构 (4字节)                         │
 * ├────────────────────────────────────────────────────────────┤
 * │  位范围    │  字段名           │  说明                     │
 * ├────────────────────────────────────────────────────────────┤
 * │  0-15      │  colsPerRefresh   │  每次刷新的列数 (16位)    │
 * │            │                   │  取值范围: 1-65535        │
 * │            │                   │  常用值: 16, 32, 64       │
 * ├────────────────────────────────────────────────────────────┤
 * │  16-23     │  lastInvalidCols  │  最后无效列数 (8位)       │
 * │            │                   │  取值范围: 0-255          │
 * │            │                   │  用于对齐显示边界         │
 * ├────────────────────────────────────────────────────────────┤
 * │  24-31     │  reserved         │  保留位 (未使用)          │
 * └────────────────────────────────────────────────────────────┘
 * </pre>
 * 
 * <h3>刷新参数计算逻辑</h3>
 * <pre>
 * ┌─────────────────────────────────────────────────────────────┐
 * │                    刷新参数计算流程                         │
 * ├─────────────────────────────────────────────────────────────┤
 * │                                                             │
 * │  输入参数:                                                  │
 * │    - virSceenCols: 虚拟屏幕宽度（总列数）                   │
 * │    - zunDepth: 准存储深度                                   │
 * │                                                             │
 * │  Step 1: 计算每次刷新的列数                                 │
 * │    ┌─────────────────────────────────────┐                 │
 * │    │ cols = getColsPerRefresh_scollMode() │                 │
 * │    │ 根据屏幕宽度和时基计算合适的刷新列数 │                 │
 * │    └─────────────────────────────────────┘                 │
 * │                                                             │
 * │  Step 2: 计算刷新次数                                       │
 * │    ┌─────────────────────────────────────┐                 │
 * │    │ n = virSceenCols / cols              │                 │
 * │    │ 计算需要多少次刷新才能显示完整屏幕   │                 │
 * │    └─────────────────────────────────────┘                 │
 * │                                                             │
 * │  Step 3: 计算无效列数                                       │
 * │    ┌─────────────────────────────────────┐                 │
 * │    │ rev = virSceenCols % cols            │                 │
 * │    │ if (rev != 0) {                      │                 │
 * │    │     n += 1;                          │                 │
 * │    │     rev = cols - rev;                │                 │
 * │    │ }                                    │                 │
 * │    │ 计算最后一次刷新的无效列数           │                 │
 * │    └─────────────────────────────────────┘                 │
 * │                                                             │
 * │  Step 4: 设置寄存器值                                       │
 * │    ┌─────────────────────────────────────┐                 │
 * │    │ setColsPerRefresh(cols)              │                 │
 * │    │ setLastInvalidCols(rev)              │                 │
 * │    └─────────────────────────────────────┘                 │
 * │                                                             │
 * └─────────────────────────────────────────────────────────────┘
 * </pre>
 * 
 * <h3>与其他寄存器的配合关系</h3>
 * <pre>
 * ┌─────────────────────────────────────────────────────────────┐
 * │              滚动模式寄存器配合关系                         │
 * ├─────────────────────────────────────────────────────────────┤
 * │                                                             │
 * │  滚动模式需要多个寄存器协同工作:                            │
 * │                                                             │
 * │  ┌─────────────────────────────────────────────┐           │
 * │  │  FPGA_PRE_SAMP (预采样寄存器)               │           │
 * │  │    - 滚动模式下预采样深度设为0              │           │
 * │  │    - 后采样深度根据刷新参数计算             │           │
 * │  └─────────────────────────────────────────────┘           │
 * │                                                             │
 * │  ┌─────────────────────────────────────────────┐           │
 * │  │  FPGA_SCROLL_ROW (滚动行寄存器)             │           │
 * │  │    - 设置每次刷新的列数                     │           │
 * │  │    - 设置最后一次的无效列数                 │           │
 * │  └─────────────────────────────────────────────┘           │
 * │                                                             │
 * │  ┌─────────────────────────────────────────────┐           │
 * │  │  FPGA_SLOW_SCALE_SET (慢时基设置寄存器)     │           │
 * │  │    - 设置刷新次数                           │           │
 * │  │    - 设置触发位置距离                       │           │
 * │  └─────────────────────────────────────────────┘           │
 * │                                                             │
 * │  ┌─────────────────────────────────────────────┐           │
 * │  │  FPGA_ZUN_DEPTH (存储深度寄存器)            │           │
 * │  │    - 设置显示的数据长度                     │           │
 * │  └─────────────────────────────────────────────┘           │
 * │                                                             │
 * └─────────────────────────────────────────────────────────────┘
 * </pre>
 * 
 * <h3>使用场景</h3>
 * <ul>
 *   <li><b>滚动模式</b>：慢时基下的滚动显示，波形从右向左移动</li>
 *   <li><b>慢时基模式</b>：配合慢时基缩放功能实现流畅显示</li>
 *   <li><b>实时监控</b>：长时间观察信号变化趋势</li>
 * </ul>
 * 
 * <h3>依赖关系</h3>
 * <ul>
 *   <li>{@link FPGAReg} - 父类，提供寄存器基础功能</li>
 *   <li>{@link FPGACommand} - FPGA命令管理类，负责调用设置方法</li>
 *   <li>{@link FPGAReg_PRE_SAMP} - 预采样寄存器，配合使用</li>
 *   <li>{@link FPGAReg_SLOW_SCALE_SET} - 慢时基设置寄存器，配合使用</li>
 *   <li>{@link FPGAReg_ZUN_DEPTH} - 存储深度寄存器，配合使用</li>
 * </ul>
 * 
 * <h3>寄存器地址</h3>
 * <p>寄存器ID: 0x0A (FPGA_SCROLL_ROW)</p>
 * 
 * @see FPGAReg
 * @see FPGACommand#gntR_samp_place(int)
 * @see FPGAReg_PRE_SAMP
 * @see FPGAReg_SLOW_SCALE_SET
 * @see FPGAReg_ZUN_DEPTH
 * @author Micsig智能示波器团队
 * @version 2.0
 * @since 1.0
 */
public class FPGAReg_SCROLL_ROW extends FPGAReg {   // 继承FPGAReg基类，获得寄存器通用功能
    
    /**
     * 构造函数 - 初始化滚动行寄存器
     * 
     * <p>创建滚动行寄存器实例，配置寄存器ID和数据大小。</p>
     * 
     * <h4>初始化参数</h4>
     * <ul>
     *   <li><b>寄存器ID</b>: FPGA_SCROLL_ROW (0x0A)</li>
     *   <li><b>数据大小</b>: 4字节（32位数据）</li>
     * </ul>
     * 
     * <h4>寄存器地址说明</h4>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器地址映射                         │
     * ├─────────────────────────────────────────┤
     * │  0x0A = FPGA_SCROLL_ROW                 │
     * │  功能: 设置滚动显示的刷新参数           │
     * │  大小: 4字节                            │
     * │  访问: 只写 (由FPGA读取)                │
     * │                                         │
     * │  数据结构:                              │
     * │  位0-15: 每次刷新列数                   │
     * │  位16-23: 最后无效列数                  │
     * │  位24-31: 保留                          │
     * └─────────────────────────────────────────┘
     * </pre>
     * 
     * <h4>调用时机</h4>
     * <p>在FPGACommand初始化时创建实例，存储在fpgaRegs数组中。
     * 当需要配置滚动模式参数时，通过setColsPerRefresh和setLastInvalidCols方法设置值。</p>
     * 
     * @see FPGAReg#FPGAReg(int, int)
     * @see FPGACommand#initFpgaRegs()
     */
    public FPGAReg_SCROLL_ROW() {   // 构造函数：初始化滚动行寄存器
        super(FPGA_SCROLL_ROW, 4);   // 调用父类构造函数，设置寄存器ID为0x0A，数据大小为4字节
    }   // 构造函数结束

    /**
     * 设置每次刷新的列数 - 配置滚动显示的刷新宽度
     * 
     * <p>此方法设置滚动模式下每次刷新显示的列数。
     * 列数决定了每次刷新时波形移动的步长，影响滚动的流畅度。</p>
     * 
     * <h4>参数说明</h4>
     * <table border="1">
     *   <tr><th>参数</th><th>类型</th><th>说明</th></tr>
     *   <tr><td>val</td><td>int</td><td>每次刷新的列数，取值范围1-65535</td></tr>
     * </table>
     * 
     * <h4>列数选择原则</h4>
     * <ul>
     *   <li><b>较小列数</b>（如16）：滚动更平滑，但刷新次数更多</li>
     *   <li><b>较大列数</b>（如64）：刷新次数少，但滚动步长较大</li>
     *   <li><b>列数对齐</b>：需要与屏幕宽度配合，避免显示错位</li>
     * </ul>
     * 
     * <h4>数据存储位置</h4>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器位映射                           │
     * ├─────────────────────────────────────────┤
     * │  位0-15: colsPerRefresh (16位)          │
     * │                                         │
     * │  示例: val = 32                         │
     * │    二进制: 0000 0000 0010 0000          │
     * │    存储位置: 位0-15                     │
     * └─────────────────────────────────────────┘
     * </pre>
     * 
     * <h4>调用时机</h4>
     * <ul>
     *   <li>进入滚动模式时，根据时基和屏幕宽度计算列数</li>
     *   <li>切换慢时基档位时，重新计算刷新列数</li>
     *   <li>调整屏幕显示宽度时，更新刷新列数</li>
     * </ul>
     * 
     * <h4>典型值示例</h4>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  典型刷新列数配置                       │
     * ├─────────────────────────────────────────┤
     * │  屏幕宽度: 800列                        │
     * │                                         │
     * │  配置1: colsPerRefresh = 16             │
     * │    刷新次数: 800/16 = 50次              │
     * │    滚动步长: 16列                       │
     * │                                         │
     * │  配置2: colsPerRefresh = 32             │
     * │    刷新次数: 800/32 = 25次              │
     * │    滚动步长: 32列                       │
     * │                                         │
     * │  配置3: colsPerRefresh = 64             │
     * │    刷新次数: 800/64 = 12次              │
     * │    滚动步长: 64列                       │
     * └─────────────────────────────────────────┘
     * </pre>
     * 
     * @param val 每次刷新的列数，取值范围1-65535
     * @see FPGAReg#setVal(int, int, int)
     * @see FPGACommand#gntR_samp_place(int)
     */
    public void setColsPerRefresh(int val){   // 方法：设置每次刷新的列数
        setVal(0,16,val);   // 设置寄存器值：从位0开始，长度16位，值为val（每次刷新列数）
    }   // setColsPerRefresh方法结束
    
    /**
     * 设置最后无效列数 - 配置最后一次刷新的对齐参数
     * 
     * <p>此方法设置滚动模式下最后一次刷新的无效列数。
     * 无效列数用于处理屏幕宽度与刷新列数不对齐的情况，
     * 确保显示边界正确对齐。</p>
     * 
     * <h4>参数说明</h4>
     * <table border="1">
     *   <tr><th>参数</th><th>类型</th><th>说明</th></tr>
     *   <tr><td>val</td><td>int</td><td>最后无效列数，取值范围0-255</td></tr>
     * </table>
     * 
     * <h4>无效列数计算原理</h4>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  无效列数计算原理                       │
     * ├─────────────────────────────────────────┤
     * │                                         │
     * │  问题: 屏幕宽度可能不是刷新列数的整数倍 │
     * │                                         │
     * │  示例:                                  │
     * │    屏幕宽度 = 100列                     │
     * │    刷新列数 = 16列                      │
     * │                                         │
     * │  计算:                                  │
     * │    刷新次数 n = 100 / 16 = 6            │
     * │    余数 rev = 100 % 16 = 4              │
     * │                                         │
     * │  如果 rev != 0:                         │
     * │    n += 1 (需要额外一次刷新)            │
     * │    无效列数 = 16 - 4 = 12               │
     * │                                         │
     * │  结果:                                  │
     * │    刷新1-5: 各16列，共80列              │
     * │    刷新6: 16列，其中12列无效            │
     * │    有效显示: 80 + 4 = 84列              │
     * │    等等...实际计算可能有调整            │
     * │                                         │
     * └─────────────────────────────────────────┘
     * </pre>
     * 
     * <h4>数据存储位置</h4>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器位映射                           │
     * ├─────────────────────────────────────────┤
     * │  位16-23: lastInvalidCols (8位)         │
     * │                                         │
     * │  示例: val = 12                         │
     * │    二进制: 0000 1100                    │
     * │    存储位置: 位16-23                    │
     * └─────────────────────────────────────────┘
     * </pre>
     * 
     * <h4>调用时机</h4>
     * <ul>
     *   <li>进入滚动模式时，根据屏幕宽度和刷新列数计算无效列数</li>
     *   <li>切换慢时基档位时，重新计算无效列数</li>
     *   <li>与setColsPerRefresh同步调用</li>
     * </ul>
     * 
     * <h4>边界对齐说明</h4>
     * <p>无效列数确保FPGA在最后一次刷新时不会超出显示边界，
     * 避免显示错位或数据溢出。当屏幕宽度正好是刷新列数的整数倍时，
     * 无效列数为0。</p>
     * 
     * @param val 最后无效列数，取值范围0-255
     * @see FPGAReg#setVal(int, int, int)
     * @see FPGACommand#gntR_samp_place(int)
     */
    public void setLastInvalidCols(int val){   // 方法：设置最后一次刷新的无效列数
        setVal(16,8,val);   // 设置寄存器值：从位16开始，长度8位，值为val（无效列数）
    }   // setLastInvalidCols方法结束
}   // FPGAReg_SCROLL_ROW类结束