/**
 * 示波器FPGA自动触发时间寄存器类
 * 继承自FPGAReg基类，负责管理FPGA自动触发时间参数
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>设置自动触发等待时间</li>
 *   <li>控制自动触发模式的触发时机</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建自动触发时间寄存器对象
 * FPGAReg_TRIG_AUTO_TRIG_TIME reg = new FPGAReg_TRIG_AUTO_TRIG_TIME();
 *
 * // 设置自动触发时间为100ms
 * reg.setTrigTime(100, 0);  // val=100, val2=0
 *
 * // 设置自动触发时间为50ms
 * reg.setTrigTime(50, 0);  // val=50, val2=0
 *
 * // 发送命令到FPGA
 * FPGACommand.getInstance().sendCommand(reg);
 * </pre>
 *
 * @see FPGAReg
 * @see FPGACommand
 * @see Trigger
 * @see Scope
 * @author zhuzh
 * @version 1.0
 * @since 2018-4-8
 */
public class FPGAReg_TRIG_AUTO_TRIG_TIME extends FPGAReg {                                // 类声明：自动触发时间寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化自动触发时间寄存器
     *
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_TRIG_AUTO_TRIG_TIME</li>
     *   <li>数据长度：4字节（32位）</li>
     * </ul>
     *
     * <p><b>寄存器地址说明：</b></p>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器地址映射                         │
     * ├─────────────────────────────────────────┤
     * │  FPGA_TRIG_AUTO_TRIG_TIME (0x24)        │
     * │  功能: 设置自动触发等待时间             │
     * │  大小: 4字节                            │
     * │  访问: 只写 (由FPGA读取)                │
     * │                                         │
     * │  数据结构:                              │
     * │  位0-24: val（主要触发时间，25位）      │
     * │  位25-31: val2（次要触发时间，7位）     │
     * │                                         │
     * │  时间单位: 1ms                          │
     * └─────────────────────────────────────────┘
     * </pre>
     *
     * <p><b>调用时机：</b></p>
     * <ul>
     *   <li>自动触发时间改变时，需要重新设置</li>
     *   <li>切换到自动模式时，需要设置自动触发时间</li>
     *   <li>调整触发响应速度时，需要调整自动触发时间</li>
     * </ul>
     */
    public FPGAReg_TRIG_AUTO_TRIG_TIME() {                                                // 构造方法：初始化自动触发时间寄存器
        // 调用父类构造函数，设置寄存器ID和数据长度
        // FPGA_TRIG_AUTO_TRIG_TIME：自动触发时间寄存器地址
        // 4：数据长度为4字节（32位）
        super(FPGA_TRIG_AUTO_TRIG_TIME, 4);                                                // 调用父类构造函数，设置寄存器ID为FPGA_TRIG_AUTO_TRIG_TIME，长度4字节
    }                                                                                     // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置自动触发时间
     * 设置自动触发等待时间，控制自动触发时机
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>自动触发时间决定了等待多久后自动触发</li>
     *   <li>时间单位为1ms（毫秒）</li>
     *   <li>val是主要触发时间值（高位25位）</li>
     *   <li>val2是次要触发时间值（低位7位）</li>
     *   <li>组合使用可以实现精确的触发时间控制</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>val：主要触发时间值（25位，范围0到33554431）</li>
     *   <li>val2：次要触发时间值（7位，范围0到127）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器位映射                           │
     * ├─────────────────────────────────────────┤
     * │  位0-24: val (25位)                     │
     * │  位25-31: val2 (7位)                    │
     * │                                         │
     * │  示例: val=100, val2=0                  │
     * │    表示自动触发时间为100ms              │
     * │                                         │
     * │  示例: val=50, val2=0                   │
     * │    表示自动触发时间为50ms               │
     * └─────────────────────────────────────────┘
     * </pre>
     *
     * <p><b>典型值示例：</b></p>
     * <ul>
     *   <li>自动模式：val=100, val2=0（等待100ms后自动触发）</li>
     *   <li>正常模式：val=50, val2=0（等待50ms后自动触发）</li>
     *   <li>快速响应：val=10, val2=0（等待10ms后自动触发）</li>
     * </ul>
     *
     * <p><b>应用场景：</b></p>
     * <ul>
     *   <li>自动模式：自动模式下设置适当的自动触发时间</li>
     *   <li>无触发信号：当没有触发信号时自动采集波形</li>
     *   <li>实时监控：需要实时监控信号状态时使用</li>
     * </ul>
     *
     * @param val 主要触发时间值（25位，单位ms）
     * @param val2 次要触发时间值（7位）
     */
    public void setTrigTime(int val, int val2){                                           // 公有方法：设置自动触发时间
        // 调用父类setVal方法，设置主要触发时间值
        // 参数1：起始位=0（从位0开始）
        // 参数2：位宽度=25（25位）
        // 参数3：主要触发时间值
        setVal(0, 25, val);                                                                // 设置位0-24的值，值为val（主要触发时间）

        // 调用父类setVal方法，设置次要触发时间值
        // 参数1：起始位=25（从位25开始）
        // 参数2：位宽度=7（7位）
        // 参数3：次要触发时间值
        setVal(25, 7, val2);                                                               // 设置位25-31的值，值为val2（次要触发时间）
    }                                                                                     // 方法结束

}                                                                                         // 类结束