/**
 * 示波器FPGA通道Y轴位置寄存器类
 * 继承自FPGAReg基类，负责管理FPGA通道Y轴波形偏移量参数
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>设置通道Y轴波形偏移量（垂直位置）</li>
 *   <li>控制波形在屏幕上的垂直显示位置</li>
 *   <li>支持多通道的Y轴位置设置</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建Y轴位置寄存器对象
 * FPGAReg_CH_Y_PLACE reg = new FPGAReg_CH_Y_PLACE();
 *
 * // 设置CH1的Y轴位置（波形居中）
 * reg.setVolm(0, 0);  // 设置CH1主要偏移值为0
 * reg.setVoln(0, 0);  // 设置CH1次要偏移值为0
 *
 * // 设置CH2的Y轴位置（波形向下移动）
 * reg.setVolm(1, 100);  // 设置CH2主要偏移值为100
 * reg.setVoln(1, 50);   // 设置CH2次要偏移值为50
 *
 * // 发送命令到FPGA
 * FPGACommand.getInstance().sendCommand(reg);
 * </pre>
 *
 * @see FPGAReg
 * @see FPGACommand
 * @see Channel
 * @see Scope
 * @author zhuzh
 * @version 1.0
 * @since 2018-4-8
 */
public class FPGAReg_CH_Y_PLACE extends FPGAReg {                                         // 类声明：通道Y轴位置寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化通道Y轴位置寄存器
     *
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_CH_Y_PLACE</li>
     *   <li>数据长度：16字节（4个字，每个字32位）</li>
     * </ul>
     *
     * <p><b>寄存器地址说明：</b></p>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器地址映射                         │
     * ├─────────────────────────────────────────┤
     * │  FPGA_CH_Y_PLACE (0x0C)                 │
     * │  功能: 设置通道Y轴波形偏移量            │
     * │  大小: 16字节（4个字）                  │
     * │  访问: 只写 (由FPGA读取)                │
     * │                                         │
     * │  数据结构:                              │
     * │  字0: CH1 Y轴位置（32位）               │
     * │  字1: CH2 Y轴位置（32位）               │
     * │  字2: CH3 Y轴位置（32位）               │
     * │  字3: CH4 Y轴位置（32位）               │
     * │                                         │
     * │  每个字结构:                            │
     * │  位0-17: volm（主要偏移值，18位）       │
     * │  位18-28: voln（次要偏移值，11位）      │
     * │  位29-31: 保留                          │
     * └─────────────────────────────────────────┘
     * </pre>
     *
     * <p><b>调用时机：</b></p>
     * <ul>
     *   <li>通道Y轴位置改变时，需要重新设置偏移值</li>
     *   <li>垂直刻度改变时，可能需要调整Y轴位置</li>
     *   <li>波形显示模式切换时（分离/叠加）</li>
     * </ul>
     */
    public FPGAReg_CH_Y_PLACE() {                                                         // 构造方法：初始化通道Y轴位置寄存器
        // 调用父类构造函数，设置寄存器ID和数据长度
        // FPGA_CH_Y_PLACE：通道Y轴位置寄存器地址
        // 16：数据长度为16字节（4个32位字，每个通道一个字）
        super(FPGA_CH_Y_PLACE, 16);                                                        // 调用父类构造函数，设置寄存器ID为FPGA_CH_Y_PLACE，长度16字节
    }                                                                                     // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法 - Y轴位置设置
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置主要Y轴偏移值
     * 设置指定通道的主要Y轴偏移值（高位18位）
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>主要Y轴偏移值用于控制波形在屏幕上的垂直位置</li>
     *   <li>volm是高位18位，用于粗调波形位置</li>
     *   <li>正值使波形向下移动，负值使波形向上移动</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>idx：通道索引（0=CH1，1=CH2，2=CH3，3=CH4）</li>
     *   <li>val：主要偏移值（18位，范围0到262143）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器位映射                           │
     * ├─────────────────────────────────────────┤
     * │  字idx的位0-17: volm (18位)             │
     * │                                         │
     * │  示例: idx=0, val=100                   │
     * │    存储位置: 字0的位0-17                │
     * │    表示CH1的主要Y轴偏移值为100          │
     * └─────────────────────────────────────────┘
     * </pre>
     *
     * <p><b>应用场景：</b></p>
     * <ul>
     *   <li>波形分离显示：设置不同通道的Y轴位置</li>
     *   <li>波形叠加显示：将多个通道的Y轴位置设为相同值</li>
     *   <li>垂直位置调整：调整波形在屏幕上的位置</li>
     * </ul>
     *
     * @param idx 通道索引（0=CH1，1=CH2，2=CH3，3=CH4）
     * @param val 主要Y轴偏移值（18位）
     */
    public void setVolm(int idx, int val){                                                // 公有方法：设置主要Y轴偏移值
        // 调用父类setVal方法，设置指定字的主要偏移值
        // 参数1：字索引=idx（通道索引对应的字）
        // 参数2：起始位=0（从位0开始）
        // 参数3：位宽度=18（18位）
        // 参数4：主要偏移值
        setVal(idx, 0, 18, val);                                                           // 设置字idx的位0-17，值为val（主要Y轴偏移值）
    }                                                                                     // 方法结束

    /**
     * 设置次要Y轴偏移值
     * 设置指定通道的次要Y轴偏移值（低位11位）
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>次要Y轴偏移值用于精确控制波形在屏幕上的垂直位置</li>
     *   <li>voln是低位11位，用于精调波形位置</li>
     *   <li>与volm组合使用可以实现精确的Y轴位置控制</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>idx：通道索引（0=CH1，1=CH2，2=CH3，3=CH4）</li>
     *   <li>val：次要偏移值（11位，范围0到2047）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器位映射                           │
     * ├─────────────────────────────────────────┤
     * │  字idx的位18-28: voln (11位)            │
     * │                                         │
     * │  示例: idx=0, val=50                    │
     * │    存位置: 字0的位18-28                 │
     * │    表示CH1的次要Y轴偏移值为50           │
     * └─────────────────────────────────────────┘
     * </pre>
     *
     * <p><b>应用场景：</b></p>
     * <ul>
     *   <li>精确位置调整：与volm配合实现精确的Y轴位置控制</li>
     *   <li>微调波形位置：对波形位置进行微调</li>
     * </ul>
     *
     * @param idx 通道索引（0=CH1，1=CH2，2=CH3，3=CH4）
     * @param val 次要Y轴偏移值（11位）
     */
    public void setVoln(int idx, int val){                                                // 公有方法：设置次要Y轴偏移值
        // 调用父类setVal方法，设置指定字的次要偏移值
        // 参数1：字索引=idx（通道索引对应的字）
        // 参数2：起始位=18（从位18开始）
        // 参数3：位宽度=11（11位）
        // 参数4：次要偏移值
        setVal(idx, 18, 11, val);                                                          // 设置字idx的位18-28，值为val（次要Y轴偏移值）
    }                                                                                     // 方法结束

}                                                                                         // 类结束