/**
 * 示波器FPGA通道Y轴位置补充寄存器类
 * 继承自FPGAReg基类，负责管理FPGA通道Y轴位置的补充参数
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>设置通道Y轴位置补充偏移值</li>
 *   <li>配合FPGAReg_CH_Y_PLACE实现精确的Y轴位置控制</li>
 *   <li>支持多通道的Y轴位置补充设置</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建Y轴位置补充寄存器对象
 * FPGAReg_CH_Y_PLACE_BC reg = new FPGAReg_CH_Y_PLACE_BC();
 *
 * // 设置CH1的Y轴位置补充值
 * reg.setChYBc(0, 100);  // 设置CH1补充偏移值为100
 *
 * // 设置CH2的Y轴位置补充值
 * reg.setChYBc(1, -50);  // 设置CH2补充偏移值为-50
 *
 * // 发送命令到FPGA
 * FPGACommand.getInstance().sendCommand(reg);
 * </pre>
 *
 * @see FPGAReg
 * @see FPGAReg_CH_Y_PLACE
 * @see FPGACommand
 * @see Channel
 * @author zhuzh
 * @version 1.0
 * @since 2018-4-8
 */
public class FPGAReg_CH_Y_PLACE_BC extends FPGAReg {                                       // 类声明：通道Y轴位置补充寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化通道Y轴位置补充寄存器
     *
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_CH_Y_PLACE_BC</li>
     *   <li>数据长度：8字节（2个字，每个字32位）</li>
     * </ul>
     *
     * <p><b>寄存器地址说明：</b></p>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器地址映射                         │
     * ├─────────────────────────────────────────┤
     * │  FPGA_CH_Y_PLACE_BC                     │
     * │  功能: 设置通道Y轴位置补充偏移值        │
     * │  大小: 8字节（2个字）                   │
     * │  访问: 只写 (由FPGA读取)                │
     * │                                         │
     * │  数据结构:                              │
     * │  字0: CH1/CH2 Y轴补充位置               │
     * │    位0-15: CH2补充偏移值                │
     * │    位16-31: CH1补充偏移值               │
     * │  字1: CH3/CH4 Y轴补充位置               │
     * │    位0-15: CH3补充偏移值                │
     * │    位16-31: CH4补充偏移值               │
     * └─────────────────────────────────────────┘
     * </pre>
     *
     * <p><b>调用时机：</b></p>
     * <ul>
     *   <li>通道校准后需要设置补充偏移值</li>
     *   <li>基线调整时需要更新补充值</li>
     *   <li>精确位置控制时配合主寄存器使用</li>
     * </ul>
     */
    public FPGAReg_CH_Y_PLACE_BC() {                                                       // 构造方法：初始化通道Y轴位置补充寄存器
        // 调用父类构造函数，设置寄存器ID和数据长度
        // FPGA_CH_Y_PLACE_BC：通道Y轴位置补充寄存器地址
        // 8：数据长度为8字节（2个32位字）
        super(FPGA_CH_Y_PLACE_BC, 8);                                                       // 调用父类构造函数，设置寄存器ID为FPGA_CH_Y_PLACE_BC，长度8字节
    }                                                                                      // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法 - Y轴位置补充设置
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置通道Y轴位置补充偏移值
     * 设置指定通道的Y轴位置补充偏移值
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>设置指定通道的Y轴位置补充偏移值</li>
     *   <li>补充偏移值用于校准补偿或基线调整</li>
     *   <li>配合主Y轴位置寄存器实现精确控制</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>idx：通道索引（0=CH1，1=CH2，2=CH3，3=CH4）</li>
     *   <li>val：补充偏移值（16位，范围-32768到32767）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器位映射                           │
     * ├─────────────────────────────────────────┤
     * │  CH1(idx=0): 字0的位16-31               │
     * │  CH2(idx=1): 字0的位0-15                │
     * │  CH3(idx=2): 字1的位0-15                │
     * │  CH4(idx=3): 字1的位16-31               │
     * │                                         │
     * │  注意：通道顺序与主寄存器不同           │
     * │  CH1在高位，CH2在低位                   │
     * └─────────────────────────────────────────┘
     * </pre>
     *
     * <p><b>应用场景：</b></p>
     * <ul>
     *   <li>通道校准：存储校准后的补偿值</li>
     *   <li>基线调整：调整波形的基线位置</li>
     *   <li>精确显示：实现精确的波形显示位置</li>
     * </ul>
     *
     * @param idx 通道索引（0=CH1，1=CH2，2=CH3，3=CH4）
     * @param val Y轴位置补充偏移值（16位有符号整数）
     */
    public void setChYBc(int idx, int val){                                                // 公有方法：设置通道Y轴位置补充偏移值
        // 根据通道索引设置对应的寄存器位
        // 使用switch语句处理不同通道的位映射
        switch (idx){                                                                       // switch语句：根据通道索引选择对应的位映射
            case 0:                                                                         // case 0：处理CH1通道
                // CH1的补充偏移值存储在字0的位16-31
                // 参数1：字索引=0（第一个32位字）
                // 参数2：起始位=16（从位16开始）
                // 参数3：位宽度=16（16位）
                // 参数4：补充偏移值
                setVal(0, 16, val);                                                         // 设置字0的位16-31，值为val（CH1补充偏移值）
                break;                                                                      // break：结束case 0
            case 1:                                                                         // case 1：处理CH2通道
                // CH2的补充偏移值存储在字0的位0-15
                // 参数1：字索引=0（第一个32位字）
                // 参数2：起始位=0（从位0开始）
                // 参数3：位宽度=16（16位）
                // 参数4：补充偏移值
                setVal(16, 16, val);                                                        // 设置字0的位0-15，值为val（CH2补充偏移值）
                break;                                                                      // break：结束case 1
            case 2:                                                                         // case 2：处理CH3通道
                // CH3的补充偏移值存储在字1的位0-15
                // 参数1：字索引=1（第二个32位字）
                // 参数2：起始位=0（从位0开始）
                // 参数3：位宽度=16（16位）
                // 参数4：补充偏移值
                setVal(1, 0, 16, val);                                                      // 设置字1的位0-15，值为val（CH3补充偏移值）
                break;                                                                      // break：结束case 2
            case 3:                                                                         // case 3：处理CH4通道
                // CH4的补充偏移值存储在字1的位16-31
                // 参数1：字索引=1（第二个32位字）
                // 参数2：起始位=16（从位16开始）
                // 参数3：位宽度=16（16位）
                // 参数4：补充偏移值
                setVal(1, 16, 16, val);                                                     // 设置字1的位16-31，值为val（CH4补充偏移值）
                break;                                                                      // break：结束case 3
        }                                                                                   // switch语句结束
    }                                                                                       // 方法结束

}                                                                                          // 类结束