/**
 * 示波器FPGA状态数据类
 * 负责存储和管理FPGA返回的状态数据
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>存储频率计数结果</li>
 *   <li>存储自动测量结果（最小值、最大值、周期）</li>
 *   <li>管理数据有效性标志</li>
 *   <li>提供数据访问接口</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建FPGA状态对象
 * FPGA_Status status = new FPGA_Status();
 *
 * // 获取频率计数结果
 * FpgaFreq freq = status.getFreq();
 * if (freq.isVaild()) {
 *     int t = freq.getT();  // 周期值
 *     int n = freq.getN();  // 计数值
 * }
 *
 * // 获取通道0的自动测量结果
 * FpgaAuto auto = status.getAuto(0);
 * if (auto.isVaild()) {
 *     int min = auto.getMinVal();  // 最小值
 *     int max = auto.getMaxVal();  // 最大值
 *     long cycle = auto.getCycle();  // 周期
 * }
 *
 * // 检查所有通道数据有效性
 * status.check();
 * </pre>
 *
 * @see FpgaFreq
 * @see FpgaAuto
 * @see FPGACommand
 */
public class FPGA_Status {                                                               // 类声明：FPGA状态数据类

    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 频率计数结果对象
     * 存储频率计数测量的结果数据
     */
    private FpgaFreq freq;                                                               // 成员变量：频率计数结果对象

    /**
     * 自动测量结果数组
     * 每个通道对应一个FpgaAuto对象
     * 数组大小由ChannelFactory.CH_CNT决定
     */
    private FpgaAuto [] auto = new FpgaAuto[ChannelFactory.CH_CNT];                      // 成员变量：自动测量结果数组

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化自动测量结果数组和频率计数结果对象
     */
    public FPGA_Status(){                                                                // 构造方法：初始化FPGA状态对象
        for(int i=0;i<auto.length;i++){                                                  // 循环：遍历所有通道
            auto[i] = new FpgaAuto();                                                    // 为每个通道创建自动测量结果对象
        }                                                                                // 循环结束
        freq = new FpgaFreq();                                                           // 创建频率计数结果对象
    }                                                                                    // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取指定通道的自动测量结果
     * 
     * @param chIdx 通道索引
     * @return 自动测量结果对象
     */
    public FpgaAuto getAuto(int chIdx) {                                                 // 公有方法：获取自动测量结果
        return auto[chIdx];                                                              // 返回指定通道的自动测量结果对象
    }                                                                                    // 方法结束

    /**
     * 检查所有通道数据有效性
     * 如果任意一个通道的数据无效，则将所有通道的数据标记为无效
     * 
     * <p><b>检查逻辑：</b></p>
     * <ol>
     *   <li>遍历所有通道的自动测量结果</li>
     *   <li>检查每个通道的有效性标志</li>
     *   <li>如果发现无效数据，将所有通道标记为无效</li>
     * </ol>
     */
    public void check(){                                                                 // 公有方法：检查数据有效性
        boolean bValid = true;                                                           // 局部变量：有效性标志，初始为true
        for(FpgaAuto a:auto){                                                            // 循环：遍历所有通道的自动测量结果
            bValid = a.isVaild();                                                        // 获取当前通道的有效性标志
            if(!bValid){                                                                 // 判断：如果数据无效
                break;                                                                   // 跳出循环
            }                                                                            // 判断结束
        }                                                                                // 循环结束
        if(!bValid) {                                                                    // 判断：如果存在无效数据
            for (FpgaAuto a : auto) {                                                    // 循环：遍历所有通道
                a.setVaild(bValid);                                                      // 设置为无效
            }                                                                            // 循环结束
        }                                                                                // 判断结束
    }                                                                                    // 方法结束

    /**
     * 获取频率计数结果
     * 
     * @return 频率计数结果对象
     */
    public FpgaFreq getFreq(){                                                           // 公有方法：获取频率计数结果
        return freq;                                                                     // 返回频率计数结果对象
    }                                                                                    // 方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 内部类：FpgaFreq - 频率计数结果类
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 频率计数结果内部类
     * 存储频率计数测量的结果数据
     *
     * <p><b>数据说明：</b></p>
     * <ul>
     *   <li>T值：周期时间（时钟周期数）</li>
     *   <li>N值：信号周期数</li>
     *   <li>频率 = N / T × 时钟频率</li>
     * </ul>
     */
    public class FpgaFreq{                                                               // 内部类：频率计数结果类

        /**
         * 数据有效标志
         * true=数据有效，false=数据无效
         */
        private boolean bVaild = false;                                                  // 成员变量：有效标志，初始为false

        /**
         * 周期计数值数组
         * TNs[0]: T值（周期时间）
         * TNs[1]: N值（信号周期数）
         */
        private int [] TNs = new int[2];                                                 // 成员变量：周期计数值数组

        /**
         * 检查数据是否有效
         * 
         * @return true=有效，false=无效
         */
        public boolean isVaild() {                                                       // 公有方法：检查数据有效性
            return bVaild;                                                               // 返回有效标志
        }                                                                                // 方法结束

        /**
         * 设置数据有效标志
         * 
         * @param bVaild 有效标志（true=有效，false=无效）
         */
        public void setVaild(boolean bVaild) {                                           // 公有方法：设置有效标志
            this.bVaild = bVaild;                                                        // 设置有效标志
        }                                                                                // 方法结束

        /**
         * 获取N值（信号周期数）
         * 用于频率计算
         * 
         * @return N值
         */
        public int getN() {                                                              // 公有方法：获取N值
            return TNs[1];                                                               // 返回TNs数组的第2个元素（N值）
        }                                                                                // 方法结束

        /**
         * 获取T值（周期时间）
         * 用于频率计算
         * 
         * @return T值（时钟周期数）
         */
        public int getT(){                                                               // 公有方法：获取T值
            return TNs[0];                                                               // 返回TNs数组的第1个元素（T值）
        }                                                                                // 方法结束

        /**
         * 设置周期计数值
         * 
         * @param idx 索引（0=T值，1=N值）
         * @param val 计数值
         */
        public void setTN(int idx ,int val) {                                            // 公有方法：设置周期计数值
            this.TNs[idx] = val;                                                         // 设置指定索引的计数值
        }                                                                                // 方法结束
    }                                                                                    // 内部类结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 内部类：FpgaAuto - 自动测量结果类
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 自动测量结果内部类
     * 存储单个通道的自动测量结果数据
     *
     * <p><b>测量参数：</b></p>
     * <ul>
     *   <li>最小值：波形的最小ADC值</li>
     *   <li>最大值：波形的最大ADC值</li>
     *   <li>周期：波形的周期（时钟周期数）</li>
     * </ul>
     */
    public class FpgaAuto{                                                               // 内部类：自动测量结果类

        /**
         * 数据有效标志
         * true=数据有效，false=数据无效
         */
        private boolean bVaild = false;                                                  // 成员变量：有效标志，初始为false

        /**
         * 最小值
         * 波形的最小ADC值
         */
        private int minVal ;                                                             // 成员变量：最小值

        /**
         * 最大值
         * 波形的最大ADC值
         */
        private int maxVal;                                                              // 成员变量：最大值

        /**
         * 周期
         * 波形的周期（时钟周期数）
         * 使用long类型存储，支持大周期测量
         */
        private long cycle;                                                              // 成员变量：周期

        /**
         * 检查数据是否有效
         * 
         * @return true=有效，false=无效
         */
        public boolean isVaild() {                                                       // 公有方法：检查数据有效性
            return bVaild;                                                               // 返回有效标志
        }                                                                                // 方法结束

        /**
         * 设置数据有效标志
         * 
         * @param bVaild 有效标志（true=有效，false=无效）
         */
        public void setVaild(boolean bVaild) {                                           // 公有方法：设置有效标志
            this.bVaild = bVaild;                                                        // 设置有效标志
        }                                                                                // 方法结束

        /**
         * 获取最小值
         * 
         * @return 最小ADC值
         */
        public int getMinVal() {                                                         // 公有方法：获取最小值
            return minVal;                                                               // 返回最小值
        }                                                                                // 方法结束

        /**
         * 设置最小值
         * 
         * @param minVal 最小ADC值
         */
        public void setMinVal(int minVal) {                                              // 公有方法：设置最小值
            this.minVal = minVal;                                                        // 设置最小值
        }                                                                                // 方法结束

        /**
         * 获取最大值
         * 
         * @return 最大ADC值
         */
        public int getMaxVal() {                                                         // 公有方法：获取最大值
            return maxVal;                                                               // 返回最大值
        }                                                                                // 方法结束

        /**
         * 设置最大值
         * 
         * @param maxVal 最大ADC值
         */
        public void setMaxVal(int maxVal) {                                              // 公有方法：设置最大值
            this.maxVal = maxVal;                                                        // 设置最大值
        }                                                                                // 方法结束

        /**
         * 获取周期
         * 
         * @return 周期值（时钟周期数）
         */
        public long getCycle() {                                                         // 公有方法：获取周期
            return cycle;                                                                // 返回周期值
        }                                                                                // 方法结束

        /**
         * 设置周期
         * 将输入值转换为无符号32位整数存储
         * 
         * @param cycle 周期值
         */
        public void setCycle(long cycle) {                                               // 公有方法：设置周期
            this.cycle = (cycle & 0xFFFFFFFFL);                                          // 转换为无符号32位整数并存储
        }                                                                                // 方法结束
    }                                                                                    // 内部类结束

}                                                                                        // 类结束
