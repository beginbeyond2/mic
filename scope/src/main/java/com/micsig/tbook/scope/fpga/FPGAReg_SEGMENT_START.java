package com.micsig.tbook.scope.fpga;   // FPGA寄存器包，包含所有FPGA通信相关的寄存器类

import com.micsig.tbook.scope.Sample.SegmentSample;   // 导入分段采样管理类，用于获取分段采样参数
import com.micsig.tbook.scope.Scope;   // 导入示波器主类，用于获取运行状态

/**
 * FPGA分段起始寄存器类 - 控制分段采样的起始帧号
 * 
 * ┌─────────────────────────────────────────────────────────────────┐
 * │                    FPGA分段起始寄存器架构                        │
 * ├─────────────────────────────────────────────────────────────────┤
 * │                                                                 │
 * │  ┌─────────────────┐      ┌─────────────────┐                  │
 * │  │ SegmentSample   │      │     Scope       │                  │
 * │  │  (分段采样管理)  │      │  (示波器状态)   │                  │
 * │  └────────┬────────┘      └────────┬────────┘                  │
 * │           │                        │                           │
 * │           │ isSegmentEnable()       │ isRun()                   │
 * │           │ getSegmentDisplayType() │                           │
 * │           │ getFrameNo()            │                           │
 * │           │ getFittingBegingFrame() │                           │
 * │           ▼                        ▼                           │
 * │  ┌─────────────────────────────────────────────┐               │
 * │  │        FPGAReg_SEGMENT_START                │               │
 * │  │         (分段起始寄存器)                     │               │
 * │  │                                             │               │
 * │  │  ┌─────────────────────────────────────┐   │               │
 * │  │  │  寄存器数据 (4字节)                  │   │               │
 * │  │  │  ┌───────────────────────────────┐  │   │               │
 * │  │  │  │  位31-0: 起始帧号 (32位)      │  │   │               │
 * │  │  │  │  - 单帧模式: 当前帧号/0x7FFFFFFF│  │   │               │
 * │  │  │  │  - 拟合模式: 拟合起始帧号      │  │   │               │
 * │  │  │  │  - 禁用状态: 0               │  │   │               │
 * │  │  │  └───────────────────────────────┘  │   │               │
 * │  │  └─────────────────────────────────────┘   │               │
 * │  └─────────────────────────────────────────────┘               │
 * │                       │                                       │
 * │                       ▼                                       │
 * │           ┌─────────────────────┐                             │
 * │           │   FPGA硬件 (0x4C)   │                             │
 * │           │  分段采样起始控制   │                             │
 * │           └─────────────────────┘                             │
 * │                                                                 │
 * └─────────────────────────────────────────────────────────────────┘
 * 
 * <h3>模块定位</h3>
 * <p>本类是FPGA分段采样系统的核心组件，负责配置分段采样的起始帧号。
 * 分段采样是示波器的高级采集模式，能够捕获多个波形片段（帧），用于分析
 * 罕见事件或长时间信号变化趋势。</p>
 * 
 * <h3>核心职责</h3>
 * <ul>
 *   <li><b>起始帧号配置</b>：根据分段显示模式设置不同的起始帧号</li>
 *   <li><b>模式适配</b>：支持单帧显示模式和拟合显示模式</li>
 *   <li><b>运行状态响应</b>：在运行状态下自动捕获最新帧</li>
 * </ul>
 * 
 * <h3>分段显示模式说明</h3>
 * <pre>
 * ┌───────────────────────────────────────────────────────────────┐
 * │                    分段采样显示模式                            │
 * ├───────────────────────────────────────────────────────────────┤
 * │                                                               │
 * │  1. 单帧显示模式 (SEGMENT_DISPLAY_SFRAME = 0)                 │
 * │     ┌─────┐ ┌─────┐ ┌─────┐ ┌─────┐ ┌─────┐                  │
 * │     │ 帧1 │ │ 帧2 │ │ 帧3 │ │ 帧4 │ │ 帧5 │ ...              │
 * │     └─────┘ └─────┘ └─────┘ └─────┘ └─────┘                  │
 * │                              ▲                                │
 * │                              │                                │
 * │                     当前显示帧 (frameNo)                      │
 * │     特点：每次只显示一个帧，用户可逐帧浏览                      │
 * │                                                               │
 * │  2. 拟合显示模式 (SEGMENT_DISPLAY_FITTING = 1)                │
 * │     ┌─────┐ ┌─────┐ ┌─────┐ ┌─────┐ ┌─────┐                  │
 * │     │ 帧1 │ │ 帧2 │ │ 帧3 │ │ 帧4 │ │ 帧5 │ ...              │
 * │     └──┬──┘ └──┬──┘ └──┬──┘ └──┬──┘ └──┬──┘                  │
 * │        └────────┴────────┴────────┴────────┘                  │
 * │                         │                                     │
 * │                         ▼                                     │
 * │              ┌─────────────────────┐                         │
 * │              │ 拟合显示帧范围       │                         │
 * │              │ fittingBegingFrame │                         │
 * │              │      到             │                         │
 * │              │ fittingEndFrame     │                         │
 * │              └─────────────────────┘                         │
 * │     特点：将多个帧叠加显示，用于观察信号变化趋势                │
 * │                                                               │
 * └───────────────────────────────────────────────────────────────┘
 * </pre>
 * 
 * <h3>寄存器数据格式</h3>
 * <pre>
 * ┌────────────────────────────────────────────────────────────┐
 * │              寄存器数据结构 (4字节)                         │
 * ├────────────────────────────────────────────────────────────┤
 * │  字节偏移  │  位范围  │  字段名      │  说明               │
 * ├────────────────────────────────────────────────────────────┤
 * │  0-3       │  31-0    │  startFrame  │  起始帧号           │
 * │            │          │              │  - 0: 分段禁用      │
 * │            │          │              │  - frameNo: 单帧    │
 * │            │          │              │  - 0x7FFFFFFF: 运行 │
 * │            │          │              │  - fittingBegin: 拟合│
 * └────────────────────────────────────────────────────────────┘
 * </pre>
 * 
 * <h3>帧号计算逻辑</h3>
 * <pre>
 * ┌─────────────────────────────────────────────────────────────┐
 * │                    帧号计算流程                             │
 * ├─────────────────────────────────────────────────────────────┤
 * │                                                             │
 * │  开始                                                       │
 * │    │                                                        │
 * │    ▼                                                        │
 * │  ┌─────────────────────┐                                   │
 * │  │ 分段采样是否启用？  │                                   │
 * │  │ isSegmentEnable()   │                                   │
 * │  └──────────┬──────────┘                                   │
 * │             │                                               │
 * │     ┌───────┴───────┐                                      │
 * │     │               │                                      │
 * │    否               是                                      │
 * │     │               │                                      │
 * │     ▼               ▼                                      │
 * │  ┌───────┐   ┌─────────────────────┐                      │
 * │  │ 设为0 │   │ 显示模式判断         │                      │
 * │  └───────┘   │ getSegmentDisplayType│                      │
 * │              └──────────┬──────────┘                       │
 * │                         │                                  │
 * │               ┌─────────┴─────────┐                       │
 * │               │                   │                       │
 * │        单帧模式(0)          拟合模式(1)                     │
 * │               │                   │                       │
 * │               ▼                   ▼                       │
 * │      ┌────────────────┐   ┌─────────────────────┐         │
 * │      │ 运行状态判断    │   │ 获取拟合起始帧号    │         │
 * │      │ isRun()         │   │ getFittingBegingFrame│        │
 * │      └────────┬───────┘   └──────────┬──────────┘         │
 * │               │                      │                     │
 * │        ┌──────┴──────┐               │                     │
 * │        │             │               │                     │
 * │      运行           停止              │                     │
 * │        │             │               │                     │
 * │        ▼             ▼               ▼                     │
 * │   ┌──────────┐ ┌──────────┐   ┌──────────┐                │
 * │   │0x7FFFFFFF│ │ frameNo  │   │fittingBegin│              │
 * │   │(最大值)  │ │(当前帧) │   │(拟合起始) │               │
 * │   └──────────┘ └──────────┘   └──────────┘                │
 * │                                                             │
 * └─────────────────────────────────────────────────────────────┘
 * </pre>
 * 
 * <h3>使用场景</h3>
 * <ul>
 *   <li><b>单帧浏览</b>：用户在停止状态下逐帧查看历史波形</li>
 *   <li><b>实时捕获</b>：运行状态下自动显示最新捕获的帧</li>
 *   <li><b>拟合分析</b>：叠加显示多个帧，观察信号抖动和变化趋势</li>
 * </ul>
 * 
 * <h3>依赖关系</h3>
 * <ul>
 *   <li>{@link FPGAReg} - 父类，提供寄存器基础功能</li>
 *   <li>{@link SegmentSample} - 分段采样管理类，提供帧号和显示模式</li>
 *   <li>{@link Scope} - 示波器主类，提供运行状态</li>
 *   <li>{@link FPGACommand} - FPGA命令管理类，负责调用onCommand</li>
 * </ul>
 * 
 * <h3>寄存器地址</h3>
 * <p>寄存器ID: 0x4C (FPGA_SEGMENT_START)</p>
 * 
 * @see FPGAReg
 * @see SegmentSample
 * @see FPGAReg_SegmentFrame
 * @see FPGAReg_SEGMENT_NUMS
 * @author Micsig智能示波器团队
 * @version 2.0
 * @since 1.0
 */
public class FPGAReg_SEGMENT_START extends FPGAReg{   // 继承FPGAReg基类，获得寄存器通用功能
    
    /**
     * 构造函数 - 初始化分段起始寄存器
     * 
     * <p>创建分段起始寄存器实例，配置寄存器ID和数据大小。</p>
     * 
     * <h4>初始化参数</h4>
     * <ul>
     *   <li><b>寄存器ID</b>: FPGA_SEGMENT_START (0x4C)</li>
     *   <li><b>数据大小</b>: 4字节（32位帧号）</li>
     * </ul>
     * 
     * <h4>寄存器地址说明</h4>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器地址映射                         │
     * ├─────────────────────────────────────────┤
     * │  0x4C = FPGA_SEGMENT_START              │
     * │  功能: 设置分段采样的起始帧号           │
     * │  大小: 4字节                            │
     * │  访问: 只写 (由FPGA读取)                │
     * └─────────────────────────────────────────┘
     * </pre>
     * 
     * <h4>调用时机</h4>
     * <p>在FPGACommand初始化时创建实例，存储在fpgaRegs数组中。
     * 当需要更新分段起始帧号时，调用onCommand方法自动计算并设置值。</p>
     * 
     * @see FPGAReg#FPGAReg(int, int)
     * @see FPGACommand#initFpgaRegs()
     */
    public FPGAReg_SEGMENT_START() {   // 构造函数：初始化分段起始寄存器
        super(FPGAReg.FPGA_SEGMENT_START, 4);   // 调用父类构造函数，设置寄存器ID为0x4C，数据大小为4字节
    }   // 构造函数结束

    /**
     * 命令回调方法 - 自动计算并设置分段起始帧号
     * 
     * <p>此方法在每次需要更新FPGA分段起始帧号时被调用，
     * 根据分段采样状态和显示模式自动计算合适的起始帧号。</p>
     * 
     * <h4>计算逻辑详解</h4>
     * <pre>
     * ┌───────────────────────────────────────────────────────────────┐
     * │                    onCommand执行流程                          │
     * ├───────────────────────────────────────────────────────────────┤
     * │                                                               │
     * │  Step 1: 获取分段采样实例                                     │
     * │    └─ SegmentSample.getInstance()                             │
     * │                                                               │
     * │  Step 2: 检查分段采样是否启用                                  │
     * │    └─ segmentSample.isSegmentEnable()                         │
     * │                                                               │
     * │  Step 3: 根据显示模式计算帧号                                  │
     * │    ├─ 单帧模式 (SEGMENT_DISPLAY_SFRAME)                       │
     * │    │   ├─ 运行状态: 设为0x7FFFFFFF (自动捕获最新帧)            │
     * │    │   └─ 停止状态: 设为当前帧号 (用户浏览历史帧)              │
     * │    │                                                           │
     * │    └─ 拟合模式 (SEGMENT_DISPLAY_FITTING)                       │
     * │        └─ 设为拟合起始帧号 (叠加显示帧范围起点)                │
     * │                                                               │
     * │  Step 4: 设置寄存器值                                          │
     * │    └─ setVal(0, frameNo)                                      │
     * │                                                               │
     * └───────────────────────────────────────────────────────────────┘
     * </pre>
     * 
     * <h4>帧号特殊值说明</h4>
     * <table border="1">
     *   <tr><th>帧号值</th><th>含义</th><th>使用场景</th></tr>
     *   <tr><td>0</td><td>禁用状态</td><td>分段采样未启用</td></tr>
     *   <tr><td>0x7FFFFFFF</td><td>最大值(2147483647)</td><td>运行状态，自动捕获最新帧</td></tr>
     *   <tr><td>frameNo</td><td>当前帧号</td><td>停止状态，单帧浏览</td></tr>
     *   <tr><td>fittingBegin</td><td>拟合起始帧</td><td>拟合显示模式</td></tr>
     * </table>
     * 
     * <h4>运行状态特殊处理</h4>
     * <p>当示波器处于运行状态时，将帧号设为0x7FFFFFFF（最大有符号32位整数），
     * 这样FPGA会自动捕获并显示最新的帧数据，实现实时监控功能。</p>
     * 
     * <h4>调用时机</h4>
     * <ul>
     *   <li>分段采样启用/禁用状态改变时</li>
     *   <li>分段显示模式切换时（单帧/拟合）</li>
     *   <li>用户切换帧号时（单帧浏览模式）</li>
     *   <li>示波器运行状态改变时（运行/停止）</li>
     *   <li>拟合帧范围改变时（拟合显示模式）</li>
     * </ul>
     * 
     * @see SegmentSample#isSegmentEnable()
     * @see SegmentSample#getSegmentDisplayType()
     * @see SegmentSample#getFrameNo()
     * @see SegmentSample#getFittingBegingFrame()
     * @see Scope#isRun(boolean)
     * @see FPGAReg#setVal(int, int)
     */
    @Override   // 重写父类FPGAReg的onCommand方法
    public void onCommand() {   // 命令回调方法：自动计算并设置分段起始帧号
        SegmentSample segmentSample = SegmentSample.getInstance();   // 获取分段采样管理实例（单例模式）
        
        if(segmentSample.isSegmentEnable()) {   // 判断：分段采样是否启用
            // 分段采样已启用，根据显示模式设置起始帧号
            if (segmentSample.getSegmentDisplayType() == SegmentSample.SEGMENT_DISPLAY_SFRAME) {   // 判断：是否为单帧显示模式
                // 单帧显示模式：每次只显示一个帧
                int frameno = (int)segmentSample.getFrameNo();   // 获取当前帧号（用户选择的帧号）
                
                if(Scope.getInstance().isRun(true)){   // 判断：示波器是否处于运行状态
                    // 运行状态：设置为最大值，让FPGA自动捕获最新帧
                    frameno =  0x7FFFFFFF;   // 设置为0x7FFFFFFF (2147483647)，FPGA会自动显示最新捕获的帧
                }   // 运行状态判断结束
                
                setVal(0, frameno);   // 设置寄存器值：字节偏移0，值为计算得到的帧号
                
            } else {   // 单帧模式判断结束，进入拟合显示模式
                // 拟合显示模式：叠加显示多个帧
                setVal(0, segmentSample.getFittingBegingFrame());   // 设置寄存器值：使用拟合起始帧号
            }   // 显示模式判断结束
            
        }else{   // 分段采样启用判断结束，分段采样未启用
            // 分段采样未启用：设置为0
            setVal(0,0);   // 设置寄存器值为0，表示分段采样功能关闭
        }   // 分段采样启用判断结束
    }   // onCommand方法结束
}   // FPGAReg_SEGMENT_START类结束