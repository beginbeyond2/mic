package com.micsig.tbook.scope.Trigger;

/**
 * ╔══════════════════════════════════════════════════════════════════════════════╗
 * ║                   TriggerNEdge - 第N边沿触发器类                             ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【模块定位】                                                                 ║
 * ║   Trigger模块的第N边沿触发器实现类，在第N个边沿后触发采集。                   ║
 * ║   第N边沿触发是一种高级触发方式，适用于周期性复杂信号的分析。                 ║
 * ║                                                                              ║
 * ║ 【核心职责】                                                                 ║
 * ║   1. 管理触发边沿类型（上升沿/下降沿）                                        ║
 * ║   2. 管理空闲时间（两次触发之间的最小时间间隔）                              ║
 * ║   3. 管理边沿计数（第N个边沿触发）                                           ║
 * ║   4. 提供单触发源访问接口                                                    ║
 * ║                                                                              ║
 * ║ 【继承体系】                                                                 ║
 * ║                          ┌─────────────────┐                                 ║
 * ║                          │   Trigger       │ ← 抽象基类                      ║
 * ║                          │   (abstract)    │                                 ║
 * ║                          └────────┬────────┘                                 ║
 * ║                                   │                                          ║
 * ║         ┌───────────┬─────────────┼─────────────┬───────────┐               ║
 * ║         │           │             │             │           │               ║
 * ║         ▼           ▼             ▼             ▼           ▼               ║
 * ║   ┌──────────┐ ┌──────────┐ ┌──────────┐ ┌──────────┐ ┌──────────┐         ║
 * ║   │Edge      │ │PulseWidth│ │Logic     │ │Slope     │ │NEdge     │         ║
 * ║   │边沿触发  │ │脉宽触发  │ │逻辑触发  │ │斜率触发  │ │第N边沿   │ ← 本类   ║
 * ║   │(单源)    │ │(单源)    │ │(多源)    │ │(单源)    │ │(单源)    │         ║
 * ║   └──────────┘ └──────────┘ └──────────┘ └──────────┘ └──────────┘         ║
 * ║                                                                              ║
 * ║ 【第N边沿触发说明】                                                          ║
 * ║   第N边沿触发是一种高级触发方式，在第N个边沿后触发采集。                      ║
 * ║   适用于周期性复杂信号的分析，如脉冲序列、时钟信号等。                        ║
 * ║                                                                              ║
 * ║   ┌─────────────────────────────────────────────────────────────────────┐   ║
 * ║   │                     第N边沿触发示意图（N=3）                         │   ║
 * ║   │                                                                     │   ║
 * ║   │      ┌──────┐      ┌──────┐      ┌──────┐                         │   ║
 * ║   │      │      │      │      │      │      │                         │   ║
 * ║   │ ─────┘      └──────┘      └──────┘      └──────                   │   ║
 * ║   │      ↑      ↑      ↑                                              │   ║
 * ║   │     边沿1  边沿2  边沿3                                             │   ║
 * ║   │                   ↑                                                │   ║
 * ║   │                触发点                                              │   ║
 * ║   │                                                                     │   ║
 * ║   │  设置 edge=3，在第3个边沿后触发采集                                 │   ║
 * ║   └─────────────────────────────────────────────────────────────────────┘   ║
 * ║                                                                              ║
 * ║ 【边沿类型说明】                                                             ║
 * ║   ┌────────────────┬────────────────────────────────────────────────────┐  ║
 * ║   │ 类型           │ 说明                                                │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 上升沿(ASC)    │ 检测信号从低电平跳变到高电平的边沿                  │  ║
 * ║   │                │ 适用于检测脉冲的起始位置                            │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 下降沿(DSC)    │ 检测信号从高电平跳变到低电平的边沿                  │  ║
 * ║   │                │ 适用于检测脉冲的结束位置                            │  ║
 * ║   └────────────────┴────────────────────────────────────────────────────┘  ║
 * ║                                                                              ║
 * ║ 【空闲时间说明】                                                             ║
 * ║   空闲时间是指两次触发之间的最小时间间隔。                                   ║
 * ║   在空闲时间内，即使满足触发条件也不会触发。                                 ║
 * ║   用于稳定显示周期性复杂信号，避免误触发。                                   ║
 * ║                                                                              ║
 * ║ 【依赖关系】                                                                 ║
 * ║   → Trigger: 继承触发器基类                                                 ║
 * ║   → TriggerFactory: 工厂类，管理触发器实例                                  ║
 * ║                                                                              ║
 * ║ 【使用场景】                                                                 ║
 * ║   1. 用户选择第N边沿触发类型时，TriggerFactory创建TriggerNEdge实例         ║
 * ║   2. 用户设置边沿计数（第几个边沿触发）时                                    ║
 * ║   3. 用户设置触发边沿类型（上升沿/下降沿）时                                 ║
 * ║   4. 用户设置空闲时间时                                                     ║
 * ║                                                                              ║
 * ║ 【设计模式】                                                                 ║
 * ║   - 模板方法模式（Template Method）：继承Trigger的抽象方法                 ║
 * ║                                                                              ║
 * ║ 【线程安全】                                                                 ║
 * ║   触发参数未加同步保护，建议在UI线程操作。                                   ║
 * ║                                                                              ║
 * ║ 【作者】 Created by zhuzh on 2018-6-1                                        ║
 * ╚══════════════════════════════════════════════════════════════════════════════╝
 */
public class TriggerNEdge extends Trigger {

    // ═══════════════════════════════════════════════════════════════════════════════
    // 触发边沿类型常量定义
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 上升沿触发
     * 检测信号从低电平跳变到高电平的边沿
     * 适用于检测脉冲的起始位置
     */
    public static final int TET_ASC = 0;

    /**
     * 下降沿触发
     * 检测信号从高电平跳变到低电平的边沿
     * 适用于检测脉冲的结束位置
     */
    public static final int TET_DSC = 1;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 空闲时间（FPGA单位）
     * 单位：纳秒
     * 默认值：8ns
     *
     * 空闲时间是指两次触发之间的最小时间间隔。
     * 在空闲时间内，即使满足触发条件也不会触发。
     * 用于稳定显示周期性复杂信号，避免误触发。
     */
    private long idleTime = 8;

    /**
     * 触发边沿类型
     * 取值：TET_ASC 或 TET_DSC
     * 默认值：TET_ASC（上升沿触发）
     */
    private int slope = TET_ASC;

    /**
     * 边沿计数
     * 在第N个边沿后触发采集
     * 默认值：1（第1个边沿触发，等同于普通边沿触发）
     *
     * <p><b>示例：</b>
     * <pre>
     * edge = 1 → 第1个边沿触发（等同于普通边沿触发）
     * edge = 2 → 第2个边沿触发
     * edge = 3 → 第3个边沿触发
     * </pre>
     */
    private int edge = 1;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造第N边沿触发器实例
     * 调用父类Trigger构造方法设置触发类型为第N边沿触发
     *
     * <p><b>初始化流程：</b>
     * <ol>
     *   <li>调用父类Trigger构造方法，设置触发类型为TRIG_TYPE_NEDGE</li>
     * </ol>
     *
     * @example
     *   TriggerNEdge trigger = new TriggerNEdge();
     */
    public TriggerNEdge() {
        super(Trigger.TRIG_TYPE_NEDGE);
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 空闲时间访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取空闲时间
     *
     * @return 空闲时间（纳秒）
     */
    public long getIdleTime() {
        return idleTime;
    }

    /**
     * 设置空闲时间
     * 更新空闲时间并发送FPGA消息
     *
     * @param idleTime 空闲时间（纳秒）
     *                 用于设置两次触发之间的最小时间间隔
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setIdleTime(long idleTime) {
        this.idleTime = idleTime;
        triggerParamChange();
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 触发边沿类型访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取触发边沿类型
     *
     * @return 触发边沿类型
     *         TET_ASC (0): 上升沿触发
     *         TET_DSC (1): 下降沿触发
     */
    public int getSlope() {
        return slope;
    }

    /**
     * 设置触发边沿类型
     * 更新触发边沿类型并发送FPGA消息
     *
     * @param slope 触发边沿类型
     *              TET_ASC: 上升沿触发
     *              TET_DSC: 下降沿触发
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setSlope(int slope) {
        this.slope = slope;
        triggerParamChange();
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 边沿计数访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取边沿计数
     *
     * @return 边沿计数（第N个边沿触发）
     */
    public int getEdge() {
        return edge;
    }

    /**
     * 设置边沿计数
     * 更新边沿计数并发送FPGA消息
     *
     * @param edge 边沿计数
     *             1: 第1个边沿触发（等同于普通边沿触发）
     *             2: 第2个边沿触发
     *             3: 第3个边沿触发
     *             等等...
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setEdge(int edge) {
        this.edge = edge;
        triggerParamChange();
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 抽象方法实现 - 继承自Trigger
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取触发源数量
     * 第N边沿触发只支持单触发源
     *
     * @return 始终返回1，表示单触发源
     */
    @Override
    public int getTriggerSourceCnt() {
        return 1;
    }

    /**
     * 判断是否支持多触发源
     * 第N边沿触发只支持单触发源
     *
     * @return 始终返回false，表示只支持单触发源
     */
    @Override
    protected boolean isMultitriggerSource() {
        return false;
    }


}
