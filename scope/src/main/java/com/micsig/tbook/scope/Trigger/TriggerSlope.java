package com.micsig.tbook.scope.Trigger;

/**
 * ╔══════════════════════════════════════════════════════════════════════════════╗
 * ║                   TriggerSlope - 斜率触发器类                                ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【模块定位】                                                                 ║
 * ║   Trigger模块的斜率触发器实现类，根据信号斜率条件触发采集。                   ║
 * ║   斜率触发是一种高级触发方式，适用于检测信号变化速率满足特定条件的波形。      ║
 * ║                                                                              ║
 * ║ 【核心职责】                                                                 ║
 * ║   1. 管理斜率方向（上升沿/下降沿/任意）                                      ║
 * ║   2. 管理斜率条件（小于/大于/之间）                                          ║
 * ║   3. 管理斜率时间参数                                                        ║
 * ║   4. 支持双触发电平（上限电平和下限电平）                                    ║
 * ║   5. 提供单触发源访问接口                                                    ║
 * ║                                                                              ║
 * ║ 【继承体系】                                                                 ║
 * ║                          ┌─────────────────┐                                 ║
 * ║                          │   Trigger       │ ← 抽象基类                      ║
 * ║                          │   (abstract)    │                                 ║
 * ║                          └────────┬────────┘                                 ║
 * ║                                   │                                          ║
 * ║         ┌───────────┬─────────────┼─────────────┬───────────┐               ║
 * ║         │           │             │             │           │               ║
 * ║         ▼           ▼             ▼             ▼           ▼               ║
 * ║   ┌──────────┐ ┌──────────┐ ┌──────────┐ ┌──────────┐ ┌──────────┐         ║
 * ║   │Edge      │ │PulseWidth│ │Logic     │ │Slope     │ │TimeOut   │         ║
 * ║   │边沿触发  │ │脉宽触发  │ │逻辑触发  │ │斜率触发  │ │超时触发  │         ║
 * ║   │(单源)    │ │(单源)    │ │(多源)    │ │(单源)    │ │(单源)    │         ║
 * ║   └──────────┘ └──────────┘ └──────────┘ └──────────┘ └──────────┘         ║
 * ║                                              ↑                              ║
 * ║                                            本类                             ║
 * ║                                                                              ║
 * ║ 【斜率触发说明】                                                             ║
 * ║   斜率触发根据信号的变化速率（斜率）来触发采集。                              ║
 * ║   斜率 = ΔV / Δt，即单位时间内电压的变化量。                                 ║
 * ║   当信号斜率满足设定条件时，示波器触发采集。                                  ║
 * ║                                                                              ║
 * ║   ┌─────────────────────────────────────────────────────────────────────┐   ║
 * ║   │                     斜率触发示意图                                   │   ║
 * ║   │                                                                     │   ║
 * ║   │  上升斜率：                                                         │   ║
 * ║   │      上限电平 ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─                    │   ║
 * ║   │                                 ┌──────                            │   ║
 * ║   │                                /                                    │   ║
 * ║   │      下限电平 ─ ─ ─ ─ ─ ─ ─ ─/─ ─ ─ ─ ─ ─ ─ ─ ─                   │   ║
 * ║   │                            /                                        │   ║
 * ║   │ ──────────────────────────/                                         │   ║
 * ║   │                          ←─ Δt ─→                                   │   ║
 * ║   │                                                                     │   ║
 * ║   │  斜率 = (上限电平 - 下限电平) / Δt                                  │   ║
 * ║   │  当斜率满足条件时，触发采集                                          │   ║
 * ║   │                                                                     │   ║
 * ║   │  下降斜率：                                                         │   ║
 * ║   │                                 ┌──────                            │   ║
 * ║   │      上限电平 ─ ─ ─ ─ ─ ─ ─ ─ ─│─ ─ ─ ─ ─ ─ ─ ─ ─                   │   ║
 * ║   │                                \                                    │   ║
 * ║   │      下限电平 ─ ─ ─ ─ ─ ─ ─ ─ \─ ─ ─ ─ ─ ─ ─ ─ ─                   │   ║
 * ║   │                                 \                                   │   ║
 * ║   │ ─────────────────────────────────┘                                  │   ║
 * ║   │                          ←─ Δt ─→                                   │   ║
 * ║   │                                                                     │   ║
 * ║   │  斜率 = (下限电平 - 上限电平) / Δt（负值）                          │   ║
 * ║   │  当斜率满足条件时，触发采集                                          │   ║
 * ║   └─────────────────────────────────────────────────────────────────────┘   ║
 * ║                                                                              ║
 * ║ 【斜率方向说明】                                                             ║
 * ║   ┌────────────────┬────────────────────────────────────────────────────┐  ║
 * ║   │ 方向           │ 说明                                                │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 上升沿(ASC)    │ 检测信号从低电平上升到高电平的斜率                  │  ║
 * ║   │                │ 斜率为正值                                          │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 下降沿(DSC)    │ 检测信号从高电平下降到低电平的斜率                  │  ║
 * ║   │                │ 斜率为负值                                          │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 任意(EITHER)   │ 检测上升和下降斜率                                  │  ║
 * ║   │                │ 适用于检测任意方向的斜率                            │  ║
 * ║   └────────────────┴────────────────────────────────────────────────────┘  ║
 * ║                                                                              ║
 * ║ 【斜率条件说明】                                                             ║
 * ║   ┌────────────────┬────────────────────────────────────────────────────┐  ║
 * ║   │ 条件           │ 说明                                                │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 小于(LESSER)   │ 斜率小于设定值时触发                                │  ║
 * ║   │                │ 适用于检测缓慢变化的信号                            │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 大于(GREATER)  │ 斜率大于设定值时触发                                │  ║
 * ║   │                │ 适用于检测快速变化的信号                            │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 之间(BETWEEN)  │ 斜率在设定范围内时触发                              │  ║
 * ║   │                │ 适用于检测特定变化速率的信号                        │  ║
 * ║   └────────────────┴────────────────────────────────────────────────────┘  ║
 * ║                                                                              ║
 * ║ 【双触发电平说明】                                                           ║
 * ║   斜率触发需要两个触发电平来计算斜率：                                       ║
 * ║   - 上限电平（TRIGGER_LEVEL_HIGH）：斜率计算的终点电平                     ║
 * ║   - 下限电平（TRIGGER_LEVEL_NORMAL）：斜率计算的起点电平                   ║
 * ║   斜率 = (上限电平 - 下限电平) / 时间                                       ║
 * ║                                                                              ║
 * ║ 【依赖关系】                                                                 ║
 * ║   → Trigger: 继承触发器基类                                                 ║
 * ║   → TriggerFactory: 工厂类，管理触发器实例                                  ║
 * ║   → TriggerLevel: 触发电平管理（需要两个触发电平）                          ║
 * ║                                                                              ║
 * ║ 【使用场景】                                                                 ║
 * ║   1. 用户选择斜率触发类型时，TriggerFactory创建TriggerSlope实例            ║
 * ║   2. 用户设置斜率方向（上升沿/下降沿/任意）时                                ║
 * ║   3. 用户设置斜率条件时                                                     ║
 * ║   4. 用户设置斜率时间参数时                                                 ║
 * ║   5. 用户调整两个触发电平时                                                 ║
 * ║                                                                              ║
 * ║ 【设计模式】                                                                 ║
 * ║   - 模板方法模式（Template Method）：继承Trigger的抽象方法                 ║
 * ║                                                                              ║
 * ║ 【线程安全】                                                                 ║
 * ║   触发参数未加同步保护，建议在UI线程操作。                                   ║
 * ║                                                                              ║
 * ║ 【作者】 Created by zhuzh on 2018-6-1                                        ║
 * ╚══════════════════════════════════════════════════════════════════════════════╝
 */
public class TriggerSlope extends Trigger {

    // ═══════════════════════════════════════════════════════════════════════════════
    // 斜率条件常量定义
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 斜率条件：小于
     * 斜率小于设定值时触发
     * 适用于检测缓慢变化的信号
     */
    public static final int SLOPE_RELATION_LESSER = TRIGGER_RELATION_LESS_THAN;

    /**
     * 斜率条件：大于
     * 斜率大于设定值时触发
     * 适用于检测快速变化的信号
     */
    public static final int SLOPE_RELATION_GREATER = TRIGGER_RELATION_MORE_THAN;

    /**
     * 斜率条件：之间
     * 斜率在设定范围内时触发
     * 适用于检测特定变化速率的信号
     */
    public static final int SLOPE_RELATION_BETWEEN = TRIGGER_RELATION_EQUAL;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 斜率方向常量定义
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 上升沿斜率
     * 检测信号从低电平上升到高电平的斜率
     * 斜率为正值
     */
    public static final int SLOPE_ASC = 0;

    /**
     * 下降沿斜率
     * 检测信号从高电平下降到低电平的斜率
     * 斜率为负值
     */
    public static final int SLOPE_DSC = 1;

    /**
     * 任意斜率
     * 检测上升和下降斜率
     * 适用于检测任意方向的斜率
     */
    public static final int SLOPE_EITHER = 2;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 当前斜率方向
     * 取值：SLOPE_ASC、SLOPE_DSC 或 SLOPE_EITHER
     * 默认值：SLOPE_ASC（上升沿）
     */
    private int edge = SLOPE_ASC;

    /**
     * 当前斜率条件
     * 取值：SLOPE_RELATION_LESSER、SLOPE_RELATION_GREATER 或 SLOPE_RELATION_BETWEEN
     * 默认值：SLOPE_RELATION_LESSER（小于）
     */
    private int condition = SLOPE_RELATION_LESSER;

    /**
     * 低电平时间（FPGA单位）
     * 单位：纳秒
     * 默认值：8ns
     *
     * 用于设置斜率计算的时间窗口下限
     */
    private long timeLow = 8;

    /**
     * 高电平时间（FPGA单位）
     * 单位：纳秒
     * 默认值：8ns
     *
     * 用于设置斜率计算的时间窗口上限
     */
    private long timeHigh = 8;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造斜率触发器实例
     * 调用父类Trigger构造方法设置触发类型为斜率触发
     *
     * <p><b>初始化流程：</b>
     * <ol>
     *   <li>调用父类Trigger构造方法，设置触发类型为TRIG_TYPE_SLOPE</li>
     * </ol>
     *
     * @example
     *   TriggerSlope trigger = new TriggerSlope();
     */
    public TriggerSlope() {
        super(Trigger.TRIG_TYPE_SLOPE);
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 斜率方向访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取当前斜率方向
     *
     * @return 斜率方向
     *         SLOPE_ASC (0): 上升沿
     *         SLOPE_DSC (1): 下降沿
     *         SLOPE_EITHER (2): 任意
     */
    public int getEdge() {
        return edge;
    }

    /**
     * 设置斜率方向
     * 更新斜率方向并发送FPGA消息
     *
     * @param edge 斜率方向
     *             SLOPE_ASC: 上升沿
     *             SLOPE_DSC: 下降沿
     *             SLOPE_EITHER: 任意
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setEdge(int edge) {
        this.edge = edge;
        triggerParamChange();
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 斜率条件访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取当前斜率条件
     *
     * @return 斜率条件
     *         SLOPE_RELATION_LESSER: 小于
     *         SLOPE_RELATION_GREATER: 大于
     *         SLOPE_RELATION_BETWEEN: 之间
     */
    public int getCondition() {
        return condition;
    }

    /**
     * 设置斜率条件
     * 更新斜率条件并发送FPGA消息
     *
     * @param condition 斜率条件
     *                  SLOPE_RELATION_LESSER: 小于
     *                  SLOPE_RELATION_GREATER: 大于
     *                  SLOPE_RELATION_BETWEEN: 之间
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setCondition(int condition) {
        this.condition = condition;
        triggerParamChange();
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 时间参数访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取高电平时间
     *
     * @return 高电平时间（纳秒）
     */
    public long getTimeHigh() {
        return timeHigh;
    }

    /**
     * 设置高电平时间
     * 更新高电平时间并发送FPGA消息
     *
     * @param timeHigh 高电平时间（纳秒）
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setTimeHigh(long timeHigh) {
        this.timeHigh = timeHigh;
        triggerParamChange();
    }

    /**
     * 获取低电平时间
     *
     * @return 低电平时间（纳秒）
     */
    public long getTimeLow(){
        return timeLow;
    }

    /**
     * 设置低电平时间
     * 更新低电平时间并发送FPGA消息
     *
     * @param timeLow 低电平时间（纳秒）
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setTimeLow(long timeLow){
        this.timeLow = timeLow;
        triggerParamChange();
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 抽象方法实现 - 继承自Trigger
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取触发源对应的触发电平数量
     * 斜率触发需要两个触发电平：上限电平和下限电平
     *
     * @return 始终返回2，表示需要两个触发电平
     *
     * @see TriggerLevel#TRIGGER_LEVEL_NORMAL
     * @see TriggerLevel#TRIGGER_LEVEL_HIGH
     */
    @Override
    public int getSrcTriggerLevelCnt() {
        return 2;
    }

    /**
     * 获取触发源数量
     * 斜率触发只支持单触发源
     *
     * @return 始终返回1，表示单触发源
     */
    @Override
    public int getTriggerSourceCnt() {
        return 1;
    }

    /**
     * 判断是否支持多触发源
     * 斜率触发只支持单触发源
     *
     * @return 始终返回false，表示只支持单触发源
     */
    @Override
    protected boolean isMultitriggerSource() {
        return false;
    }

}
