package com.micsig.tbook.scope.probe.bean;

import java.util.LinkedHashMap;
import java.util.Map;

/**
 * ╔══════════════════════════════════════════════════════════════════════════════╗
 * ║                BeanFactory - 探头配置工厂类                                   ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【模块定位】                                                                 ║
 * ║   Probe模块的探头配置工厂类，位于probe.bean包下，                              ║
 * ║   负责创建和管理BaseBean实例，实现探头配置的统一创建入口。                       ║
 * ║                                                                              ║
 * ║ 【核心职责】                                                                 ║
 * ║   1. 管理探头类型与配置类的映射关系                                            ║
 * ║   2. 根据探头名称创建对应的BaseBean实例                                        ║
 * ║   3. 支持前缀匹配查找探头配置                                                  ║
 * ║   4. 提供单例访问入口                                                         ║
 * ║                                                                              ║
 * ║ 【设计模式】                                                                 ║
 * ║   ┌─────────────────────────────────────────────────────────────────────┐   ║
 * ║   │                        设计模式应用                                   │   ║
 * ║   │                                                                     │   ║
 * ║   │   1. 单例模式（Singleton）                                           │   ║
 * ║   │      - 私有静态实例变量                                              │   ║
 * ║   │      - 同步的getInstance()方法                                       │   ║
 * ║   │      - 全局唯一访问点                                                │   ║
 * ║   │                                                                     │   ║
 * ║   │   2. 工厂模式（Factory）                                             │   ║
 * ║   │      - getBean()方法创建产品对象                                     │   ║
 * ║   │      - 解耦客户端与具体产品类                                        │   ║
 * ║   │      - 通过反射动态创建实例                                          │   ║
 * ║   │                                                                     │   ║
 * ║   │   3. 注册表模式（Registry）                                          │   ║
 * ║   │      - Map维护类型名称到Class对象的映射                              │   ║
 * ║   │      - initMap()初始化注册表                                        │   ║
 * ║   └─────────────────────────────────────────────────────────────────────┘   ║
 * ║                                                                              ║
 * ║ 【工厂架构图】                                                               ║
 * ║   ┌─────────────────────────────────────────────────────────────────────┐   ║
 * ║   │                         BeanFactory                                 │   ║
 * ║   │                                                                     │   ║
 * ║   │   ┌──────────────────────────────────────────────────────────┐     │   ║
 * ║   │   │              注册表（Map<String, Class<?>>）               │     │   ║
 * ║   │   │                                                          │     │   ║
 * ║   │   │   "MSP500"  →  MSP500Bean.class                          │     │   ║
 * ║   │   │   "MDP"     →  MDPBean.class                             │     │   ║
 * ║   │   │   ...                                                     │     │   ║
 * ║   │   └──────────────────────────────────────────────────────────┘     │   ║
 * ║   │                                                                     │   ║
 * ║   │   ┌────────────────────┐      ┌────────────────────┐              │   ║
 * ║   │   │   getBean(name)    │      │ getMatchBean(name) │              │   ║
 * ║   │   │   精确匹配创建      │      │   前缀匹配创建      │              │   ║
 * ║   │   └─────────┬──────────┘      └─────────┬──────────┘              │   ║
 * ║   │             │                           │                          │   ║
 * ║   │             │         ┌─────────────────┘                          │   ║
 * ║   │             │         │                                            │   ║
 * ║   │             ▼         ▼                                            │   ║
 * ║   │      ┌──────────────────────┐                                      │   ║
 * ║   │      │  Class.newInstance() │                                      │   ║
 * ║   │      │  反射创建实例         │                                      │   ║
 * ║   │      └──────────┬───────────┘                                      │   ║
 * ║   │                 │                                                  │   ║
 * ║   │                 ▼                                                  │   ║
 * ║   │      ┌──────────────────────┐                                      │   ║
 * ║   │      │    BaseBean实例      │                                      │   ║
 * ║   │      │  (MSP500Bean/MDPBean)│                                      │   ║
 * ║   │      └──────────────────────┘                                      │   ║
 * ║   └─────────────────────────────────────────────────────────────────────┘   ║
 * ║                                                                              ║
 * ║ 【使用流程】                                                                 ║
 * ║   ┌─────────────┐    ┌─────────────┐    ┌─────────────┐                   ║
 * ║   │ 获取工厂实例 │───▶│ 调用创建方法 │───▶│ 获得BaseBean │                   ║
 * ║   │ getInstance │    │ getBean()   │    │   实例      │                   ║
 * ║   └─────────────┘    └─────────────┘    └─────────────┘                   ║
 * ║                                                                              ║
 * ║ 【应用场景】                                                                 ║
 * ║   1. ProbeFactory创建探头时获取配置                                          ║
 * ║   2. 探头自动识别时匹配配置                                                  ║
 * ║   3. 探头配置加载时创建实例                                                  ║
 * ║                                                                              ║
 * ║ 【线程安全】                                                                 ║
 * ║   - getInstance()使用synchronized保证单例创建的线程安全                      ║
 * ║   - 成员变量map使用LinkedHashMap，非线程安全，建议在初始化时完成注册          ║
 * ║                                                                              ║
 * ║ 【扩展方式】                                                                 ║
 * ║   添加新探头类型：                                                           ║
 * ║   1. 创建新的BaseBean子类（如XXXBean）                                       ║
 * ║   2. 在initMap()中注册映射关系                                               ║
 * ║   3. 无需修改其他代码                                                        ║
 * ║                                                                              ║
 * ║ 【依赖关系】                                                                 ║
 * ║   - BaseBean: 产品抽象类，工厂创建的对象基类                                 ║
 * ║   - MSP500Bean: 具体产品类，MSP500探头配置                                   ║
 * ║   - MDPBean: 具体产品类，MDP差分探头配置                                     ║
 * ║                                                                              ║
 * ║ 【作者】 MHO项目组                                                           ║
 * ╚══════════════════════════════════════════════════════════════════════════════╝
 */
public class BeanFactory {

    // ═══════════════════════════════════════════════════════════════════════════════
    // 单例实例
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 单例实例
     * 存储BeanFactory的唯一实例
     * 使用类级同步保证线程安全
     */
    static BeanFactory instance;

    /**
     * 获取单例实例
     * 使用双重检查锁定模式（DCL）的简化版本
     * 线程安全方法
     *
     * <p><b>实现说明：</b></p>
     * <ul>
     *   <li>使用synchronized同步整个方法，简化实现</li>
     *   <li>延迟初始化，首次调用时创建实例</li>
     *   <li>返回全局唯一的工厂实例</li>
     * </ul>
     *
     * @return BeanFactory单例实例
     */
    public static BeanFactory getInstance(){
        synchronized (BeanFactory.class) {                                          // 同步锁，保证线程安全
            if (instance == null) {                                                 // 检查实例是否已创建
                instance = new BeanFactory();                                       // 创建单例实例
            }
            return instance;                                                        // 返回单例实例
        }
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 探头类型注册表
     * 维护探头类型名称到Class对象的映射关系
     * 
     * <p><b>数据结构：</b></p>
     * <pre>
     * Key: 探头类型名称（如"MSP500"、"MDP"）
     * Value: 对应的BaseBean子类Class对象
     * </pre>
     * 
     * <p><b>使用LinkedHashMap的原因：</b></p>
     * <ul>
     *   <li>保持注册顺序，便于前缀匹配时的优先级控制</li>
     *   <li>快速的查找性能（O(1)）</li>
     * </ul>
     */
    private Map<String,Class<?>> map = new LinkedHashMap<>();

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造BeanFactory实例
     * 初始化探头类型注册表
     * 
     * <p><b>初始化流程：</b></p>
     * <ol>
     *   <li>调用initMap()初始化注册表</li>
     *   <li>注册所有已知的探头类型</li>
     * </ol>
     */
    public BeanFactory(){
        initMap();                                                                  // 初始化探头类型注册表
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 初始化方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 初始化探头类型注册表
     * 注册所有支持的探头类型到映射表
     * 
     * <p><b>注册规则：</b></p>
     * <ul>
     *   <li>Key: 探头类型标识符（定义在具体Bean类中的常量）</li>
     *   <li>Value: 对应的BaseBean子类Class对象</li>
     * </ul>
     * 
     * <p><b>当前支持的探头类型：</b></p>
     * <pre>
     * ┌──────────────┬─────────────────────────────────────┐
     * │ 类型标识      │ 配置类                              │
     * ├──────────────┼─────────────────────────────────────┤
     * │ MSP500       │ MSP500Bean（MSP500系列探头）         │
     * │ MDP          │ MDPBean（MDP差分探头系列）           │
     * └──────────────┴─────────────────────────────────────┘
     * </pre>
     * 
     * <p><b>扩展方式：</b></p>
     * <ol>
     *   <li>创建新的BaseBean子类</li>
     *   <li>在子类中定义类型标识常量</li>
     *   <li>在此方法中添加注册语句</li>
     * </ol>
     */
    private void initMap(){
        map.clear();                                                                // 清空注册表
        map.put(MSP500Bean.MSP500,MSP500Bean.class);                                // 注册MSP500探头配置
        map.put(MDPBean.MDP, MDPBean.class);                                        // 注册MDP差分探头配置
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 工厂方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 根据探头名称前缀匹配创建配置实例
     * 支持模糊匹配，适用于探头自动识别场景
     * 
     * <p><b>匹配规则：</b></p>
     * <ul>
     *   <li>遍历注册表，检查名称是否以注册的Key开头</li>
     *   <li>找到第一个匹配的Key后立即返回</li>
     *   <li>使用LinkedHashMap保证注册顺序即为匹配优先级</li>
     * </ul>
     * 
     * <p><b>使用场景：</b></p>
     * <ul>
     *   <li>探头自动识别时，根据探头型号名称匹配配置</li>
     *   <li>支持同一系列探头的统一配置（如MSP500-100、MSP500-200都匹配MSP500）</li>
     * </ul>
     * 
     * <p><b>示例：</b></p>
     * <pre>
     * getMatchBean("MSP500-100") → 返回MSP500Bean实例
     * getMatchBean("MDP1001")    → 返回MDPBean实例
     * getMatchBean("UNKNOWN")    → 返回null
     * </pre>
     *
     * @param name 探头名称或型号标识
     * @return 匹配的BaseBean实例，未匹配返回null
     */
    public BaseBean getMatchBean(String name){
        BaseBean bean = null;                                                       // 初始化返回值为null
        for (Map.Entry<String,Class<?>> entry : map.entrySet()) {                   // 遍历注册表
            if(name.startsWith(entry.getKey())){                                    // 检查名称是否以Key开头
                bean = getBean(entry.getKey());                                     // 使用精确匹配创建实例
                break;                                                              // 找到匹配后跳出循环
            }
        }
        return bean;                                                                // 返回匹配的实例或null
    }

    /**
     * 根据探头类型名称精确创建配置实例
     * 工厂方法核心实现，通过反射创建对象
     * 
     * <p><b>创建流程：</b></p>
     * <ol>
     *   <li>验证名称参数有效性</li>
     *   <li>从注册表查找对应的Class对象</li>
     *   <li>通过反射调用newInstance()创建实例</li>
     *   <li>捕获并处理可能的异常</li>
     * </ol>
     * 
     * <p><b>异常处理：</b></p>
     * <ul>
     *   <li>IllegalAccessException: 无访问权限（构造方法非public）</li>
     *   <li>InstantiationException: 实例化失败（抽象类、接口等）</li>
     *   <li>异常时打印堆栈跟踪，返回null</li>
     * </ul>
     * 
     * <p><b>使用示例：</b></p>
     * <pre>
     * BaseBean msp500 = BeanFactory.getInstance().getBean("MSP500");
     * BaseBean mdp = BeanFactory.getInstance().getBean("MDP");
     * </pre>
     *
     * @param name 探头类型名称（必须与注册表中的Key完全匹配）
     * @return BaseBean实例，名称无效或创建失败返回null
     */
    public BaseBean getBean(String name){
        BaseBean probe = null;                                                      // 初始化返回值为null
        if(name != null && !name.isEmpty()) {                                       // 检查名称有效性
            Class<?> c = map.get(name);                                             // 从注册表获取Class对象
            if (c != null) {                                                        // Class对象存在
                try {
                    probe = (BaseBean) c.newInstance();                             // 通过反射创建实例
                } catch (IllegalAccessException | InstantiationException e) {       // 捕获异常
                    e.printStackTrace();                                            // 打印异常堆栈
                }
            }
        }
        return probe;                                                               // 返回创建的实例或null
    }
}
