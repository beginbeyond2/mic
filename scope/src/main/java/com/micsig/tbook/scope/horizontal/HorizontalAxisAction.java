/**
 * 示波器水平轴动作处理类
 * 继承自XAction基类，负责处理水平轴相关的硬件和UI动作
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>时基档位变化：发送FPGA命令、UI消息和事件通知</li>
 *   <li>时间位置变化：使用延时过滤机制，避免频繁发送FPGA命令</li>
 *   <li>缩放模式处理：确保放大窗口时基不大于主窗口时基</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 在HorizontalAxis中创建HorizontalAxisAction实例
 * HorizontalAxisAction action = new HorizontalAxisAction(horizontalAxis);
 *
 * // 时基档位变化时调用
 * action.timeScaleIdChange();
 *
 * // 时间位置变化时调用
 * action.timePosChange();
 * </pre>
 *
 * @see HorizontalAxis
 * @see XAction
 * @see FilterThread
 * @see FPGAMessage
 * @see UiMessage
 */
public class HorizontalAxisAction extends XAction {

    // ═══════════════════════════════════════════════════════════════════════════════
    // 日志标签
    // ═══════════════════════════════════════════════════════════════════════════════

    /** 日志标签，用于日志输出时标识此类 */
    private static final String TAG = "HorizontalAxisAction";

    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /** 水平轴数据管理对象，用于获取时基和时间位置数据 */
    private HorizontalAxis horizontalAxis;

    /** 过滤线程，用于延时过滤时间位置变化，避免频繁发送FPGA命令 */
    private FilterThread filterThread;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化水平轴动作处理器，创建过滤线程
     *
     * @param horizontalAxis 水平轴数据管理对象
     */
    public HorizontalAxisAction(HorizontalAxis horizontalAxis) {
        this.horizontalAxis = horizontalAxis;                                           // 保存水平轴引用
        filterThread = new FilterThread(TAG);                                           // 创建过滤线程，使用TAG作为线程名
        filterThread.setDelayMillis(75);                                                // 设置延时时间为75ms
        filterThread.setRunnable(new Runnable() {                                       // 设置延时执行的任务
            @Override
            public void run() {                                                         // 任务执行方法
                timePosMove();                                                          // 调用时间位置移动方法
            }
        });
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 时基档位变化处理方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 处理时基档位变化
     *
     * <p><b>处理流程：</b></p>
     * <ol>
     *   <li>缩放模式检查：确保放大窗口时基不大于主窗口时基</li>
     *   <li>构建FPGA命令：根据运行状态构建不同的命令组合</li>
     *   <li>发送FPGA命令：控制硬件更新采样参数</li>
     *   <li>发送事件通知：通知其他模块时基已变化</li>
     *   <li>发送UI消息：更新界面显示</li>
     * </ol>
     *
     * <p><b>缩放模式处理：</b></p>
     * <ul>
     *   <li>如果放大窗口时基档位大于主窗口时基档位，则同步为主窗口时基</li>
     *   <li>发送UI消息通知时基变化</li>
     * </ul>
     *
     * <p><b>FPGA命令说明：</b></p>
     * <ul>
     *   <li>基础命令：显示控制(DIS)、触发偏移(TRIG_OFFSET)</li>
     *   <li>运行状态额外命令：采样模式、采样位置、触发回差、存储深度、插值控制、自动触发时间、串行解码深度</li>
     *   <li>扩展命令：插值系数</li>
     * </ul>
     */
    public void timeScaleIdChange() {
        // ─────────────────────────────────────────────────────────────────────────
        // 缩放模式处理：确保放大窗口时基不大于主窗口时基
        // ─────────────────────────────────────────────────────────────────────────
        if (Scope.getInstance().isZoom()) {                                             // 判断是否为缩放模式
            int mainID = HorizontalAxis.getInstance().getTimeScaleIdOfView(HorizontalAxis.WPI_STANDARD); // 获取主窗口时基档位ID
            if (mainID > HorizontalAxis.getInstance().getTimeScaleIdOfView(HorizontalAxis.WPI_LARGE)) { // 主窗口时基档位ID大于放大窗口
                HorizontalAxis.getInstance().setTimeScaleIdOfView(mainID);              // 将放大窗口时基同步为主窗口时基
            }
            sendUiMsg(UiMessage.UI_MESSAGE_TIME_SCALE_CHANGE);                          // 发送UI消息通知时基变化
        }

        // ─────────────────────────────────────────────────────────────────────────
        // 构建FPGA命令
        // ─────────────────────────────────────────────────────────────────────────
        int cmd = FPGAMessage.FPGA_CMD_DIS | FPGAMessage.FPGA_CMD_TRIG_OFFSET;          // 基础命令：显示控制 + 触发偏移

        if (Scope.getInstance().isRun()) {                                              // 运行状态
            cmd |= FPGAMessage.FPGA_CMD_SAMP_MODE                                       // 采样模式
                    | FPGAMessage.FPGA_CMD_SAMP_PLACE                                   // 采样位置
                    | FPGAMessage.FPGA_CMD_TRIG_HUICHA                                  // 触发回差
                    | FPGAMessage.FPGA_CMD_SAMP_ZUN_DEPTH                               // 存储深度（可能会变化）
                    | FPGAMessage.FPGA_CMD_COUY                                         // 插值控制
                    | FPGAMessage.FPGA_CMD_AUTO_TRIG_T                                  // 自动触发时间
                    | FPGAMessage.FPGA_CMD_SERIAL_CODE_DEPTH;                           // 串行解码深度
        }

        // ─────────────────────────────────────────────────────────────────────────
        // 发送FPGA命令和通知
        // ─────────────────────────────────────────────────────────────────────────
        sendFpgaMsg(cmd, FPGAMessage.FPGA_CMD_EXT_CHAZHI_COEF);                         // 发送FPGA命令，包含插值系数
        sendEvent(EventFactory.EVENT_TIME_SCALE);                                       // 发送时基变化事件
        sendUiMsg(UiMessage.UI_MESSAGE_DEPTH_SAMPFRE);                                  // 发送深度和采样频率消息
        sendUiMsg(UiMessage.UI_MESSAGE_SIMPLE_WIN_DIS);                                 // 发送简单窗口显示消息
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 时间位置移动方法（内部方法）
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 处理时间位置移动（内部方法）
     *
     * <p>此方法由FilterThread延时调用，用于发送FPGA命令更新采样位置。</p>
     *
     * <p><b>FPGA命令说明：</b></p>
     * <ul>
     *   <li>基础命令：显示控制(DIS)</li>
     *   <li>运行状态额外命令：采样位置(SAMP_PLACE)</li>
     * </ul>
     *
     * <p><b>注意：</b></p>
     * <ul>
     *   <li>此方法通过FilterThread延时75ms执行</li>
     *   <li>用于过滤频繁的时间位置变化，减少硬件通信次数</li>
     * </ul>
     */
    private void timePosMove() {
        // ─────────────────────────────────────────────────────────────────────────
        // 构建FPGA命令
        // ─────────────────────────────────────────────────────────────────────────
        int cmd = FPGAMessage.FPGA_CMD_DIS;                                             // 基础命令：显示控制

        if (Scope.getInstance().isRun()) {                                              // 运行状态
            cmd |= FPGAMessage.FPGA_CMD_SAMP_PLACE;                                     // 添加采样位置命令
        }

        // ─────────────────────────────────────────────────────────────────────────
        // 发送FPGA命令
        // ─────────────────────────────────────────────────────────────────────────
        sendFpgaMsg(cmd);                                                               // 发送FPGA命令
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 时间位置变化处理方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 处理时间位置变化
     *
     * <p><b>处理流程：</b></p>
     * <ol>
     *   <li>启动过滤线程：延时75ms后发送FPGA命令</li>
     *   <li>发送事件通知：立即发送事件，不需要延时</li>
     *   <li>发送UI消息：立即发送UI消息，不需要延时</li>
     * </ol>
     *
     * <p><b>延时过滤机制：</b></p>
     * <ul>
     *   <li>用户快速拖动触发位置时，会产生大量位置变化事件</li>
     *   <li>FilterThread会在75ms内合并这些事件，只发送一次FPGA命令</li>
     *   <li>事件和UI消息立即发送，保证界面响应速度</li>
     * </ul>
     *
     * <p><b>为什么FPGA命令需要延时？</b></p>
     * <ul>
     *   <li>硬件通信开销大，频繁发送会影响性能</li>
     *   <li>用户拖动过程中只需要更新界面，不需要实时更新硬件</li>
     *   <li>拖动结束后才需要将最终位置发送给硬件</li>
     * </ul>
     */
    public void timePosChange() {
        // ─────────────────────────────────────────────────────────────────────────
        // 启动过滤线程（延时75ms后发送FPGA命令）
        // ─────────────────────────────────────────────────────────────────────────
        filterThread.run();                                                             // 启动过滤线程

        // ─────────────────────────────────────────────────────────────────────────
        // 立即发送事件和UI消息（不需要延时）
        // ─────────────────────────────────────────────────────────────────────────
        sendEvent(EventFactory.EVENT_TIME_POS);                                         // 发送时间位置变化事件
        sendUiMsg(UiMessage.UI_MESSAGE_SIMPLE_WIN_DIS);                                 // 发送简单窗口显示消息
    }
}
