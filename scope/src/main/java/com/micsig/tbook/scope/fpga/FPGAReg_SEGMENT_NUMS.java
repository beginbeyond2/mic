package com.micsig.tbook.scope.fpga;   // FPGA寄存器包，包含所有FPGA通信相关的寄存器类

import com.micsig.tbook.scope.Sample.SegmentSample;   // 导入分段采样管理类，用于获取分段采样参数

/**
 * FPGA分段帧数寄存器类 - 控制分段采样的显示帧数量
 * 
 * ┌─────────────────────────────────────────────────────────────────┐
 * │                    FPGA分段帧数寄存器架构                        │
 * ├─────────────────────────────────────────────────────────────────┤
 * │                                                                 │
 * │  ┌─────────────────┐                                           │
 * │  │ SegmentSample   │                                           │
 * │  │  (分段采样管理)  │                                           │
 * │  └────────┬────────┘                                           │
 * │           │                                                     │
 * │           │ isSegmentEnable()                                   │
 * │           │ getSegmentDisplayType()                             │
 * │           │ getFittingBegingFrame()                             │
 * │           │ getFittingEndFrame()                                │
 * │           ▼                                                     │
 * │  ┌─────────────────────────────────────────────┐               │
 * │  │        FPGAReg_SEGMENT_NUMS                 │               │
 * │  │         (分段帧数寄存器)                     │               │
 * │  │                                             │               │
 * │  │  ┌─────────────────────────────────────┐   │               │
 * │  │  │  寄存器数据 (4字节)                  │   │               │
 * │  │  │  ┌───────────────────────────────┐  │   │               │
 * │  │  │  │  位31-0: 帧数量 (32位)        │  │   │               │
 * │  │  │  │  - 单帧模式: 1                │  │   │               │
 * │  │  │  │  - 拟合模式: 帧范围数量        │  │   │               │
 * │  │  │  │  - 禁用状态: 1               │  │   │               │
 * │  │  │  └───────────────────────────────┘  │   │               │
 * │  │  └─────────────────────────────────────┘   │               │
 * │  └─────────────────────────────────────────────┘               │
 * │                       │                                       │
 * │                       ▼                                       │
 * │           ┌─────────────────────┐                             │
 * │           │   FPGA硬件 (0x4B)   │                             │
 * │           │  分段采样帧数控制   │                             │
 * │           └─────────────────────┘                             │
 * │                                                                 │
 * └─────────────────────────────────────────────────────────────────┘
 * 
 * <h3>模块定位</h3>
 * <p>本类是FPGA分段采样系统的核心组件，负责配置分段采样的显示帧数量。
 * 与FPGAReg_SEGMENT_START配合使用，共同定义分段采样的显示范围：
 * - SEGMENT_START定义起始帧号
 * - SEGMENT_NUMS定义帧数量</p>
 * 
 * <h3>核心职责</h3>
 * <ul>
 *   <li><b>帧数量配置</b>：根据分段显示模式设置不同的帧数量</li>
 *   <li><b>模式适配</b>：支持单帧显示模式和拟合显示模式</li>
 *   <li><b>帧范围计算</b>：在拟合模式下自动计算帧范围数量</li>
 * </ul>
 * 
 * <h3>分段显示模式帧数设置</h3>
 * <pre>
 * ┌───────────────────────────────────────────────────────────────┐
 * │                    分段采样帧数设置                            │
 * ├───────────────────────────────────────────────────────────────┤
 * │                                                               │
 * │  1. 单帧显示模式 (SEGMENT_DISPLAY_SFRAME = 0)                 │
 * │     ┌─────┐ ┌─────┐ ┌─────┐ ┌─────┐ ┌─────┐                  │
 * │     │ 帧1 │ │ 帧2 │ │ 帧3 │ │ 帧4 │ │ 帧5 │ ...              │
 * │     └─────┘ └─────┘ └─────┘ └─────┘ └─────┘                  │
 * │                              ▲                                │
 * │                              │                                │
 * │                     当前显示帧 (frameNo)                      │
 * │                                                               │
 * │     FPGA设置:                                                 │
 * │       - SEGMENT_START = frameNo (起始帧号)                   │
 * │       - SEGMENT_NUMS  = 1 (帧数量)                            │
 * │                                                               │
 * │     结果: 只显示当前帧，用户可逐帧浏览                         │
 * │                                                               │
 * │  2. 拟合显示模式 (SEGMENT_DISPLAY_FITTING = 1)                │
 * │     ┌─────┐ ┌─────┐ ┌─────┐ ┌─────┐ ┌─────┐                  │
 * │     │ 帧1 │ │ 帧2 │ │ 帧3 │ │ 帧4 │ │ 帧5 │ ...              │
 * │     └──┬──┘ └──┬──┘ └──┬──┘ └──┬──┘ └──┬──┘                  │
 * │        │     │     │     │     │     │                       │
 * │        │     │     │     │     │     │                       │
 * │        │ fittingBegingFrame  fittingEndFrame │               │
 * │        │     │     │     │     │     │                       │
 * │        └─────┴─────┴─────┴─────┴─────┘                       │
 * │              └─────────────────────┘                         │
 * │                    拟合显示范围                               │
 * │                                                               │
 * │     FPGA设置:                                                 │
 * │       - SEGMENT_START = fittingBegingFrame (起始帧号)        │
 * │       - SEGMENT_NUMS  = fittingEndFrame - fittingBegingFrame │
 * │                         + 1 (帧数量)                          │
 * │                                                               │
 * │     结果: 显示帧范围内的所有帧，叠加显示                       │
 * │                                                               │
 * └───────────────────────────────────────────────────────────────┘
 * </pre>
 * 
 * <h3>寄存器数据格式</h3>
 * <pre>
 * ┌────────────────────────────────────────────────────────────┐
 * │              寄存器数据结构 (4字节)                         │
 * ├────────────────────────────────────────────────────────────┤
 * │  字节偏移  │  位范围  │  字段名    │  说明                 │
 * ├────────────────────────────────────────────────────────────┤
 * │  0-3       │  31-0    │  frameNums │  显示帧数量           │
 * │            │          │            │  - 1: 单帧模式        │
 * │            │          │            │  - n: 拟合模式帧数    │
 * └────────────────────────────────────────────────────────────┘
 * </pre>
 * 
 * <h3>帧数计算逻辑</h3>
 * <pre>
 * ┌─────────────────────────────────────────────────────────────┐
 * │                    帧数计算流程                             │
 * ├─────────────────────────────────────────────────────────────┤
 * │                                                             │
 * │  开始                                                       │
 * │    │                                                        │
 * │    ▼                                                        │
 * │  ┌─────────────────────┐                                   │
 * │  │ 分段采样是否启用？  │                                   │
 * │  │ isSegmentEnable()   │                                   │
 * │  └──────────┬──────────┘                                   │
 * │             │                                               │
 * │     ┌───────┴───────┐                                      │
 * │     │               │                                      │
 * │    否               是                                      │
 * │     │               │                                      │
 * │     ▼               ▼                                      │
 * │  ┌───────┐   ┌─────────────────────┐                      │
 * │  │ 设为1 │   │ 显示模式判断         │                      │
 * │  └───────┘   │ getSegmentDisplayType│                      │
 * │              └──────────┬──────────┘                       │
 * │                         │                                  │
 * │               ┌─────────┴─────────┐                       │
 * │               │                   │                       │
 * │        单帧模式(0)          拟合模式(1)                     │
 * │               │                   │                       │
 * │               ▼                   ▼                       │
 * │          ┌───────┐         ┌─────────────────────┐         │
 * │          │  设为1 │         │ 计算帧范围数量      │         │
 * │          │(单帧) │         │ fittingEndFrame    │         │
 * │          └───────┘         │ - fittingBegingFrame│         │
 * │                            │ + 1                │         │
 * │                            └──────────┬──────────┘         │
 * │                                       │                     │
 * │                                       ▼                     │
 * │                               ┌──────────┐                │
 * │                               │    n     │                │
 * │                               │ (帧数量) │                │
 * │                               └──────────┘                │
 * │                                                             │
 * └─────────────────────────────────────────────────────────────┘
 * </pre>
 * 
 * <h3>与SEGMENT_START的配合关系</h3>
 * <pre>
 * ┌─────────────────────────────────────────────────────────────┐
 * │              SEGMENT_START 与 SEGMENT_NUMS 配合             │
 * ├─────────────────────────────────────────────────────────────┤
 * │                                                             │
 * │  示例: 拟合显示模式，显示帧2到帧5                            │
 * │                                                             │
 * │  ┌─────┐ ┌─────┐ ┌─────┐ ┌─────┐ ┌─────┐ ┌─────┐           │
 * │  │ 帧1 │ │ 帧2 │ │ 帧3 │ │ 帧4 │ │ 帧5 │ │ 帧6 │           │
 * │  └─────┘ └─────┘ └─────┘ └─────┘ └─────┘ └─────┘           │
 * │          └──────────────────────────────┘                  │
 * │                    拟合显示范围                             │
 * │                                                             │
 * │  FPGA寄存器配置:                                            │
 * │    ┌───────────────────────────────────────────────┐       │
 * │    │  FPGA_SEGMENT_START = 2 (起始帧号)            │       │
 * │    │  FPGA_SEGMENT_NUMS  = 4 (帧数量: 5-2+1=4)     │       │
 * │    └───────────────────────────────────────────────┘       │
 * │                                                             │
 * │  结果: FPGA显示帧2、帧3、帧4、帧5的叠加波形                 │
 * │                                                             │
 * └─────────────────────────────────────────────────────────────┘
 * </pre>
 * 
 * <h3>使用场景</h3>
 * <ul>
 *   <li><b>单帧浏览</b>：帧数设为1，每次只显示一个帧</li>
 *   <li><b>拟合分析</b>：帧数设为帧范围数量，叠加显示多个帧</li>
 *   <li><b>信号抖动观察</b>：通过拟合显示观察信号的抖动范围</li>
 * </ul>
 * 
 * <h3>依赖关系</h3>
 * <ul>
 *   <li>{@link FPGAReg} - 父类，提供寄存器基础功能</li>
 *   <li>{@link SegmentSample} - 分段采样管理类，提供帧范围参数</li>
 *   <li>{@link FPGACommand} - FPGA命令管理类，负责调用onCommand</li>
 *   <li>{@link FPGAReg_SEGMENT_START} - 分段起始寄存器，配合使用</li>
 * </ul>
 * 
 * <h3>寄存器地址</h3>
 * <p>寄存器ID: 0x4B (FPGA_SEGMENT_NUMS)</p>
 * 
 * @see FPGAReg
 * @see SegmentSample
 * @see FPGAReg_SEGMENT_START
 * @see FPGAReg_SegmentFrame
 * @author Micsig智能示波器团队
 * @version 2.0
 * @since 1.0
 */
public class FPGAReg_SEGMENT_NUMS extends FPGAReg{   // 继承FPGAReg基类，获得寄存器通用功能
    
    /**
     * 构造函数 - 初始化分段帧数寄存器
     * 
     * <p>创建分段帧数寄存器实例，配置寄存器ID和数据大小。</p>
     * 
     * <h4>初始化参数</h4>
     * <ul>
     *   <li><b>寄存器ID</b>: FPGA_SEGMENT_NUMS (0x4B)</li>
     *   <li><b>数据大小</b>: 4字节（32位帧数量）</li>
     * </ul>
     * 
     * <h4>寄存器地址说明</h4>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器地址映射                         │
     * ├─────────────────────────────────────────┤
     * │  0x4B = FPGA_SEGMENT_NUMS               │
     * │  功能: 设置分段采样的显示帧数量         │
     * │  大小: 4字节                            │
     * │  访问: 只写 (由FPGA读取)                │
     * │                                         │
     * │  配合寄存器:                            │
     * │  0x4C = FPGA_SEGMENT_START (起始帧号)   │
     * └─────────────────────────────────────────┘
     * </pre>
     * 
     * <h4>调用时机</h4>
     * <p>在FPGACommand初始化时创建实例，存储在fpgaRegs数组中。
     * 当需要更新分段帧数量时，调用onCommand方法自动计算并设置值。</p>
     * 
     * @see FPGAReg#FPGAReg(int, int)
     * @see FPGACommand#initFpgaRegs()
     */
    public FPGAReg_SEGMENT_NUMS() {   // 构造函数：初始化分段帧数寄存器
        super(FPGAReg.FPGA_SEGMENT_NUMS, 4);   // 调用父类构造函数，设置寄存器ID为0x4B，数据大小为4字节
    }   // 构造函数结束

    /**
     * 命令回调方法 - 自动计算并设置分段显示帧数量
     * 
     * <p>此方法在每次需要更新FPGA分段帧数量时被调用，
     * 根据分段采样状态和显示模式自动计算合适的帧数量。</p>
     * 
     * <h4>计算逻辑详解</h4>
     * <pre>
     * ┌───────────────────────────────────────────────────────────────┐
     * │                    onCommand执行流程                          │
     * ├───────────────────────────────────────────────────────────────┤
     * │                                                               │
     * │  Step 1: 获取分段采样实例                                     │
     * │    └─ SegmentSample.getInstance()                             │
     * │                                                               │
     * │  Step 2: 检查分段采样是否启用                                  │
     * │    └─ segmentSample.isSegmentEnable()                         │
     * │                                                               │
     * │  Step 3: 根据显示模式计算帧数量                                  │
     * │    ├─ 单帧模式 (SEGMENT_DISPLAY_SFRAME)                       │
     * │    │   └─ 设为1 (只显示一个帧)                                │
     * │    │                                                           │
     * │    └─ 拟合模式 (SEGMENT_DISPLAY_FITTING)                       │
     * │        └─ 计算帧范围数量: fittingEndFrame -                   │
     * │          fittingBegingFrame + 1                               │
     * │                                                               │
     * │  Step 4: 设置寄存器值                                          │
     * │    └─ setVal(0, frameNums)                                    │
     * │                                                               │
     * └───────────────────────────────────────────────────────────────┘
     * </pre>
     * 
     * <h4>帧数量计算公式</h4>
     * <table border="1">
     *   <tr><th>显示模式</th><th>帧数量公式</th><th>说明</th></tr>
     *   <tr><td>分段禁用</td><td>1</td><td>默认显示1帧</td></tr>
     *   <tr><td>单帧模式</td><td>1</td><td>只显示当前帧</td></tr>
     *   <tr><td>拟合模式</td><td>fittingEndFrame - fittingBegingFrame + 1</td><td>显示帧范围内的所有帧</td></tr>
     * </table>
     * 
     * <h4>拟合模式帧数计算示例</h4>
     * <pre>
     * ┌─────────────────────────────────────────────────────────────┐
     * │              拟合模式帧数计算示例                           │
     * ├─────────────────────────────────────────────────────────────┤
     * │                                                             │
     * │  假设:                                                      │
     * │    fittingBegingFrame = 10 (拟合起始帧)                     │
     * │    fittingEndFrame    = 25 (拟合结束帧)                     │
     * │                                                             │
     * │  计算:                                                      │
     * │    帧数量 n = fittingEndFrame - fittingBegingFrame + 1      │
     * │             = 25 - 10 + 1                                   │
     * │             = 16                                            │
     * │                                                             │
     * │  结果:                                                      │
     * │    FPGA将显示帧10到帧25，共16帧的叠加波形                   │
     * │                                                             │
     * └─────────────────────────────────────────────────────────────┘
     * </pre>
     * 
     * <h4>调用时机</h4>
     * <ul>
     *   <li>分段采样启用/禁用状态改变时</li>
     *   <li>分段显示模式切换时（单帧/拟合）</li>
     *   <li>拟合帧范围改变时（fittingBegingFrame或fittingEndFrame变化）</li>
     *   <li>与FPGAReg_SEGMENT_START同步更新时</li>
     * </ul>
     * 
     * @see SegmentSample#isSegmentEnable()
     * @see SegmentSample#getSegmentDisplayType()
     * @see SegmentSample#getFittingBegingFrame()
     * @see SegmentSample#getFittingEndFrame()
     * @see FPGAReg#setVal(int, int)
     */
    @Override   // 重写父类FPGAReg的onCommand方法
    public void onCommand() {   // 命令回调方法：自动计算并设置分段显示帧数量
        SegmentSample segmentSample = SegmentSample.getInstance();   // 获取分段采样管理实例（单例模式）
        
        if(segmentSample.isSegmentEnable()) {   // 判断：分段采样是否启用
            // 分段采样已启用，根据显示模式设置帧数量
            if (segmentSample.getSegmentDisplayType() == SegmentSample.SEGMENT_DISPLAY_SFRAME) {   // 判断：是否为单帧显示模式
                // 单帧显示模式：每次只显示一个帧
                setVal(0, 1);   // 设置寄存器值：字节偏移0，值为1（只显示一个帧）
                
            } else {   // 单帧模式判断结束，进入拟合显示模式
                // 拟合显示模式：叠加显示多个帧
                int n = segmentSample.getFittingEndFrame() - segmentSample.getFittingBegingFrame() + 1;   // 计算帧数量：结束帧号 - 起始帧号 + 1
                setVal(0, n);   // 设置寄存器值：字节偏移0，值为计算得到的帧数量n
            }   // 显示模式判断结束
            
        }else{   // 分段采样启用判断结束，分段采样未启用
            // 分段采样未启用：设置为1（默认显示）
            setVal(0,1);   // 设置寄存器值为1，表示默认显示帧数量
        }   // 分段采样启用判断结束
    }   // onCommand方法结束
}   // FPGAReg_SEGMENT_NUMS类结束