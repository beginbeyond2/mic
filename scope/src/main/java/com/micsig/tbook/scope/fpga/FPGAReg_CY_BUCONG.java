package com.micsig.tbook.scope.fpga;  // 定义包名：示波器FPGA寄存器管理模块

/**
 * FPGA采样补充寄存器类 - 用于配置采样补充参数
 *
 * <p><b>模块定位：</b>
 * <ul>
 *   <li>所属模块：scope.fpga（示波器FPGA寄存器管理模块）</li>
 *   <li>架构层级：硬件抽象层 - FPGA寄存器配置</li>
 *   <li>设计模式：继承模式（继承FPGAReg基类）</li>
 *   <li>职责类型：采样补充配置管理器</li>
 * </ul>
 *
 * <p><b>核心职责：</b>
 * <ul>
 *   <li>配置采样补充类型（通道1-2）</li>
 *   <li>配置采样补充类型（通道3-4）</li>
 *   <li>配置AD休眠模式</li>
 *   <li>配置最大AD时钟</li>
 * </ul>
 *
 * <p><b>设计目的：</b>
 * <ul>
 *   <li>提供采样补充参数的配置接口</li>
 *   <li>支持多通道采样补充配置</li>
 *   <li>控制AD转换器的休眠和时钟</li>
 * </ul>
 *
 * <p><b>寄存器结构：</b>
 * <pre>
 * FPGA_CY_BUCONG寄存器（32位，4字节）
 * │
 * ├── 位0-3：通道1-2采样补充类型（CyType12）
 * │     └── 控制通道1和通道2的采样补充方式
 * │
 * ├── 位4：通道1-2 AD休眠（AdSleep12）
 * │     └── 控制通道1和通道2的AD休眠模式
 * │
 * ├── 位5：最大AD时钟（MaxAdClk）
 * │     └── 控制AD转换器的最大时钟频率
 * │
 * ├── 位8-11：通道3-4采样补充类型（CyType34）
 * │     └── 控制通道3和通道4的采样补充方式
 * │
 * └── 位12：通道3-4 AD休眠（AdSleep34）
 *       └── 控制通道3和通道4的AD休眠模式
 * </pre>
 *
 * <p><b>采样补充说明：</b>
 * <ul>
 *   <li>采样补充用于优化采样效率</li>
 *   <li>补充类型决定采样补充的方式</li>
 *   <li>AD休眠用于降低功耗</li>
 *   <li>最大AD时钟控制采样速率</li>
 * </ul>
 *
 * <p><b>使用场景：</b>
 * <ul>
 *   <li>采样优化：配置采样补充参数</li>
 *   <li>功耗控制：配置AD休眠模式</li>
 *   <li>采样速率控制：配置最大AD时钟</li>
 * </ul>
 *
 * <p><b>依赖关系：</b>
 * <ul>
 *   <li>继承：FPGAReg（FPGA寄存器基类）</li>
 *   <li>依赖：FPGACommand（FPGA命令处理类）</li>
 * </ul>
 *
 * <p><b>线程安全：</b>
 * <ul>
 *   <li>寄存器操作在FPGACommand中同步执行</li>
 *   <li>非线程安全，需在单线程环境中使用</li>
 * </ul>
 *
 * @author zhuzh
 * @version 1.0
 * @since 2018-4-8
 * @see FPGAReg FPGA寄存器基类
 */
public class FPGAReg_CY_BUCONG extends FPGAReg {  // 继承FPGAReg基类，复用寄存器操作方法

    /**
     * 构造函数 - 初始化采样补充寄存器
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>调用父类构造函数，设置寄存器地址和大小</li>
     *   <li>寄存器地址：FPGA_CY_BUCONG</li>
     *   <li>寄存器大小：4字节（32位）</li>
     * </ul>
     */
    public FPGAReg_CY_BUCONG() {  // 构造函数
        // 调用父类构造函数，传入寄存器地址和大小
        // FPGA_CY_BUCONG：采样补充寄存器地址
        // 4：寄存器大小为4字节（32位）
        super(FPGA_CY_BUCONG, 4);  // 初始化采样补充寄存器，地址为FPGA_CY_BUCONG，大小为4字节
    }

    /**
     * 设置通道1-2采样补充类型 - 配置通道1和通道2的采样补充方式
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>配置通道1和通道2的采样补充类型</li>
     *   <li>寄存器位：位0-3（4位）</li>
     *   <li>决定采样补充的方式</li>
     * </ul>
     *
     * <p><b>补充类型说明：</b>
     * <ul>
     *   <li>补充类型决定采样补充的具体方式</li>
     *   <li>不同的补充类型适用于不同的采样场景</li>
     *   <li>值范围：0-15（4位）</li>
     * </ul>
     *
     * @param val 采样补充类型值（0-15）
     */
    public void setCyType12(int val) {  // 设置通道1-2采样补充类型方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=0（位0开始）
        // 参数2：位宽度=4（4位）
        // 参数3：补充类型值
        setVal(0, 4, val);  // 设置位0-3的值，宽度为4位，值为val（通道1-2补充类型）
    }

    /**
     * 获取通道1-2采样补充类型 - 读取通道1和通道2的采样补充方式
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>读取通道1和通道2的采样补充类型</li>
     *   <li>返回寄存器位0-3的值</li>
     *   <li>用于检查当前配置</li>
     * </ul>
     *
     * @return 采样补充类型值（0-15）
     */
    public int getCyType12() {  // 获取通道1-2采样补充类型方法
        // 调用父类getVal方法，获取寄存器值
        // getVal()：获取整个寄存器的值
        // & 0x0F：取低4位（位0-3）
        return getVal() & 0x0F;  // 返回寄存器值的低4位（通道1-2补充类型）
    }

    /**
     * 设置通道1-2 AD休眠 - 配置通道1和通道2的AD休眠模式
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>配置通道1和通道2的AD休眠模式</li>
     *   <li>寄存器位：位4（1位）</li>
     *   <li>用于降低功耗</li>
     * </ul>
     *
     * <p><b>AD休眠说明：</b>
     * <ul>
     *   <li>0：AD正常工作</li>
     *   <li>1：AD进入休眠模式</li>
     *   <li>休眠模式可降低功耗</li>
     * </ul>
     *
     * @param val AD休眠值（0=正常，1=休眠）
     */
    public void setAdSleep12(int val) {  // 设置通道1-2 AD休眠方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=4（位4）
        // 参数2：位宽度=1（1位）
        // 参数3：休眠值
        setVal(4, 1, val);  // 设置位4的值，宽度为1位，值为val（通道1-2 AD休眠）
    }

    /**
     * 设置最大AD时钟 - 配置AD转换器的最大时钟频率
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>配置AD转换器的最大时钟频率</li>
     *   <li>寄存器位：位5（1位）</li>
     *   <li>控制采样速率</li>
     * </ul>
     *
     * <p><b>最大AD时钟说明：</b>
     * <ul>
     *   <li>0：正常时钟频率</li>
     *   <li>1：最大时钟频率</li>
     *   <li>影响采样速率和功耗</li>
     * </ul>
     *
     * @param val 最大AD时钟值（0=正常，1=最大）
     */
    public void setMaxAdClk(int val) {  // 设置最大AD时钟方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=5（位5）
        // 参数2：位宽度=1（1位）
        // 参数3：最大时钟值
        setVal(5, 1, val);  // 设置位5的值，宽度为1位，值为val（最大AD时钟）
    }

    /**
     * 设置通道3-4采样补充类型 - 配置通道3和通道4的采样补充方式
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>配置通道3和通道4的采样补充类型</li>
     *   <li>寄存器位：位8-11（4位）</li>
     *   <li>决定采样补充的方式</li>
     * </ul>
     *
     * <p><b>补充类型说明：</b>
     * <ul>
     *   <li>补充类型决定采样补充的具体方式</li>
     *   <li>不同的补充类型适用于不同的采样场景</li>
     *   <li>值范围：0-15（4位）</li>
     * </ul>
     *
     * @param val 采样补充类型值（0-15）
     */
    public void setCyType34(int val) {  // 设置通道3-4采样补充类型方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=8（位8开始）
        // 参数2：位宽度=4（4位）
        // 参数3：补充类型值
        setVal(8, 4, val);  // 设置位8-11的值，宽度为4位，值为val（通道3-4补充类型）
    }

    /**
     * 设置通道3-4 AD休眠 - 配置通道3和通道4的AD休眠模式
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>配置通道3和通道4的AD休眠模式</li>
     *   <li>寄存器位：位12（1位）</li>
     *   <li>用于降低功耗</li>
     * </ul>
     *
     * <p><b>AD休眠说明：</b>
     * <ul>
     *   <li>0：AD正常工作</li>
     *   <li>1：AD进入休眠模式</li>
     *   <li>休眠模式可降低功耗</li>
     * </ul>
     *
     * @param val AD休眠值（0=正常，1=休眠）
     */
    public void setAdSleep34(int val) {  // 设置通道3-4 AD休眠方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=12（位12）
        // 参数2：位宽度=1（1位）
        // 参数3：休眠值
        setVal(12, 1, val);  // 设置位12的值，宽度为1位，值为val（通道3-4 AD休眠）
    }

}