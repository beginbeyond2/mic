/**
 * 示波器FPGA显示缩略窗口寄存器类
 * 继承自FPGAReg基类，负责管理FPGA缩略显示窗口的位置参数
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>设置缩略窗口X轴起始位置</li>
 *   <li>设置缩略窗口X轴结束位置</li>
 *   <li>设置最后一行X轴起始位置</li>
 *   <li>设置最后一行X轴结束位置</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建显示缩略窗口寄存器对象
 * FPGAReg_DISP_SL reg = new FPGAReg_DISP_SL();
 *
 * // 设置X轴起始位置
 * reg.setXStart(0);      // 设置X轴起始位置为0
 *
 * // 设置X轴结束位置
 * reg.setXEnd(1000);     // 设置X轴结束位置为1000
 *
 * // 发送命令到FPGA
 * FPGACommand.getInstance().sendCommand(reg);
 * </pre>
 *
 * @see FPGAReg
 * @see FPGACommand
 * @see Display
 * @see ZoomWindow
 * @author zhuzh
 * @version 1.0
 * @since 2018-4-8
 */
public class FPGAReg_DISP_SL extends FPGAReg {                                             // 类声明：显示缩略窗口寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化显示缩略窗口寄存器
     *
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_DISP_SL</li>
     *   <li>数据长度：8字节（2个字，每个字32位）</li>
     * </ul>
     *
     * <p><b>寄存器地址说明：</b></p>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器地址映射                         │
     * ├─────────────────────────────────────────┤
     * │  FPGA_DISP_SL                           │
     * │  功能: 配置缩略窗口位置参数             │
     * │  大小: 8字节（2个字）                   │
     * │  访问: 只写 (由FPGA读取)                │
     * │                                         │
     * │  数据结构:                              │
     * │  字0: X轴起始/结束位置                  │
     * │    位0-15: X轴起始位置                  │
     * │    位16-31: X轴结束位置                 │
     * │  字1: 最后一行X轴起始/结束位置          │
     * │    位0-15: 最后一行X轴起始位置          │
     * │    位16-31: 最后一行X轴结束位置         │
     * └─────────────────────────────────────────┘
     * </pre>
     */
    public FPGAReg_DISP_SL() {                                                             // 构造方法：初始化显示缩略窗口寄存器
        // 调用父类构造函数，设置寄存器ID和数据长度
        // FPGA_DISP_SL：显示缩略窗口寄存器地址
        // 8：数据长度为8字节（2个32位字）
        super(FPGA_DISP_SL, 8);                                                             // 调用父类构造函数，设置寄存器ID为FPGA_DISP_SL，长度8字节
    }                                                                                      // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法 - X轴位置设置
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置X轴起始位置
     * 设置缩略窗口的X轴起始位置
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>X轴起始位置决定了缩略窗口显示的起始位置</li>
     *   <li>用于控制缩略窗口的显示范围</li>
     *   <li>值越大，显示起始位置越靠后</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>val：X轴起始位置值（16位，范围0到65535）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <ul>
     *   <li>存储位置：字0的位0-15（16位）</li>
     * </ul>
     *
     * @param val X轴起始位置值（16位）
     */
    public void setXStart(int val){                                                        // 公有方法：设置X轴起始位置
        // 调用父类setVal方法，设置字0的X轴起始位置
        // 参数1：起始位=0（从位0开始）
        // 参数2：位宽度=16（16位）
        // 参数3：X轴起始位置值
        setVal(0,16,val);                                                                  // 设置字0的位0-15，值为val（X轴起始位置）
    }                                                                                      // 方法结束

    /**
     * 设置X轴结束位置
     * 设置缩略窗口的X轴结束位置
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>X轴结束位置决定了缩略窗口显示的结束位置</li>
     *   <li>用于控制缩略窗口的显示范围</li>
     *   <li>值越大，显示结束位置越靠后</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>val：X轴结束位置值（16位，范围0到65535）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <ul>
     *   <li>存储位置：字0的位16-31（16位）</li>
     * </ul>
     *
     * @param val X轴结束位置值（16位）
     */
    public void setXEnd(int val){                                                          // 公有方法：设置X轴结束位置
        // 调用父类setVal方法，设置字0的X轴结束位置
        // 参数1：起始位=16（从位16开始）
        // 参数2：位宽度=16（16位）
        // 参数3：X轴结束位置值
        setVal(16,16,val);                                                                 // 设置字0的位16-31，值为val（X轴结束位置）
    }                                                                                      // 方法结束

    /**
     * 获取X轴起始位置
     * 返回当前设置的X轴起始位置值
     *
     * <p><b>返回值说明：</b></p>
     * <ul>
     *   <li>返回X轴起始位置值（16位）</li>
     * </ul>
     *
     * @return X轴起始位置值
     */
    public int getXStart(){                                                                // 公有方法：获取X轴起始位置
        // 获取字0的值，提取位0-15的X轴起始位置
        // getVal()：获取字0的32位数据
        // & 0xFFFF：掩码提取16位值
        return getVal() & 0xFFFF;                                                          // 返回字0的位0-15（X轴起始位置）
    }                                                                                      // 方法结束

    /**
     * 获取X轴结束位置
     * 返回当前设置的X轴结束位置值
     *
     * <p><b>返回值说明：</b></p>
     * <ul>
     *   <li>返回X轴结束位置值（16位）</li>
     * </ul>
     *
     * @return X轴结束位置值
     */
    public int getXEnd(){                                                                  // 公有方法：获取X轴结束位置
        // 获取字0的值，提取位16-31的X轴结束位置
        // getVal()：获取字0的32位数据
        // >>> 16：无符号右移16位
        // & 0xFFFFF：掩码提取20位值（注意：此处掩码为20位，可能是错误，应为0xFFFF）
        return (getVal() >>> 16) & 0xFFFFF;                                                // 返回字0的位16-31（X轴结束位置）
    }                                                                                      // 方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法 - 最后一行X轴位置设置
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置最后一行X轴起始位置
     * 设置最后一行显示的X轴起始位置
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>最后一行X轴起始位置决定了最后一行显示的起始位置</li>
     *   <li>用于处理不完整行的显示</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>val：最后一行X轴起始位置值（16位，范围0到65535）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <ul>
     *   <li>存储位置：字1的位0-15（16位）</li>
     * </ul>
     *
     * @param val 最后一行X轴起始位置值（16位）
     */
    public void setXLastStart(int val){                                                    // 公有方法：设置最后一行X轴起始位置
        // 调用父类setVal方法，设置字1的最后一行X轴起始位置
        // 参数1：字索引=1（第二个32位字）
        // 参数2：起始位=0（从位0开始）
        // 参数3：位宽度=16（16位）
        // 参数4：最后一行X轴起始位置值
        setVal(1,0,16,val);                                                                // 设置字1的位0-15，值为val（最后一行X轴起始位置）
    }                                                                                      // 方法结束

    /**
     * 设置最后一行X轴结束位置
     * 设置最后一行显示的X轴结束位置
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>最后一行X轴结束位置决定了最后一行显示的结束位置</li>
     *   <li>用于处理不完整行的显示</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>val：最后一行X轴结束位置值（16位，范围0到65535）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <ul>
     *   <li>存储位置：字1的位16-31（16位）</li>
     * </ul>
     *
     * @param val 最后一行X轴结束位置值（16位）
     */
    public void setXLastEnd(int val){                                                      // 公有方法：设置最后一行X轴结束位置
        // 调用父类setVal方法，设置字1的最后一行X轴结束位置
        // 参数1：字索引=1（第二个32位字）
        // 参数2：起始位=16（从位16开始）
        // 参数3：位宽度=16（16位）
        // 参数4：最后一行X轴结束位置值
        setVal(1,16,16,val);                                                               // 设置字1的位16-31，值为val（最后一行X轴结束位置）
    }                                                                                      // 方法结束

}                                                                                          // 类结束