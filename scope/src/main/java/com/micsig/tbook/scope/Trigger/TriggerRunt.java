package com.micsig.tbook.scope.Trigger;

/**
 * ╔══════════════════════════════════════════════════════════════════════════════╗
 * ║                   TriggerRunt - 矮脉冲触发器类                               ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【模块定位】                                                                 ║
 * ║   Trigger模块的矮脉冲触发器实现类，用于检测幅度低于阈值的矮脉冲信号。         ║
 * ║   矮脉冲触发是一种高级触发方式，适用于检测不完整的脉冲信号。                  ║
 * ║                                                                              ║
 * ║ 【核心职责】                                                                 ║
 * ║   1. 管理矮脉冲极性（正脉冲/负脉冲/任意脉冲）                                ║
 * ║   2. 管理矮脉冲条件（小于/大于/之间/无）                                     ║
 * ║   3. 管理高低电平时间参数                                                    ║
 * ║   4. 支持双触发电平（上限电平和下限电平）                                    ║
 * ║   5. 提供单触发源访问接口                                                    ║
 * ║                                                                              ║
 * ║ 【继承体系】                                                                 ║
 * ║                          ┌─────────────────┐                                 ║
 * ║                          │   Trigger       │ ← 抽象基类                      ║
 * ║                          │   (abstract)    │                                 ║
 * ║                          └────────┬────────┘                                 ║
 * ║                                   │                                          ║
 * ║         ┌───────────┬─────────────┼─────────────┬───────────┐               ║
 * ║         │           │             │             │           │               ║
 * ║         ▼           ▼             ▼             ▼           ▼               ║
 * ║   ┌──────────┐ ┌──────────┐ ┌──────────┐ ┌──────────┐ ┌──────────┐         ║
 * ║   │Edge      │ │PulseWidth│ │Logic     │ │Runt      │ │TimeOut   │         ║
 * ║   │边沿触发  │ │脉宽触发  │ │逻辑触发  │ │矮脉冲    │ │超时触发  │         ║
 * ║   │(单源)    │ │(单源)    │ │(多源)    │ │(单源)    │ │(单源)    │         ║
 * ║   └──────────┘ └──────────┘ └──────────┘ └──────────┘ └──────────┘         ║
 * ║                                              ↑                              ║
 * ║                                            本类                             ║
 * ║                                                                              ║
 * ║ 【矮脉冲触发说明】                                                           ║
 * ║   矮脉冲触发用于检测幅度低于阈值的矮脉冲信号。                               ║
 * ║   与普通脉宽触发不同，矮脉冲触发需要两个触发电平：上限电平和下限电平。       ║
 * ║   只有当脉冲穿过其中一个电平但不穿过另一个电平时，才触发采集。               ║
 * ║                                                                              ║
 * ║   ┌─────────────────────────────────────────────────────────────────────┐   ║
 * ║   │                     矮脉冲触发示意图                                 │   ║
 * ║   │                                                                     │   ║
 * ║   │  正脉冲矮脉冲：                                                     │   ║
 * ║   │      上限电平 ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─                    │   ║
 * ║   │                  ┌──────┐                                          │   ║
 * ║   │      下限电平 ─ ─│──────│─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─                   │   ║
 * ║   │                  │      │                                          │   ║
 * ║   │ ─────────────────┘      └────────────────                          │   ║
 * ║   │                  ←矮脉冲→                                           │   ║
 * ║   │                                                                     │   ║
 * ║   │  脉冲穿过下限电平，但未穿过上限电平 → 矮脉冲 → 触发采集              │   ║
 * ║   │                                                                     │   ║
 * ║   │  负脉冲矮脉冲：                                                     │   ║
 * ║   │                  ┌──────┐                                          │   ║
 * ║   │      上限电平 ─ ─│──────│─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─                   │   ║
 * ║   │      下限电平 ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─                    │   ║
 * ║   │                  │      │                                          │   ║
 * ║   │ ─────────────────┘      └────────────────                          │   ║
 * ║   │                  ←矮脉冲→                                           │   ║
 * ║   │                                                                     │   ║
 * ║   │  脉冲穿过上限电平，但未穿过下限电平 → 矮脉冲 → 触发采集              │   ║
 * ║   └─────────────────────────────────────────────────────────────────────┘   ║
 * ║                                                                              ║
 * ║ 【矮脉冲极性说明】                                                           ║
 * ║   ┌────────────────┬────────────────────────────────────────────────────┐  ║
 * ║   │ 极性           │ 说明                                                │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 正脉冲(POS)    │ 检测穿过下限电平但未穿过上限电平的正脉冲            │  ║
 * ║   │                │ 适用于检测正向矮脉冲                                │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 负脉冲(NEG)    │ 检测穿过上限电平但未穿过下限电平的负脉冲            │  ║
 * ║   │                │ 适用于检测负向矮脉冲                                │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 任意脉冲(EITHER│ 检测正向和负向矮脉冲                                │  ║
 * ║   │                │ 适用于检测任意极性的矮脉冲                          │  ║
 * ║   └────────────────┴────────────────────────────────────────────────────┘  ║
 * ║                                                                              ║
 * ║ 【矮脉冲条件说明】                                                           ║
 * ║   ┌────────────────┬────────────────────────────────────────────────────┐  ║
 * ║   │ 条件           │ 说明                                                │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 小于(LESSER)   │ 矮脉冲宽度小于设定值时触发                          │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 大于(GREATER)  │ 矮脉冲宽度大于设定值时触发                          │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 之间(BETWEEN)  │ 矮脉冲宽度在设定范围内时触发                        │  ║
 * ║   ├────────────────┼────────────────────────────────────────────────────┤  ║
 * ║   │ 无(NONE)       │ 矮脉冲宽度不在设定范围内时触发                      │  ║
 * ║   └────────────────┴────────────────────────────────────────────────────┘  ║
 * ║                                                                              ║
 * ║ 【双触发电平说明】                                                           ║
 * ║   矮脉冲触发需要两个触发电平：                                               ║
 * ║   - 上限电平（TRIGGER_LEVEL_HIGH）：脉冲不应穿过的电平                     ║
 * ║   - 下限电平（TRIGGER_LEVEL_NORMAL）：脉冲应该穿过的电平                   ║
 * ║   只有当脉冲穿过其中一个电平但不穿过另一个电平时，才被认为是矮脉冲。         ║
 * ║                                                                              ║
 * ║ 【依赖关系】                                                                 ║
 * ║   → Trigger: 继承触发器基类                                                 ║
 * ║   → TriggerFactory: 工厂类，管理触发器实例                                  ║
 * ║   → TriggerLevel: 触发电平管理（需要两个触发电平）                          ║
 * ║                                                                              ║
 * ║ 【使用场景】                                                                 ║
 * ║   1. 用户选择矮脉冲触发类型时，TriggerFactory创建TriggerRunt实例           ║
 * ║   2. 用户设置矮脉冲极性（正脉冲/负脉冲/任意脉冲）时                          ║
 * ║   3. 用户设置矮脉冲条件时                                                   ║
 * ║   4. 用户设置高低电平时间参数时                                             ║
 * ║   5. 用户调整两个触发电平时                                                 ║
 * ║                                                                              ║
 * ║ 【设计模式】                                                                 ║
 * ║   - 模板方法模式（Template Method）：继承Trigger的抽象方法                 ║
 * ║                                                                              ║
 * ║ 【线程安全】                                                                 ║
 * ║   触发参数未加同步保护，建议在UI线程操作。                                   ║
 * ║                                                                              ║
 * ║ 【作者】 Created by zhuzh on 2018-6-1                                        ║
 * ║        矮脉冲触发                                                            ║
 * ╚══════════════════════════════════════════════════════════════════════════════╝
 */
public class TriggerRunt extends Trigger {

    // ═══════════════════════════════════════════════════════════════════════════════
    // 矮脉冲极性常量定义
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 正脉冲矮脉冲极性
     * 检测穿过下限电平但未穿过上限电平的正脉冲
     * 适用于检测正向矮脉冲
     *
     * <p><b>波形示例：</b>
     * <pre>
     *      上限电平 ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─
     *                  ┌──────┐
     *      下限电平 ─ ─│──────│─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─
     *                  │      │
     * ─────────────────┘      └────────────────
     *                  ←矮脉冲→
     * </pre>
     */
    public static final int RUNT_POLARITY_POSITIVE = 0;  // 正脉冲矮脉冲极性常量，值为0

    /**
     * 负脉冲矮脉冲极性
     * 检测穿过上限电平但未穿过下限电平的负脉冲
     * 适用于检测负向矮脉冲
     *
     * <p><b>波形示例：</b>
     * <pre>
     *                  ┌──────┐
     *      上限电平 ─ ─│──────│─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─
     *      下限电平 ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─ ─
     *                  │      │
     * ─────────────────┘      └────────────────
     *                  ←矮脉冲→
     * </pre>
     */
    public static final int RUNT_POLARITY_NEGATIVE = 1;  // 负脉冲矮脉冲极性常量，值为1

    /**
     * 任意脉冲矮脉冲极性
     * 检测正向和负向矮脉冲
     * 适用于检测任意极性的矮脉冲
     */
    public static final int RUNT_POLARITY_EITHER = 2;    // 任意脉冲矮脉冲极性常量，值为2

    // ═══════════════════════════════════════════════════════════════════════════════
    // 矮脉冲条件常量定义
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 矮脉冲条件：小于
     * 矮脉冲宽度小于设定值时触发
     */
    public static final int RUNT_RELATION_LESSER = TRIGGER_RELATION_LESS_THAN;  // 小于条件，继承自Trigger基类

    /**
     * 矮脉冲条件：大于
     * 矮脉冲宽度大于设定值时触发
     */
    public static final int RUNT_RELATION_GREATER = TRIGGER_RELATION_MORE_THAN;  // 大于条件，继承自Trigger基类

    /**
     * 矮脉冲条件：之间
     * 矮脉冲宽度在设定范围内时触发
     */
    public static final int RUNT_RELATION_BETWEEN = TRIGGER_RELATION_EQUAL;      // 之间条件，继承自Trigger基类

    /**
     * 矮脉冲条件：无
     * 矮脉冲宽度不在设定范围内时触发
     */
    public static final int RUNT_RELATION_NONE = TRIGGER_RELATION_NOT_EQUAL;     // 无条件，继承自Trigger基类

    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 当前矮脉冲极性
     * 取值：RUNT_POLARITY_POSITIVE、RUNT_POLARITY_NEGATIVE 或 RUNT_POLARITY_EITHER
     * 默认值：RUNT_POLARITY_POSITIVE（正脉冲）
     */
    private int polarity = RUNT_POLARITY_POSITIVE;  // 矮脉冲极性，默认为正脉冲

    /**
     * 当前矮脉冲条件
     * 取值：RUNT_RELATION_LESSER、RUNT_RELATION_GREATER、RUNT_RELATION_BETWEEN 或 RUNT_RELATION_NONE
     * 默认值：RUNT_RELATION_LESSER（小于）
     */
    private int condition = RUNT_RELATION_LESSER;   // 矮脉冲条件，默认为小于

    /**
     * 高电平时间（FPGA单位）
     * 单位：纳秒
     * 默认值：8ns
     *
     * 用于设置高电平持续时间的阈值
     */
    private long timeHigh = 8;                      // 高电平时间，默认8纳秒

    /**
     * 低电平时间（FPGA单位）
     * 单位：纳秒
     * 默认值：8ns
     *
     * 用于设置低电平持续时间的阈值
     */
    private long timeLow = 8;                       // 低电平时间，默认8纳秒

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造矮脉冲触发器实例
     * 调用父类Trigger构造方法设置触发类型为矮脉冲触发
     *
     * <p><b>初始化流程：</b>
     * <ol>
     *   <li>调用父类Trigger构造方法，设置触发类型为TRIG_TYPE_LOW_PULSE</li>
     * </ol>
     *
     * @example
     *   TriggerRunt trigger = new TriggerRunt();
     */
    public TriggerRunt() {                          // 构造方法
        super(Trigger.TRIG_TYPE_LOW_PULSE);         // 调用父类构造方法，设置触发类型为矮脉冲触发
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 矮脉冲极性访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取当前矮脉冲极性
     *
     * @return 矮脉冲极性
     *         RUNT_POLARITY_POSITIVE (0): 正脉冲
     *         RUNT_POLARITY_NEGATIVE (1): 负脉冲
     *         RUNT_POLARITY_EITHER (2): 任意脉冲
     */
    public int getPolarity() {                      // 获取矮脉冲极性方法
        return polarity;                            // 返回当前矮脉冲极性
    }

    /**
     * 设置矮脉冲极性
     * 更新矮脉冲极性并发送FPGA消息
     *
     * @param polarity 矮脉冲极性
     *                 RUNT_POLARITY_POSITIVE: 正脉冲
     *                 RUNT_POLARITY_NEGATIVE: 负脉冲
     *                 RUNT_POLARITY_EITHER: 任意脉冲
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setPolarity(int polarity) {         // 设置矮脉冲极性方法
        this.polarity = polarity;                   // 更新矮脉冲极性成员变量
        triggerParamChange();                       // 调用父类方法发送FPGA消息和事件通知
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 高电平时间访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取高电平时间
     *
     * @return 高电平时间（纳秒）
     */
    public long getTimeHigh() {                     // 获取高电平时间方法
        return timeHigh;                            // 返回当前高电平时间
    }

    /**
     * 设置高电平时间
     * 更新高电平时间并发送FPGA消息
     *
     * @param timeHigh 高电平时间（纳秒）
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setTimeHigh(long timeHigh) {        // 设置高电平时间方法
        this.timeHigh = timeHigh;                   // 更新高电平时间成员变量
        triggerParamChange();                       // 调用父类方法发送FPGA消息和事件通知
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 矮脉冲条件访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取当前矮脉冲条件
     *
     * @return 矮脉冲条件
     *         RUNT_RELATION_LESSER: 小于
     *         RUNT_RELATION_GREATER: 大于
     *         RUNT_RELATION_BETWEEN: 之间
     *         RUNT_RELATION_NONE: 无
     */
    public int getCondition() {                     // 获取矮脉冲条件方法
        return condition;                           // 返回当前矮脉冲条件
    }

    /**
     * 设置矮脉冲条件
     * 更新矮脉冲条件并发送FPGA消息
     *
     * @param condition 矮脉冲条件
     *                  RUNT_RELATION_LESSER: 小于
     *                  RUNT_RELATION_GREATER: 大于
     *                  RUNT_RELATION_BETWEEN: 之间
     *                  RUNT_RELATION_NONE: 无
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setCondition(int condition) {       // 设置矮脉冲条件方法
        this.condition = condition;                 // 更新矮脉冲条件成员变量
        triggerParamChange();                       // 调用父类方法发送FPGA消息和事件通知
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 低电平时间访问方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取低电平时间
     *
     * @return 低电平时间（纳秒）
     */
    public long getTimeLow() {                      // 获取低电平时间方法
        return timeLow;                             // 返回当前低电平时间
    }

    /**
     * 设置低电平时间
     * 更新低电平时间并发送FPGA消息
     *
     * @param timeLow 低电平时间（纳秒）
     *
     * @note 设置后会发送FPGA_CMD_TRIG命令
     */
    public void setTimeLow(long timeLow) {          // 设置低电平时间方法
        this.timeLow = timeLow;                     // 更新低电平时间成员变量
        triggerParamChange();                       // 调用父类方法发送FPGA消息和事件通知
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 抽象方法实现 - 继承自Trigger
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取触发源对应的触发电平数量
     * 矮脉冲触发需要两个触发电平：上限电平和下限电平
     *
     * @return 始终返回2，表示需要两个触发电平
     *
     * @see TriggerLevel#TRIGGER_LEVEL_NORMAL
     * @see TriggerLevel#TRIGGER_LEVEL_HIGH
     */
    @Override                                       // 重写父类方法注解
    public int getSrcTriggerLevelCnt() {            // 获取触发电平数量方法
        return 2;                                   // 返回2，表示需要两个触发电平（上限和下限）
    }

    /**
     * 获取触发源数量
     * 矮脉冲触发只支持单触发源
     *
     * @return 始终返回1，表示单触发源
     */
    @Override                                       // 重写父类方法注解
    public int getTriggerSourceCnt() {              // 获取触发源数量方法
        return 1;                                   // 返回1，表示单触发源
    }

    /**
     * 判断是否支持多触发源
     * 矮脉冲触发只支持单触发源
     *
     * @return 始终返回false，表示只支持单触发源
     */
    @Override                                       // 重写父类方法注解
    protected boolean isMultitriggerSource() {      // 判断是否支持多触发源方法
        return false;                               // 返回false，表示不支持多触发源
    }

}
