/**
 * 示波器FPGA通道12位数值寄存器类
 * 继承自FPGAReg基类，负责设置FPGA通道的12位数值参数
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>设置通道1/2的12位数值</li>
 *   <li>设置通道3/4的12位数值</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建通道12位数值寄存器对象
 * FPGAReg_NUM_CY12 reg = new FPGAReg_NUM_CY12();
 *
 * // 设置通道1/2的12位数值
 * reg.setVol12(2048);  // 设置通道1/2的数值为2048
 *
 * // 设置通道3/4的12位数值
 * reg.setVol34(2048);  // 设置通道3/4的数值为2048
 *
 * // 发送命令到FPGA
 * FPGACommand.getInstance().sendCommand(reg);
 * </pre>
 *
 * @see FPGAReg
 * @see FPGACommand
 * @see Channel
 * @author zhuzh
 * @version 1.0
 * @since 2018-4-8
 */
public class FPGAReg_NUM_CY12 extends FPGAReg {                                            // 类声明：通道12位数值寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化通道12位数值寄存器
     *
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_NUM_CY12</li>
     *   <li>数据长度：8字节（2个字，每个字32位）</li>
     * </ul>
     *
     * <p><b>寄存器地址说明：</b></p>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器地址映射                         │
     * ├─────────────────────────────────────────┤
     * │  FPGA_NUM_CY12                          │
     * │  功能: 设置通道12位数值                 │
     * │  大小: 8字节（2个字）                   │
     * │  访问: 只写 (由FPGA读取)                │
     * │                                         │
     * │  数据结构:                              │
     * │  字0: 通道1/2的12位数值                 │
     * │  字1: 通道3/4的12位数值                 │
     * └─────────────────────────────────────────┘
     * </pre>
     */
    public FPGAReg_NUM_CY12() {                                                            // 构造方法：初始化通道12位数值寄存器
        // 调用父类构造函数，设置寄存器ID和数据长度
        // FPGA_NUM_CY12：通道12位数值寄存器地址
        // 8：数据长度为8字节（2个32位字）
        super(FPGA_NUM_CY12, 8);                                                            // 调用父类构造函数，设置寄存器ID为FPGA_NUM_CY12，长度8字节
    }                                                                                      // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法 - 通道数值设置
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置通道1/2的12位数值
     * 设置通道1和通道2的12位数值参数
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>设置通道1和通道2的12位数值</li>
     *   <li>用于控制通道的12位参数</li>
     *   <li>值范围：0到4095（12位分辨率）</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>val：12位数值（32位，实际使用12位）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <ul>
     *   <li>存储位置：字0（32位）</li>
     * </ul>
     *
     * @param val 通道1/2的12位数值
     */
    public void setVol12(int val){                                                         // 公有方法：设置通道1/2的12位数值
        // 调用父类setVal方法，设置字0的值
        setVal(0, val);                                                                    // 设置字0，值为val（通道1/2的12位数值）
    }                                                                                      // 方法结束

    /**
     * 设置通道3/4的12位数值
     * 设置通道3和通道4的12位数值参数
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>设置通道3和通道4的12位数值</li>
     *   <li>用于控制通道的12位参数</li>
     *   <li>值范围：0到4095（12位分辨率）</li>
     * </ul>
     *
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>val：12位数值（32位，实际使用12位）</li>
     * </ul>
     *
     * <p><b>数据存储位置：</b></p>
     * <ul>
     *   <li>存储位置：字1（32位）</li>
     * </ul>
     *
     * @param val 通道3/4的12位数值
     */
    public void setVol34(int val){                                                         // 公有方法：设置通道3/4的12位数值
        // 调用父类setVal方法，设置字1的值
        setVal(1, val);                                                                    // 设置字1，值为val（通道3/4的12位数值）
    }                                                                                      // 方法结束

}                                                                                          // 类结束