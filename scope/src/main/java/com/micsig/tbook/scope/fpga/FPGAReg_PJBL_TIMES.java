/**
 * 示波器FPGA平均次数寄存器类
 * 继承自FPGAReg基类，负责设置FPGA平均采样次数参数
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>设置平均采样次数</li>
 *   <li>从Sample获取采样次数</li>
 *   <li>自动更新寄存器值</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建平均次数寄存器对象
 * FPGAReg_PJBL_TIMES reg = new FPGAReg_PJBL_TIMES();
 *
 * // 发送命令到FPGA（会自动调用onCommand更新采样次数）
 * FPGACommand.getInstance().sendCommand(reg);
 *
 * // onCommand会自动执行：
 * // setVal(0, 32, Sample.getInstance().getSampleNum())
 * </pre>
 *
 * @see FPGAReg
 * @see FPGACommand
 * @see Sample
 * @author zhuzh
 * @version 1.0
 * @since 2018/3/16
 */
public class FPGAReg_PJBL_TIMES extends FPGAReg {                                          // 类声明：平均次数寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化平均次数寄存器
     *
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_PJBL_TIMES</li>
     *   <li>数据长度：4字节（32位）</li>
     * </ul>
     *
     * <p><b>寄存器地址说明：</b></p>
     * <pre>
     * ┌─────────────────────────────────────────┐
     * │  寄存器地址映射                         │
     * ├─────────────────────────────────────────┤
     * │  FPGA_PJBL_TIMES                        │
     * │  功能: 设置平均采样次数                 │
     * │  大小: 4字节                            │
     * │  访问: 只写 (由FPGA读取)                │
     * │                                         │
     * │  数据结构:                              │
     * │  位0-31: 平均采样次数                   │
     * └─────────────────────────────────────────┘
     * </pre>
     */
    public FPGAReg_PJBL_TIMES() {                                                          // 构造方法：初始化平均次数寄存器
        // 调用父类构造函数，设置寄存器ID和数据长度
        // FPGA_PJBL_TIMES：平均次数寄存器地址
        // 4：数据长度为4字节（32位）
        super(FPGAReg.FPGA_PJBL_TIMES, 4);                                                  // 调用父类构造函数，设置寄存器ID为FPGA_PJBL_TIMES，长度4字节
    }                                                                                      // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法 - 命令回调
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 命令回调方法
     * 在发送命令前自动更新采样次数
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>重写父类的onCommand方法</li>
     *   <li>在发送命令前自动调用</li>
     *   <li>从Sample获取当前采样次数并设置到寄存器</li>
     * </ul>
     *
     * <p><b>调用时机：</b></p>
     * <ul>
     *   <li>FPGACommand发送命令前自动调用</li>
     *   <li>确保寄存器值与Sample中的采样次数同步</li>
     * </ul>
     *
     * <p><b>数据来源：</b></p>
     * <ul>
     *   <li>Sample.getInstance().getSampleNum()：获取当前采样次数</li>
     *   <li>采样次数由用户设置或系统默认</li>
     * </ul>
     *
     * <p><b>数据存储：</b></p>
     * <ul>
     *   <li>setVal(0, 32, sampleNum)：设置字0的位0-31为采样次数</li>
     *   <li>使用32位存储采样次数值</li>
     * </ul>
     */
    @Override                                                                              // 重写父类方法：onCommand
    public void onCommand() {                                                              // 公有方法：命令回调，自动更新采样次数
        // 从Sample获取当前采样次数，并设置到寄存器
        // Sample.getInstance()：获取Sample单例对象
        // getSampleNum()：获取当前采样次数
        // setVal(0, 32, sampleNum)：设置字0的位0-31为采样次数
        setVal(0, 32, Sample.getInstance().getSampleNum());                                 // 设置字0的位0-31，值为Sample中的采样次数
    }                                                                                      // 方法结束

}                                                                                          // 类结束