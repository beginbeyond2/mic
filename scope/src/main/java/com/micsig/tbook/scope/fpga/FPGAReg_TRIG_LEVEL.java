/**
 * 示波器FPGA触发电平寄存器类
 * 继承自FPGAReg基类，负责管理FPGA触发电平配置
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>存储各触发源的触发电平值</li>
 *   <li>支持上升沿和下降沿触发电平配置</li>
 *   <li>支持触发电平回差值（迟滞）配置</li>
 *   <li>支持溢出标志检测</li>
 * </ul>
 *
 * <p><b>寄存器结构：</b></p>
 * <ul>
 *   <li>寄存器ID：FPGA_TRIG_LEVEL (0x27)</li>
 *   <li>数据长度：20字节（5个字，每个字32位）</li>
 *   <li>每个字存储一个触发源的触发电平配置</li>
 *   <li>位0-14：上升沿触发电平值（15位）</li>
 *   <li>位15：上升沿溢出标志（1位）</li>
 *   <li>位16-30：下降沿触发电平值（15位）</li>
 *   <li>位31：下降沿溢出标志（1位）</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 通过FPGACommand设置触发电平
 * FPGACommand fpgaCmd = FPGACommand.getInstance();
 *
 * // 设置触发电平寄存器
 * // fpgaIdx: FPGA索引
 * // value: 触发电平值
 * // trigIdx: 触发源索引
 * // riseHuiCha: 上升沿回差值
 * // fallHuiCha: 下降沿回差值
 * fpgaCmd.setTriggerLevelReg(fpgaIdx, value, trigIdx, riseHuiCha, fallHuiCha);
 *
 * // 内部操作：
 * // FPGAReg reg = getFPGAReg(fpgaIdx, FPGAReg.FPGA_TRIG_LEVEL);
 * // reg.setVal(idx, 0, 15, x1);    // 设置上升沿触发电平值
 * // reg.setVal(idx, 15, 1, bOF);   // 设置溢出标志
 * // reg.setVal(idx, 16, 15, x2);   // 设置下降沿触发电平值
 * // reg.setVal(idx, 31, 1, bOF);   // 设置溢出标志
 * </pre>
 *
 * @see FPGAReg
 * @see FPGACommand#setTriggerLevelReg(int, int, int, int, int)
 * @see FPGACommand#setTriggerLevelReg_snd(int, int, int, int)
 * @see FPGACommand#gntR_trigLevel(int)
 * @see Trigger
 * @see TriggerLevel
 */
public class FPGAReg_TRIG_LEVEL extends FPGAReg {                                         // 类声明：触发电平寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化触发电平寄存器
     *
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_TRIG_LEVEL (0x27)</li>
     *   <li>数据长度：20字节（5个字，每个字32位）</li>
     * </ul>
     *
     * <p><b>寄存器结构说明：</b></p>
     * <ul>
     *   <li>字0：触发源1的触发电平配置</li>
     *   <li>字1：触发源2的触发电平配置</li>
     *   <li>字2：触发源3的触发电平配置</li>
     *   <li>字3：触发源4的触发电平配置</li>
     *   <li>字4：第二触发源的触发电平配置</li>
     * </ul>
     *
     * <p><b>每个字的数据格式：</b></p>
     * <ul>
     *   <li>位0-14：上升沿触发电平值（15位，范围0-32767）</li>
     *   <li>位15：上升沿溢出标志（1位，0=正常，1=溢出）</li>
     *   <li>位16-30：下降沿触发电平值（15位，范围0-32767）</li>
     *   <li>位31：下降沿溢出标志（1位，0=正常，1=溢出）</li>
     * </ul>
     *
     * <p><b>触发电平计算公式：</b></p>
     * <pre>
     * // 上升沿触发电平
     * x1 = value + 512 * 32 + riseHuiCha * 32
     *
     * // 下降沿触发电平
     * x2 = value + 512 * 32 - fallHuiCha * 32
     *
     * // 其中：
     * // - value: 用户设置的触发电平值
     * // - 512 * 32: 基准偏移量（AD中心点偏移）
     * // - riseHuiCha: 上升沿回差值（迟滞）
     * // - fallHuiCha: 下降沿回差值（迟滞）
     * </pre>
     */
    public FPGAReg_TRIG_LEVEL() {                                                         // 构造方法：初始化触发电平寄存器
        super(FPGA_TRIG_LEVEL, 20);                                                       // 调用父类构造函数，设置寄存器ID和数据长度20字节
    }                                                                                    // 构造方法结束

}                                                                                        // 类结束