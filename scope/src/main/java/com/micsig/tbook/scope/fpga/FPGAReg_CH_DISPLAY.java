package com.micsig.tbook.scope.fpga;  // 定义包名：示波器FPGA寄存器管理模块

/**
 * FPGA通道显示寄存器类 - 用于配置示波器通道的显示属性
 *
 * <p><b>模块定位：</b>
 * <ul>
 *   <li>所属模块：scope.fpga（示波器FPGA寄存器管理模块）</li>
 *   <li>架构层级：硬件抽象层 - FPGA显示配置</li>
 *   <li>设计模式：继承模式（继承FPGAReg基类）</li>
 *   <li>职责类型：显示配置管理器</li>
 * </ul>
 *
 * <p><b>核心职责：</b>
 * <ul>
 *   <li>配置通道的显示启用状态</li>
 *   <li>配置通道的显示颜色</li>
 *   <li>支持多通道显示配置</li>
 *   <li>封装FPGA显示寄存器的位操作</li>
 * </ul>
 *
 * <p><b>设计目的：</b>
 * <ul>
 *   <li>提供通道显示属性的配置接口</li>
 *   <li>支持通道显示的启用/禁用</li>
 *   <li>支持通道颜色的自定义</li>
 *   <li>支持多通道显示管理</li>
 * </ul>
 *
 * <p><b>寄存器结构：</b>
 * <pre>
 * FPGA_CH_DISPLAY寄存器（4字节，32位）
 * │
 * ├── 位0-3：通道启用标志（ChEnable）
 * │     ├── 位0：CH1启用标志（0=禁用，1=启用）
 * │     ├── 位1：CH2启用标志（0=禁用，1=启用）
 * │     ├── 位2：CH3启用标志（0=禁用，1=启用）
 * │     └── 位3：CH4启用标志（0=禁用，1=启用）
 * │
 * ├── 位4-5：通道1颜色（Ch1Color）
 * │     └── 通道1的显示颜色（2位，0-3）
 * │
 * ├── 位6-7：通道2颜色（Ch2Color）
 * │     └── 通道2的显示颜色（2位，0-3）
 * │
 * └── 位8-31：保留字段
 *       └── 未使用，保留为0
 * </pre>
 *
 * <p><b>显示属性说明：</b>
 * <ul>
 *   <li>通道启用：控制通道是否在屏幕上显示波形</li>
 *   <li>通道颜色：设置通道波形的显示颜色</li>
 *   <li>颜色值：通常对应预定义的颜色方案（如黄色、蓝色、绿色、红色等）</li>
 *   <li>颜色值范围：2位，0-3，对应4种颜色</li>
 * </ul>
 *
 * <p><b>使用场景：</b>
 * <ul>
 *   <li>通道显示控制：用户启用或禁用通道显示</li>
 *   <li>颜色自定义：用户自定义通道显示颜色</li>
 *   <li>界面初始化：初始化通道显示属性</li>
 *   <li>显示切换：动态切换通道显示状态</li>
 * </ul>
 *
 * <p><b>依赖关系：</b>
 * <ul>
 *   <li>继承：FPGAReg（FPGA寄存器基类）</li>
 *   <li>依赖：FPGACommand（FPGA命令处理类）</li>
 *   <li>依赖：Channel（通道类）</li>
 * </ul>
 *
 * <p><b>线程安全：</b>
 * <ul>
 *   <li>寄存器操作在FPGACommand中同步执行</li>
 *   <li>显示配置需要确保硬件稳定</li>
 * </ul>
 *
 * <p><b>使用示例：</b>
 * <pre>
 * // 创建通道显示寄存器实例
 * FPGAReg_CH_DISPLAY regChDisplay = new FPGAReg_CH_DISPLAY();
 *
 * // 启用通道1和通道2显示
 * regChDisplay.setChEnable(0, 1);  // 启用CH1显示
 * regChDisplay.setChEnable(1, 1);  // 启用CH2显示
 *
 * // 设置通道颜色
 * regChDisplay.setCh1Color(0);  // 设置CH1颜色为颜色0
 * regChDisplay.setCh2Color(1);  // 设置CH2颜色为颜色1
 *
 * // 发送寄存器配置到FPGA
 * fpgaCommand.sendCmd(regChDisplay);
 * </pre>
 *
 * @author zhuzh
 * @version 1.0
 * @since 2018/4/10
 * @see FPGAReg FPGA寄存器基类
 * @see FPGACommand FPGA命令处理类
 */
public class FPGAReg_CH_DISPLAY extends FPGAReg {  // 继承FPGAReg基类，复用寄存器操作方法

    /**
     * 日志标签 - 用于日志输出和调试
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>标识日志来源类</li>
     *   <li>用于调试和故障诊断</li>
     * </ul>
     */
    private static final String TAG = "FPGAReg_CH_DISPLAY";  // 日志标签，用于日志输出和调试

    /**
     * 构造函数 - 初始化通道显示寄存器
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>调用父类构造函数，设置寄存器地址和大小</li>
     *   <li>寄存器地址：FPGA_CH_DISPLAY（通道显示寄存器）</li>
     *   <li>寄存器大小：4字节（32位）</li>
     * </ul>
     *
     * <p><b>寄存器地址说明：</b>
     * <ul>
     *   <li>FPGA_CH_DISPLAY：用于配置通道的显示属性</li>
     *   <li>地址值由FPGAReg基类定义</li>
     * </ul>
     *
     * <p><b>寄存器大小说明：</b>
     * <ul>
     *   <li>4字节（32位）用于存储通道显示配置</li>
     *   <li>包含通道启用标志和颜色配置</li>
     * </ul>
     */
    public FPGAReg_CH_DISPLAY() {  // 构造函数
        // 调用父类构造函数，传入寄存器地址和大小
        // FPGA_CH_DISPLAY：通道显示寄存器地址
        // 4：寄存器大小为4字节（32位）
        super(FPGA_CH_DISPLAY, 4);  // 初始化通道显示寄存器，地址为FPGA_CH_DISPLAY，大小为4字节
    }

    /**
     * 设置通道启用标志 - 配置指定通道的显示启用状态
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>设置指定通道的显示启用标志</li>
     *   <li>控制通道是否在屏幕上显示波形</li>
     *   <li>支持多通道配置</li>
     * </ul>
     *
     * <p><b>通道启用说明：</b>
     * <ul>
     *   <li>启用（val=1）：通道波形显示在屏幕上</li>
     *   <li>禁用（val=0）：通道波形不显示在屏幕上</li>
     *   <li>每个通道有独立的启用标志</li>
     * </ul>
     *
     * <p><b>使用场景：</b>
     * <ul>
     *   <li>用户启用通道：用户选择显示某个通道</li>
     *   <li>用户禁用通道：用户选择隐藏某个通道</li>
     *   <li>批量配置：批量启用或禁用多个通道</li>
     * </ul>
     *
     * @param idx 通道索引（0=CH1，1=CH2，2=CH3，3=CH4）
     * @param val 启用标志（0=禁用，1=启用）
     */
    public void setChEnable(int idx, int val){  // 设置通道启用标志方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=idx（通道索引对应的位）
        // 参数2：位宽度=1位
        // 参数3：启用标志值（0或1）
        setVal(idx, 1, val);  // 设置指定通道位的启用标志，宽度为1位，值为val
    }

    /**
     * 设置通道1颜色 - 配置通道1的显示颜色
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>设置通道1的显示颜色</li>
     *   <li>数据位置：位4-5（2位）</li>
     *   <li>颜色值对应预定义的颜色方案</li>
     * </ul>
     *
     * <p><b>颜色值说明：</b>
     * <ul>
     *   <li>颜色值：2位，范围0-3</li>
     *   <li>通常对应预定义的颜色（如黄色、蓝色、绿色、红色等）</li>
     *   <li>具体颜色由FPGA和显示系统定义</li>
     * </ul>
     *
     * <p><b>使用场景：</b>
     * <ul>
     *   <li>颜色自定义：用户自定义通道1的显示颜色</li>
     *   <li>颜色方案切换：切换不同的颜色方案</li>
     * </ul>
     *
     * @param val 颜色值（2位，0-3）
     */
    public void setCh1Color(int val){  // 设置通道1颜色方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=4（位4开始）
        // 参数2：位宽度=2位
        // 参数3：颜色值（0-3）
        setVal(4, 2, val);  // 设置位4-5的值，宽度为2位，值为val
    }

    /**
     * 设置通道2颜色 - 配置通道2的显示颜色
     *
     * <p><b>功能说明：</b>
     * <ul>
     *   <li>设置通道2的显示颜色</li>
     *   <li>数据位置：位6-7（2位）</li>
     *   <li>颜色值对应预定义的颜色方案</li>
     * </ul>
     *
     * <p><b>颜色值说明：</b>
     * <ul>
     *   <li>颜色值：2位，范围0-3</li>
     *   <li>通常对应预定义的颜色（如黄色、蓝色、绿色、红色等）</li>
     *   <li>具体颜色由FPGA和显示系统定义</li>
     * </ul>
     *
     * <p><b>使用场景：</b>
     * <ul>
     *   <li>颜色自定义：用户自定义通道2的显示颜色</li>
     *   <li>颜色方案切换：切换不同的颜色方案</li>
     * </ul>
     *
     * @param val 颜色值（2位，0-3）
     */
    public void setCh2Color(int val){  // 设置通道2颜色方法
        // 调用父类setVal方法，设置寄存器值
        // 参数1：起始位位置=6（位6开始）
        // 参数2：位宽度=2位
        // 参数3：颜色值（0-3）
        setVal(6, 2, val);  // 设置位6-7的值，宽度为2位，值为val
    }
}
