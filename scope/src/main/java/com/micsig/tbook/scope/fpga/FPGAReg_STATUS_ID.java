/**
 * 示波器FPGA状态ID寄存器类
 * 继承自FPGAReg基类，负责读取FPGA芯片ID，用于验证FPGA是否正常加载
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>读取FPGA芯片ID</li>
 *   <li>验证FPGA是否正常加载</li>
 *   <li>提供FPGA加载状态检测支持</li>
 * </ul>
 *
 * <p><b>寄存器结构：</b></p>
 * <ul>
 *   <li>寄存器ID：FPGA_STATUS_ID (0x80)</li>
 *   <li>数据长度：4字节（32位）</li>
 *   <li>寄存器类型：读寄存器（需要从FPGA接收数据）</li>
 * </ul>
 *
 * <p><b>FPGA ID值：</b></p>
 * <pre>
 * FPGA_ID = 0x46504741
 * 解析为ASCII字符："FPGA"
 * - 0x46 = 'F'
 * - 0x50 = 'P'
 * - 0x47 = 'G'
 * - 0x41 = 'A'
 *
 * 用于验证FPGA是否正常加载：
 * - 如果读取的ID == 0x46504741，则FPGA加载成功
 * - 如果读取的ID != 0x46504741，则FPGA加载失败
 * </pre>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 通过FPGACommand读取FPGA ID
 * FPGACommand fpgaCmd = FPGACommand.getInstance();
 * int fpgaId = fpgaCmd.readFpgaId(fpgaIdx);
 *
 * // 内部操作：
 * // FPGAReg_STATUS_ID regStatusId = new FPGAReg_STATUS_ID();
 * // regStatusId.setFpgaIdx(fpgaIdx);
 * // int id = regStatusId.getId();  // 获取FPGA ID
 *
 * // 验证FPGA ID
 * int FPGA_ID = 0x46504741;  // "FPGA"的ASCII码
 * if (fpgaId == FPGA_ID) {
 *     // FPGA加载成功
 * } else {
 *     // FPGA加载失败，需要重新加载
 * }
 *
 * // 多FPGA系统读取所有FPGA ID
 * int allFpgaId = fpgaCmd.readFpgaId(0) | fpgaCmd.readFpgaId(1);
 * </pre>
 *
 * @see FPGAReg
 * @see FPGACommand#readFpgaId(int)
 * @see FPGAMessage#fpgaId()
 * @see ScopeMessage#FPGA_ID
 * @see FPGABoot
 */
public class FPGAReg_STATUS_ID extends FPGAReg {                                         // 类声明：FPGA状态ID寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 接收数据寄存器
     * 用于存储从FPGA接收的ID数据
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>存储FPGA返回的ID数据</li>
     *   <li>与发送寄存器分离，避免数据冲突</li>
     *   <li>通过getVal()方法获取ID值</li>
     * </ul>
     *
     * <p><b>数据格式：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_STATUS_ID</li>
     *   <li>数据长度：4字节</li>
     *   <li>数据内容：FPGA芯片ID（32位）</li>
     * </ul>
     *
     * <p><b>ID值说明：</b></p>
     * <ul>
     *   <li>正常值：0x46504741（"FPGA"的ASCII码）</li>
     *   <li>异常值：其他值表示FPGA未正常加载</li>
     * </ul>
     */
    private FPGAReg recvReg;                                                             // 成员变量：接收数据寄存器，存储从FPGA接收的ID数据

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化FPGA状态ID寄存器
     *
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_STATUS_ID (0x80)</li>
     *   <li>数据长度：4字节</li>
     *   <li>寄存器类型：读寄存器（true表示需要接收数据）</li>
     * </ul>
     *
     * <p><b>初始化操作：</b></p>
     * <ul>
     *   <li>调用父类构造函数，设置寄存器ID和数据长度</li>
     *   <li>创建接收数据寄存器recvReg</li>
     *   <li>recvReg用于存储从FPGA接收的ID数据</li>
     * </ul>
     *
     * <p><b>读寄存器说明：</b></p>
     * <ul>
     *   <li>发送寄存器：用于发送读取命令到FPGA</li>
     *   <li>接收寄存器（recvReg）：用于存储FPGA返回的数据</li>
     *   <li>两个寄存器分离，避免数据冲突</li>
     * </ul>
     *
     * <p><b>FPGA ID值：</b></p>
     * <ul>
     *   <li>正常值：0x46504741（"FPGA"的ASCII码）</li>
     *   <li>F=0x46, P=0x50, G=0x47, A=0x41</li>
     *   <li>用于验证FPGA是否正常加载</li>
     * </ul>
     */
    public FPGAReg_STATUS_ID() {                                                         // 构造方法：初始化FPGA状态ID寄存器
        super(FPGA_STATUS_ID, 4, true);                                                  // 调用父类构造函数，设置寄存器ID、数据长度4字节、读寄存器标志true
        recvReg = new FPGAReg(FPGA_STATUS_ID,4);                                         // 创建接收数据寄存器，用于存储从FPGA接收的ID数据
    }                                                                                    // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法 - ID读取
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取FPGA ID值
     * 从接收寄存器读取FPGA芯片ID（字0）
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>从recvReg读取ID值</li>
     *   <li>返回字0的ID数据</li>
     *   <li>用于获取完整的32位FPGA ID值</li>
     * </ul>
     *
     * <p><b>FPGA ID值说明：</b></p>
     * <pre>
     * 正常FPGA ID值：
     * - 值：0x46504741
     * - 解析："FPGA"的ASCII码
     * - F=0x46, P=0x50, G=0x47, A=0x41
     *
     * ID验证：
     * - 如果getId() == 0x46504741，FPGA加载成功
     * - 如果getId() != 0x46504741，FPGA加载失败
     * </pre>
     *
     * <p><b>应用场景：</b></p>
     * <ul>
     *   <li>FPGA加载验证</li>
     *   <li>FPGA状态检测</li>
     *   <li>系统启动验证</li>
     * </ul>
     *
     * @return FPGA ID值（32位整数）
     */
    public int getId(){                                                                  // 公有方法：获取FPGA ID值
        return recvReg.getVal(0);                                                        // 从接收寄存器获取字0的ID值
    }                                                                                    // 方法结束

    /**
     * 获取接收数据寄存器
     * 返回用于存储FPGA返回数据的接收寄存器
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>返回recvReg接收数据寄存器对象</li>
     *   <li>用于FPGA数据接收和解析</li>
     *   <li>FPGA返回的ID数据存储在recvReg中</li>
     * </ul>
     *
     * <p><b>使用场景：</b></p>
     * <ul>
     *   <li>FPGA数据接收处理</li>
     *   <li>数据解析和转换</li>
     *   <li>ID验证</li>
     * </ul>
     *
     * @return 接收数据寄存器对象
     */
    public FPGAReg getRecvReg(){                                                         // 公有方法：获取接收数据寄存器
        return recvReg;                                                                  // 返回接收数据寄存器对象
    }                                                                                    // 方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 重写方法 - FPGA索引设置
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置FPGA索引
     * 重写父类方法，同步设置recvReg的FPGA索引
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>重写父类setFpgaIdx方法</li>
     *   <li>设置本寄存器的FPGA索引</li>
     *   <li>同步设置recvReg的FPGA索引</li>
     *   <li>确保两个寄存器的索引一致</li>
     * </ul>
     *
     * <p><b>重写原因：</b></p>
     * <ul>
     *   <li>本类包含两个寄存器对象（本类和recvReg）</li>
     *   <li>需要确保两个寄存器的FPGA索引一致</li>
     *   <li>避免数据读写时索引不匹配</li>
     *   <li>保证数据接收的正确性</li>
     * </ul>
     *
     * <p><b>FPGA索引说明：</b></p>
     * <ul>
     *   <li>多FPGA系统中，每个FPGA有独立的索引</li>
     *   <li>索引0：第一个FPGA</li>
     *   <li>索引1：第二个FPGA</li>
     *   <li>索引用于区分不同的FPGA芯片</li>
     * </ul>
     *
     * <p><b>使用场景：</b></p>
     * <ul>
     *   <li>多FPGA系统</li>
     *   <li>FPGA数据读写</li>
     *   <li>FPGA ID读取</li>
     * </ul>
     *
     * @param fpgaIdx FPGA索引（0或1）
     */
    @Override                                                                            // 注解：重写父类方法
    public void setFpgaIdx(int fpgaIdx) {                                                // 公有方法：设置FPGA索引
        super.setFpgaIdx(fpgaIdx);                                                       // 调用父类方法，设置本寄存器的FPGA索引
        recvReg.setFpgaIdx(fpgaIdx);                                                     // 同步设置recvReg的FPGA索引，确保两个寄存器的索引一致
    }                                                                                    // 方法结束
}                                                                                        // 类结束