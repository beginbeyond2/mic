package com.micsig.tbook.scope.Event;                                                     // 包声明：事件模块

/**
 * ╔══════════════════════════════════════════════════════════════════════════════╗
 * ║              EventBase - 示波器事件基类                                        ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【模块定位】                                                                 ║
 * ║   Event模块的事件基类，位于Event包下，                                        ║
 * ║   是示波器事件系统的核心数据结构。                                             ║
 * ║                                                                              ║
 * ║ 【核心职责】                                                                 ║
 * ║   1. 封装事件ID，标识事件类型                                                 ║
 * ║   2. 封装事件数据，传递事件相关信息                                           ║
 * ║   3. 封装通道索引，标识事件关联的通道                                         ║
 * ║   4. 支持同步/异步事件传递                                                    ║
 * ║   5. 支持主发送者标识                                                         ║
 * ║                                                                              ║
 * ║ 【事件系统架构】                                                             ║
 * ║   ┌──────────────────────────────────────────────────────────────────────┐ ║
 * ║   │                        事件系统架构                                   │ ║
 * ║   │                                                                      │ ║
 * ║   │   【事件生产者】                                                       │ ║
 * ║   │   - 各种模块（通道、触发、水平轴等）                                   │ ║
 * ║   │   - 创建EventBase对象                                                 │ ║
 * ║   │   - 调用EventFactory.sendEvent()发送事件                              │ ║
 * ║   │                                                                      │ ║
 * ║   │   【事件中心】                                                         │ ║
 * ║   │   - EventFactory：事件工厂，管理事件分发                              │ ║
 * ║   │   - 维护观察者列表                                                    │ ║
 * ║   │   - 同步/异步分发事件                                                 │ ║
 * ║   │                                                                      │ ║
 * ║   │   【事件消费者】                                                       │ ║
 * ║   │   - 实现Observer接口的类                                              │ ║
 * ║   │   - 注册到EventFactory                                                │ ║
 * ║   │   - 在update()方法中处理事件                                          │ ║
 * ║   └──────────────────────────────────────────────────────────────────────┘ ║
 * ║                                                                              ║
 * ║ 【事件数据流】                                                               ║
 * ║   ┌─────────────┐    ┌─────────────┐    ┌─────────────┐                   ║
 * ║   │ 事件生产者  │───▶│ EventBase   │───▶│ EventFactory│                   ║
 * ║   │ (创建事件)  │    │ (封装数据)  │    │ (分发事件)  │                   ║
 * ║   └─────────────┘    └─────────────┘    └──────┬──────┘                   ║
 * ║                                                │                           ║
 * ║                                                ▼                           ║
 * ║                                    ┌─────────────────┐                     ║
 * ║                                    │  观察者列表     │                     ║
 * ║                                    │  (处理事件)     │                     ║
 * ║                                    └─────────────────┘                     ║
 * ║                                                                              ║
 * ║ 【事件ID说明】                                                               ║
 * ║   事件ID由EventFactory中定义的常量标识，常见事件包括：                        ║
 * ║   - EVENT_TIME_SCALE: 时基变化事件                                          ║
 * ║   - EVENT_TIME_POS: 时间位置变化事件                                        ║
 * ║   - EVENT_CHANNEL_OPEN: 通道打开事件                                        ║
 * ║   - EVENT_CHANNEL_CLOSE: 通道关闭事件                                       ║
 * ║   - EVENT_TRIGGER_MODE: 触发模式变化事件                                    ║
 * ║   - EVENT_MATH_FFT_SCALE: FFT频率档位变化事件                               ║
 * ║   - EVENT_REF_WAVE_UPDATE: 参考波形更新事件                                 ║
 * ║                                                                              ║
 * ║ 【成员变量说明】                                                             ║
 * ║   ┌──────────────────────────────────────────────────────────────────────┐ ║
 * ║   │                        成员变量说明                                   │ ║
 * ║   │                                                                      │ ║
 * ║   │   【eventId】事件ID                                                   │ ║
 * ║   │   - 类型：int                                                        │ ║
 * ║   │   - 作用：标识事件类型                                                │ ║
 * ║   │   - 取值：EventFactory中定义的事件常量                                │ ║
 * ║   │                                                                      │ ║
 * ║   │   【data】事件数据                                                    │ ║
 * ║   │   - 类型：Object                                                     │ ║
 * ║   │   - 作用：携带事件相关的数据                                          │ ║
 * ║   │   - 取值：任意对象，根据事件类型而定                                  │ ║
 * ║   │                                                                      │ ║
 * ║   │   【channelIndex】通道索引                                            │ ║
 * ║   │   - 类型：int                                                        │ ║
 * ║   │   - 作用：标识事件关联的通道                                          │ ║
 * ║   │   - 取值：通道号（如CH1=0, CH2=1, MATH=2等）                         │ ║
 * ║   │   - 示例：数学通道事件时，存储当前操作的数学通道号                     │ ║
 * ║   │                                                                      │ ║
 * ║   │   【mainSender】主发送者标识                                          │ ║
 * ║   │   - 类型：boolean                                                    │ ║
 * ║   │   - 作用：标识事件是否由主发送者发送                                  │ ║
 * ║   │   - 取值：true=主发送者，false=非主发送者                             │ ║
 * ║   │                                                                      │ ║
 * ║   │   【async】异步标识                                                   │ ║
 * ║   │   - 类型：boolean                                                    │ ║
 * ║   │   - 作用：标识事件是否异步处理                                        │ ║
 * ║   │   - 取值：true=异步，false=同步                                      │ ║
 * ║   └──────────────────────────────────────────────────────────────────────┘ ║
 * ║                                                                              ║
 * ║ 【同步/异步事件】                                                            ║
 * ║   ┌──────────────────────────────────────────────────────────────────────┐ ║
 * ║   │                        同步/异步事件对比                              │ ║
 * ║   │                                                                      │ ║
 * ║   │   【同步事件】                                                         │ ║
 * ║   │   - 在发送线程中立即执行                                              │ ║
 * ║   │   - 阻塞发送线程直到所有观察者处理完成                                │ ║
 * ║   │   - 适用于需要立即响应的事件                                          │ ║
 * ║   │                                                                      │ ║
 * ║   │   【异步事件】                                                         │ ║
 * ║   │   - 在事件线程中执行                                                  │ ║
 * ║   │   - 不阻塞发送线程                                                    │ ║
 * ║   │   - 适用于耗时操作或需要延迟处理的事件                                │ ║
 * ║   └──────────────────────────────────────────────────────────────────────┘ ║
 * ║                                                                              ║
 * ║ 【应用场景】                                                                 ║
 * ║   1. 时基变化：通知界面更新时基显示                                         ║
 * ║   2. 通道状态变化：通知通道打开/关闭                                        ║
 * ║   3. 触发设置变化：通知触发模式更新                                         ║
 * ║   4. 数学运算完成：通知数学通道更新                                         ║
 * ║   5. FFT计算完成：通知FFT频谱更新                                           ║
 * ║                                                                              ║
 * ║ 【设计模式】                                                                 ║
 * ║   - 观察者模式：作为被观察者传递的数据对象                                  ║
 * ║   - 数据传输对象（DTO）：封装事件数据                                       ║
 * ║                                                                              ║
 * ║ 【线程安全】                                                                 ║
 * ║   - 成员变量无volatile修饰，需注意多线程访问                                ║
 * ║   - 异步事件在事件线程中处理，避免阻塞UI线程                                ║
 * ║                                                                              ║
 * ║ 【依赖关系】                                                                 ║
 * ║   - EventFactory: 事件工厂，创建和分发事件                                 ║
 * ║   - Observer: 观察者接口，接收事件通知                                     ║
 * ║                                                                              ║
 * ║ 【作者】 MHO项目组                                                           ║
 * ╚══════════════════════════════════════════════════════════════════════════════╝
 */

/**
 * 示波器事件基类
 * 封装事件ID、数据、通道索引等信息
 * 是示波器事件系统的核心数据结构
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>事件ID：标识事件类型</li>
 *   <li>事件数据：携带事件相关信息</li>
 *   <li>通道索引：标识事件关联的通道</li>
 *   <li>异步标识：支持同步/异步事件传递</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建简单事件（只有ID）
 * EventBase event1 = new EventBase(EventFactory.EVENT_TIME_SCALE);
 *
 * // 创建带数据的事件
 * EventBase event2 = new EventBase(EventFactory.EVENT_CHANNEL_OPEN, channelData);
 *
 * // 创建带数据和通道索引的事件
 * EventBase event3 = new EventBase(EventFactory.EVENT_MATH_FFT_SCALE, fftData, mathChannelIndex);
 *
 * // 发送事件
 * EventFactory.sendEvent(event1);
 * </pre>
 *
 * @see EventFactory
 * @see java.util.Observer
 */
public class EventBase {                                                                   // 类声明：事件基类

    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /** 事件数据，携带事件相关的数据，类型根据事件类型而定 */
    private Object data = null;                                                            // 成员变量：事件数据，初始值为null

    /** 事件ID，标识事件类型，取值为EventFactory中定义的事件常量 */
    private int eventId = -1;                                                              // 成员变量：事件ID，初始值为-1（无效值）

    /** 通道索引，标识事件关联的通道，如CH1=0, CH2=1, MATH=2等 */
    private int channelIndex = -1;                                                         // 成员变量：通道索引，初始值为-1（无效值），示例：数学通道时存储当前操作的数学通道号

    /** 主发送者标识，标识事件是否由主发送者发送 */
    private boolean mainSender = false;                                                    // 成员变量：主发送者标识，初始值为false

    /** 异步标识，标识事件是否异步处理 */
    private boolean async = false;                                                         // 成员变量：异步标识，初始值为false（同步）

    // ═══════════════════════════════════════════════════════════════════════════════
    // 异步标识获取和设置方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 判断事件是否异步处理
     *
     * @return true=异步处理，false=同步处理
     */
    public boolean isAsync() {                                                             // 公有方法：判断事件是否异步处理
        return async;                                                                      // 返回异步标识
    }                                                                                       // 方法结束

    /**
     * 设置事件异步标识
     *
     * @param async true=异步处理，false=同步处理
     */
    public void setAsync(boolean async) {                                                  // 公有方法：设置事件异步标识
        this.async = async;                                                                // 更新异步标识
    }                                                                                       // 方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数（只指定事件ID）
     * 创建只包含事件ID的事件对象
     *
     * @param id 事件ID
     */
    public EventBase(int id) {                                                             // 构造方法：创建只包含事件ID的事件对象
        this.eventId = id;                                                                 // 设置事件ID
    }                                                                                       // 构造方法结束

    /**
     * 构造函数（指定事件ID、数据和可选的通道索引）
     * 创建包含事件ID、数据和通道索引的事件对象
     *
     * @param id            事件ID
     * @param data          事件数据
     * @param channelIndex  可选参数，通道索引（可变参数）
     */
    public EventBase(int id, Object data, int... channelIndex) {                           // 构造方法：创建包含事件ID、数据和通道索引的事件对象
        this.eventId = id;                                                                 // 设置事件ID
        this.data = data;                                                                  // 设置事件数据
        if (channelIndex.length > 0) {                                                     // 如果提供了通道索引参数
            this.channelIndex = channelIndex[0];                                           // 设置通道索引（取第一个参数）
        }                                                                                   // if语句结束
    }                                                                                       // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 通道索引获取和设置方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取通道索引
     *
     * @return 通道索引，-1表示未指定
     */
    public int getChannelIndex() {                                                         // 公有方法：获取通道索引
        return channelIndex;                                                               // 返回通道索引
    }                                                                                       // 方法结束

    /**
     * 设置通道索引
     *
     * @param channelIndex 通道索引
     */
    public void setChannelIndex(int channelIndex) {                                        // 公有方法：设置通道索引
        this.channelIndex = channelIndex;                                                  // 更新通道索引
    }                                                                                       // 方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 事件ID获取和设置方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置事件ID
     *
     * @param id 事件ID
     */
    public void setId(int id) {                                                            // 公有方法：设置事件ID
        this.eventId = id;                                                                 // 更新事件ID
    }                                                                                       // 方法结束

    /**
     * 获取事件ID
     *
     * @return 事件ID
     */
    public int getId() {                                                                   // 公有方法：获取事件ID
        return eventId;                                                                    // 返回事件ID
    }                                                                                       // 方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 事件数据获取和设置方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置事件数据
     *
     * @param data 事件数据
     */
    public void setData(Object data) {                                                     // 公有方法：设置事件数据
        this.data = data;                                                                  // 更新事件数据
    }                                                                                       // 方法结束

    /**
     * 获取事件数据
     *
     * @return 事件数据
     */
    public Object getData() {                                                              // 公有方法：获取事件数据
        return this.data;                                                                  // 返回事件数据
    }                                                                                       // 方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 主发送者标识获取和设置方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 判断是否为主发送者
     *
     * @return true=主发送者，false=非主发送者
     */
    public boolean isMainSender() {                                                        // 公有方法：判断是否为主发送者
        return mainSender;                                                                 // 返回主发送者标识
    }                                                                                       // 方法结束

    /**
     * 设置主发送者标识
     *
     * @param mainSender true=主发送者，false=非主发送者
     */
    public void setMainSender(boolean mainSender) {                                        // 公有方法：设置主发送者标识
        this.mainSender = mainSender;                                                      // 更新主发送者标识
    }                                                                                       // 方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // toString方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取事件对象的字符串表示
     * 用于日志输出和调试
     *
     * @return 事件对象的字符串表示
     */
    @Override
    public String toString() {                                                             // 重写方法：获取事件对象的字符串表示
        return "EventBase{" +                                                              // 返回字符串：EventBase{
                "eventId=" + eventId +                                                     // 添加事件ID
                ", data=" + data +                                                         // 添加事件数据
                '}';                                                                       // 添加结束大括号
    }                                                                                       // 方法结束
}                                                                                           // 类结束
