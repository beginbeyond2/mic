    /**
     * 生成FFT轴（不强制发送事件）
     * 调用genFFT_Axis(false)
     */
    public void genFFT_Axis() {                                                            // 公有方法：生成FFT轴（不强制发送事件）
        genFFT_Axis(false);                                                                // 调用重载方法，参数为false
    }                                                                                       // 方法结束

    /**
     * 生成FFT轴
     * 根据采样率和FFT点数计算有效频率范围，并更新频率档位范围
     *
     * <p><b>处理流程：</b></p>
     * <ol>
     *   <li>计算FFT点数</li>
     *   <li>获取采样率</li>
     *   <li>计算最大频率（奈奎斯特频率/水平格数）</li>
     *   <li>计算最小频率（频率分辨率）</li>
     *   <li>查找对应的频率档位范围</li>
     *   <li>更新当前频率档位（如果在范围外）</li>
     *   <li>发送事件通知</li>
     * </ol>
     *
     * <p><b>频率范围计算：</b></p>
     * <ul>
     *   <li>maxF = fs / 2 / 水平格数（每格显示的最大频率）</li>
     *   <li>minF = fs / point（频率分辨率）</li>
     * </ul>
     *
     * @param b 是否强制发送事件
     */
    public void genFFT_Axis(boolean b) {                                                   // 公有方法：生成FFT轴，参数b表示是否强制发送事件
        // ─────────────────────────────────────────────────────────────────────────
        // 计算FFT点数和采样率
        // ─────────────────────────────────────────────────────────────────────────
        int point = MathNative.CalFFTPointNum(mathChannel.getWaveLen());                   // 计算FFT点数
        double fs = mathChannel.getSampleRate();                                           // 获取采样率

        // ─────────────────────────────────────────────────────────────────────────
        // 计算有效频率范围
        // ─────────────────────────────────────────────────────────────────────────
        double maxF = fs / 2 / ScopeBase.getHorizonGridCnt();                              // 计算最大频率（奈奎斯特频率/水平格数）
        double minF = fs / point;                                                          // 计算最小频率（频率分辨率）
        if (maxF < minF)                                                                   // 如果最大频率小于最小频率（异常情况）
            maxF = minF;                                                                   // 则调整最大频率等于最小频率

        // ─────────────────────────────────────────────────────────────────────────
        // 查找对应的频率档位范围
        // ─────────────────────────────────────────────────────────────────────────
        int minID = fft_xScaleId_ceill(maxF);                                              // 查找最大频率对应的档位（往大找）
        int maxID = fft_xScaleId_floor(minF);                                              // 查找最小频率对应的档位（往小找）

        // ─────────────────────────────────────────────────────────────────────────
        // 更新档位范围
        // ─────────────────────────────────────────────────────────────────────────
        min_xScale = minID;                                                                // 更新最小档位ID
        max_xScale = maxID;                                                                // 更新最大档位ID

        // ─────────────────────────────────────────────────────────────────────────
        // 检查当前档位是否在有效范围内
        // ─────────────────────────────────────────────────────────────────────────
        boolean change = false;                                                            // 初始化变化标志为false
        if (xScale < min_xScale) {                                                         // 如果当前档位小于最小档位
            xScale = min_xScale;                                                           // 则更新为最小档位
            change = true;                                                                 // 设置变化标志为true
        } else if (xScale > max_xScale) {                                                  // 否则如果当前档位大于最大档位
            xScale = max_xScale;                                                           // 则更新为最大档位
            change = true;                                                                 // 设置变化标志为true
        }                                                                                   // if-else语句结束

        // ─────────────────────────────────────────────────────────────────────────
        // 发送事件通知
        // ─────────────────────────────────────────────────────────────────────────
        if (change || b) {                                                                 // 如果档位发生变化或强制发送
            EventFactory.sendEvent(new EventBase(EventFactory.EVENT_MATH_FFT_SCALE, mathChannel.getChId()), true); // 发送FFT频率档位变化事件
        }                                                                                   // if语句结束
    }                                                                                       // 方法结束

