/**
 * 示波器FPGA缩放深度寄存器类
 * 继承自FPGAReg基类，负责管理FPGA缩放深度参数
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>设置慢时基模式下的数据量</li>
 *   <li>管理第一次刷新显示所需的数据量</li>
 *   <li>自动处理非慢时基模式下的深度设置</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建缩放深度寄存器对象
 * FPGAReg_ZUN_DEPTH reg = new FPGAReg_ZUN_DEPTH();
 *
 * // 设置缩放深度（慢时基模式）
 * reg.Set_zunDepth(10000);  // 设置10000个采样点
 *
 * // 发送命令到FPGA
 * FPGACommand.getInstance().sendCommand(reg);
 *
 * // 在非慢时基模式下，onCommand()会自动设置采样存储深度
 * </pre>
 *
 * @see FPGAReg
 * @see Scope
 * @see Sample
 */
public class FPGAReg_ZUN_DEPTH extends FPGAReg {                                         // 类声明：缩放深度寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化缩放深度寄存器
     * 
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_ZUN_DEPTH</li>
     *   <li>数据长度：4字节（32位）</li>
     * </ul>
     */
    public FPGAReg_ZUN_DEPTH() {                                                         // 构造方法：初始化缩放深度寄存器
        super(FPGAReg.FPGA_ZUN_DEPTH,4);                                                 // 调用父类构造函数，设置寄存器ID和数据长度
    }                                                                                    // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置缩放深度
     * 用于慢时基模式下，设置第一次刷新显示所需的数据量
     * 
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>慢时基模式下，波形是分段刷新的</li>
     *   <li>第一次刷新需要足够的数据量来填充初始显示</li>
     *   <li>深度值以采样点数为单位</li>
     * </ul>
     * 
     * <p><b>参数范围：</b></p>
     * <ul>
     *   <li>最小值：屏幕宽度对应的采样点数</li>
     *   <li>最大值：采样存储深度</li>
     * </ul>
     * 
     * @param depth 缩放深度值（采样点数）
     */
    //慢时基时使用：第一次刷新显示时，需要多少数据量
    public void Set_zunDepth(int depth){                                                 // 公有方法：设置缩放深度
        setVal(0, 32, depth);                                                            // 在偏移量0处设置32位的深度值
    }                                                                                    // 方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 重写方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 命令发送前的回调方法
     * 重写父类的onCommand方法，在非慢时基模式下自动设置采样存储深度
     * 
     * <p><b>处理逻辑：</b></p>
     * <ol>
     *   <li>检查当前是否为慢时基模式</li>
     *   <li>如果不是慢时基模式，自动设置采样存储深度</li>
     *   <li>如果是慢时基模式，保持之前设置的深度值</li>
     * </ol>
     * 
     * <p><b>应用场景：</b></p>
     * <ul>
     *   <li>从慢时基模式切换到其他模式时</li>
     *   <li>需要恢复默认采样存储深度时</li>
     * </ul>
     */
    @Override                                                                            // 注解：重写父类方法
    public void onCommand() {                                                            // 公有方法：命令发送前的回调
        if (!Scope.getInstance().isInSlowScaleMode()) {                                  // 判断：是否非慢时基模式
            //非慢时基模式处理
            setVal(0, 32, Sample.getInstance().getSampleMemDepth());                     // 设置采样存储深度
        }                                                                                // 判断结束
    }                                                                                    // 方法结束

}                                                                                        // 类结束
