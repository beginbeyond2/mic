/**
 * 数学表达式错误类
 * 用于封装数学表达式解析过程中的错误信息
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>错误位置：记录错误在表达式中的位置</li>
 *   <li>错误类型：标识错误的类型（语法错误、数值错误等）</li>
 *   <li>错误值：记录导致错误的具体值</li>
 *   <li>诊断信息：提供详细的错误描述</li>
 * </ul>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 创建错误对象
 * MathExprError error = new MathExprError(false, 1, 5, "(", "括号不匹配");
 *
 * // 检查是否成功
 * if (!error.isSuccess()) {
 *     System.out.println("错误位置: " + error.getPosition());
 *     System.out.println("错误类型: " + error.getMode());
 *     System.out.println("诊断信息: " + error.getDiagnostic());
 * }
 * </pre>
 *
 * @see MathExprWave
 * @see MathNative
 */
public class MathExprError {

    // ═══════════════════════════════════════════════════════════════════════════════
    // 错误类型常量定义
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 错误类型常量定义
     *
     * <p><b>错误类型说明：</b></p>
     * <ul>
     *   <li>e_unknown = 0: 未知错误</li>
     *   <li>e_syntax  = 1: 语法错误</li>
     *   <li>e_token   = 2: 标记错误</li>
     *   <li>e_numeric = 4: 数值错误</li>
     *   <li>e_symtab  = 5: 符号表错误</li>
     *   <li>e_lexer   = 6: 词法错误</li>
     *   <li>e_helper  = 7: 辅助错误</li>
     * </ul>
     */
    /*   mode
         e_unknown = 0, Unknown Error
         e_syntax  = 1, Syntax Error
         e_token   = 2, Token Error
         e_numeric = 4, Numeric Error
         e_symtab  = 5, Symbol Error
         e_lexer   = 6, Lexer Error
         e_helper  = 7 Helper Error

     */

    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /** 错误位置，表达式中的字符索引（从0开始） */
    private int position;

    /** 错误类型，取值范围：0-7 */
    private int mode;

    /** 错误相关的值，记录导致错误的具体值 */
    private String value;

    /** 解析成功标志，true表示解析成功，false表示解析失败 */
    private boolean success;

    /** 诊断信息，详细的错误描述 */
    private String diagnostic;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 默认构造函数
     * 创建一个空的错误对象
     */
    public MathExprError(){

    }

    /**
     * 完整构造函数
     * 创建一个包含所有错误信息的错误对象
     *
     * @param success 解析成功标志
     *                true: 解析成功
     *                false: 解析失败
     * @param mode 错误类型（0-7）
     *             0: 未知错误
     *             1: 语法错误
     *             2: 标记错误
     *             4: 数值错误
     *             5: 符号表错误
     *             6: 词法错误
     *             7: 辅助错误
     * @param position 错误位置（字符索引）
     * @param value 错误相关的值
     * @param diagnostic 诊断信息描述
     */
    public MathExprError(boolean success,int mode,int position,String value,String diagnostic){
        this.mode = mode;                                                            // 设置错误类型
        this.position = position;                                                    // 设置错误位置
        this.value = value;                                                          // 设置错误值
        this.success = success;                                                      // 设置成功标志
        this.diagnostic = diagnostic;                                                // 设置诊断信息
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // Getter方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取解析成功标志
     *
     * @return true: 解析成功
     *         false: 解析失败
     */
    public boolean isSuccess() {
        return success;                                                              // 返回成功标志
    }

    /**
     * 获取错误位置
     *
     * @return 错误位置（字符索引，从0开始）
     */
    public int getPosition() {
        return position;                                                             // 返回错误位置
    }

    /**
     * 获取错误类型
     *
     * @return 错误类型（0-7）
     */
    public int getMode() {
        return mode;                                                                 // 返回错误类型
    }

    /**
     * 获取错误相关的值
     *
     * @return 错误值
     */
    public String getValue() {
        return value;                                                                // 返回错误值
    }

    /**
     * 获取诊断信息
     *
     * @return 诊断信息描述
     */
    public String getDiagnostic() {
        return diagnostic;                                                           // 返回诊断信息
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // Object方法重写
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 返回对象的字符串表示
     * 用于调试和日志输出
     *
     * @return 包含所有字段值的字符串
     */
    @Override
    public String toString() {
        return "success:" + success + ",mode:" + mode                                // 成功标志和错误类型
                + ",position:" + position + ",value:"                                // 错误位置和错误值
                + value + ",diagnostic:" + diagnostic;                               // 诊断信息
    }
}
