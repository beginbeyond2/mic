/**
 * 示波器FPGA串行解码深度寄存器类
 * 继承自FPGAReg基类，负责配置FPGA串行解码的采样深度
 *
 * <p><b>主要功能：</b></p>
 * <ul>
 *   <li>设置串行解码的采样深度</li>
 *   <li>控制解码多少帧数据</li>
 *   <li>提供串行总线解码功能支持</li>
 * </ul>
 *
 * <p><b>寄存器结构：</b></p>
 * <ul>
 *   <li>寄存器ID：FPGA_SERIAL_DEC_DEPTH (0x0B)</li>
 *   <li>数据长度：4字节（32位）</li>
 *   <li>寄存器类型：写寄存器（用于配置串行解码深度）</li>
 * </ul>
 *
 * <p><b>位定义：</b></p>
 * <ul>
 *   <li>位0-25：解码深度值（26位）</li>
 *   <li>位26-31：保留位</li>
 * </ul>
 *
 * <p><b>串行解码说明：</b></p>
 * <pre>
 * 串行解码：解码串行总线数据
 * 支持的串行总线类型：
 * - UART：通用异步收发传输器
 * - LIN：局域互联网络
 * - CAN：控制器局域网络
 * - SPI：串行外设接口
 * - I2C：内部集成电路
 * - ARINC429：航空电子数字数据总线
 * - MILSTD1553B：军用标准数据总线
 *
 * 解码深度：决定FPGA解码多少帧数据
 * - 深度越大：解码的数据帧越多
 * - 深度越小：解码的数据帧越少
 * - 影响解码显示范围和性能
 *
 * 解码深度计算公式：
 * x = x * timeOneScreen / (100 * 40 * 10000)
 * 根据屏幕时间范围计算解码深度
 * </pre>
 *
 * <p><b>使用示例：</b></p>
 * <pre>
 * // 配置串行解码深度
 * FPGACommand fpgaCmd = FPGACommand.getInstance();
 * FPGAReg_SERIAL_DEC_DEPTH reg = (FPGAReg_SERIAL_DEC_DEPTH)
 *     fpgaCmd.getFPGAReg(fpgaIdx, FPGAReg.FPGA_SERIAL_DEC_DEPTH);
 *
 * // 计算解码深度
 * int timeOneScreen = 1000;  // 屏幕时间范围（单位：ns）
 * int x = 100;  // 初始值
 * x = x * timeOneScreen / (100 * 40 * 10000);  // 计算解码深度
 *
 * // 设置解码深度
 * reg.setDecDepth((int) x);  // 设置解码深度值
 *
 * // 发送命令到FPGA
 * fpgaCmd.sendCommand(reg);
 * </pre>
 *
 * @see FPGAReg
 * @see FPGACommand#gntR_serial_dec_depth(int)
 * @see HorizontalAxisAction
 * @see Scope
 */
public class FPGAReg_SERIAL_DEC_DEPTH extends FPGAReg {                                  // 类声明：FPGA串行解码深度寄存器类，继承FPGAReg

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造函数
     * 初始化FPGA串行解码深度寄存器
     *
     * <p><b>初始化参数：</b></p>
     * <ul>
     *   <li>寄存器ID：FPGA_SERIAL_DEC_DEPTH (0x0B)</li>
     *   <li>数据长度：4字节（32位）</li>
     * </ul>
     *
     * <p><b>寄存器结构：</b></p>
     * <ul>
     *   <li>位0-25：解码深度值（26位）</li>
     *   <li>位26-31：保留位</li>
     * </ul>
     *
     * <p><b>使用说明：</b></p>
     * <ul>
     *   <li>创建寄存器对象后需要调用setDecDepth方法设置解码深度</li>
     *   <li>然后通过FPGACommand发送命令到FPGA</li>
     *   <li>FPGA配置解码深度并执行串行解码</li>
     * </ul>
     *
     * <p><b>解码深度说明：</b></p>
     * <ul>
     *   <li>解码深度：决定FPGA解码多少帧数据</li>
     *   <li>深度越大：解码的数据帧越多</li>
     *   <li>深度越小：解码的数据帧越少</li>
     *   <li>影响解码显示范围和性能</li>
     * </ul>
     */
    public FPGAReg_SERIAL_DEC_DEPTH() {                                                  // 构造方法：初始化FPGA串行解码深度寄存器
        super(FPGA_SERIAL_DEC_DEPTH, 4);                                                 // 调用父类构造函数，设置寄存器ID和数据长度4字节
    }                                                                                    // 构造方法结束

    // ═══════════════════════════════════════════════════════════════════════════════
    // 公有方法 - 解码深度设置
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 设置解码深度
     * 设置串行解码的采样深度值
     *
     * <p><b>功能说明：</b></p>
     * <ul>
     *   <li>设置串行解码的采样深度值</li>
     *   <li>决定FPGA解码多少帧数据</li>
     *   <li>影响解码显示范围和性能</li>
     * </ul>
     *
     * <p><b>寄存器位设置：</b></p>
     * <pre>
     * 位0-25：设置为解码深度值val（26位）
     *
     * 解码深度值范围：
     * - 最小值：0
     * - 最大值：2^26 - 1 = 67108863
     * - 实际范围：根据屏幕时间范围计算
     * </pre>
     *
     * <p><b>解码深度计算：</b></p>
     * <pre>
     * 解码深度计算公式：
     * x = x * timeOneScreen / (100 * 40 * 10000)
     *
     * 参数说明：
     * - x：初始值
     * - timeOneScreen：屏幕时间范围
     * - 100 * 40 * 10000：基准系数
     *
     * 计算目的：
     * - 根据屏幕时间范围计算解码深度
     * - 确保解码深度与显示范围匹配
     * - 优化解码性能
     * </pre>
     *
     * <p><b>解码深度说明：</b></p>
     * <ul>
     *   <li>解码深度：决定FPGA解码多少帧数据</li>
     *   <li>深度越大：解码的数据帧越多，显示范围越大</li>
     *   <li>深度越小：解码的数据帧越少，显示范围越小</li>
     *   <li>需要与屏幕时间范围匹配</li>
     * </ul>
     *
     * <p><b>应用场景：</b></p>
     * <ul>
     *   <li>串行解码模式配置</li>
     *   <li>解码深度调整</li>
     *   <li>解码显示优化</li>
     *   <li>串行总线解码</li>
     * </ul>
     *
     * @param val 解码深度值（26位，范围0-67108863）
     */
    public void setDecDepth(int val){                                                    // 公有方法：设置解码深度
        setVal(0,26,val);                                                                // 设置字0的位0-25为解码深度值val（26位）
    }                                                                                    // 方法结束
}                                                                                        // 类结束