package com.micsig.tbook.ui.top.view.selectHorList;

import com.micsig.tbook.ui.bean.RxMsgSelect;

/**
 * ╔══════════════════════════════════════════════════════════════════════════════╗
 * ║                          TopBeanHorizontal                                   ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 模块定位: 水平选择列表项数据模型                                              ║
 * ║ 核心职责: 封装水平选择列表中每个选项的索引和文本信息                           ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 架构设计:                                                                    ║
 * ║   ┌────────────────────┐                                                    ║
 * ║   │  TopBeanHorizontal  │ ──继承──▶ RxMsgSelect                             ║
 * ║   └────────────────────┘                                                    ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 数据流向:                                                                    ║
 * ║   TopViewSelectHorListToList ──▶ 创建TopBeanHorizontal ──▶ 返回选中项       ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 依赖关系:                                                                    ║
 * ║   - RxMsgSelect: 基类，提供选择状态管理                                      ║
 * ║   - TopViewSelectHorListToList: 使用此类返回选中项                          ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 使用示例:                                                                    ║
 * ║   TopBeanHorizontal bean = new TopBeanHorizontal(0, "选项1");               ║
 * ║   int index = bean.getIndex();                                              ║
 * ║   String text = bean.getText();                                             ║
 * ╚══════════════════════════════════════════════════════════════════════════════╝
 */

public class TopBeanHorizontal extends RxMsgSelect {
    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /** 选项索引 */
    private int index;

    /** 显示文本 */
    private String text;

    // ═══════════════════════════════════════════════════════════════════════════════
    // Getter/Setter方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取索引
     * ══════════════════════════════════════════════════════════════════════════════
     * @return 索引值
     */
    public int getIndex() {
        return index;
    }

    /**
     * 设置索引
     * ══════════════════════════════════════════════════════════════════════════════
     * @param index 索引值
     */
    public void setIndex(int index) {
        this.index = index;
    }

    /**
     * 获取显示文本
     * ══════════════════════════════════════════════════════════════════════════════
     * @return 显示文本
     */
    public String getText() {
        return text;
    }

    /**
     * 设置显示文本
     * ══════════════════════════════════════════════════════════════════════════════
     * @param text 显示文本
     */
    public void setText(String text) {
        this.text = text;
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造方法
     * ══════════════════════════════════════════════════════════════════════════════
     * @param index 索引值
     * @param text  显示文本
     */
    public TopBeanHorizontal(int index, String text) {
        this.index = index;
        this.text = text;
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // toString方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 返回字符串表示
     * ══════════════════════════════════════════════════════════════════════════════
     * @return 字符串表示
     */
    @Override
    public String toString() {
        return "TopBeanChannel{" +
                "index=" + index +
                ", text='" + text + '\'' +
                '}';
    }
}
