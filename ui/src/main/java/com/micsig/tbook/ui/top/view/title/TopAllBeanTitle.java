package com.micsig.tbook.ui.top.view.title;

import com.micsig.tbook.ui.bean.RxMsgSelect;

/**
 * ╔══════════════════════════════════════════════════════════════════════════════╗
 * ║                          TopAllBeanTitle                                     ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 模块定位: 顶部标题栏全部项数据模型                                            ║
 * ║ 核心职责: 封装标题栏中所有项的索引、文本和可见性信息                           ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 架构设计:                                                                    ║
 * ║   ┌──────────────────┐                                                      ║
 * ║   │  TopAllBeanTitle  │ ──继承──▶ RxMsgSelect                               ║
 * ║   └──────────────────┘                                                      ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 数据流向:                                                                    ║
 * ║   TopViewTitle.setData() ──▶ 创建TopAllBeanTitle ──▶ allList列表            ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 依赖关系:                                                                    ║
 * ║   - RxMsgSelect: 基类，提供选择状态管理                                      ║
 * ║   - TopViewTitle: 使用此类管理全部项                                         ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 使用示例:                                                                    ║
 * ║   TopAllBeanTitle bean = new TopAllBeanTitle(0, "设置", true);              ║
 * ║   if (bean.isVisible()) {                                                   ║
 * ║       // 添加到显示列表                                                      ║
 * ║   }                                                                         ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 注意事项:                                                                    ║
 * ║   1. visible属性控制该项是否在标题栏中显示                                    ║
 * ║   2. 支持克隆操作                                                            ║
 * ╚══════════════════════════════════════════════════════════════════════════════╝
 */

public class TopAllBeanTitle extends RxMsgSelect implements Cloneable {
    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /** 在全部列表中的索引 */
    private int index;

    /** 显示文本 */
    private String text;

    /** 是否可见（是否在标题栏中显示） */
    private boolean visible;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 克隆方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 克隆对象
     * ══════════════════════════════════════════════════════════════════════════════
     * @return 克隆的对象
     * @throws CloneNotSupportedException 不支持克隆异常
     */
    @Override
    public Object clone() throws CloneNotSupportedException {
        return super.clone();
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造方法
     * ══════════════════════════════════════════════════════════════════════════════
     * @param index   在全部列表中的索引
     * @param text    显示文本
     * @param visible 是否可见
     */
    public TopAllBeanTitle(int index, String text, boolean visible) {
        this.index = index;
        this.text = text;
        this.visible = visible;
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // Getter/Setter方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取索引
     * ══════════════════════════════════════════════════════════════════════════════
     * @return 索引值
     */
    public int getIndex() {
        return index;
    }

    /**
     * 设置索引
     * ══════════════════════════════════════════════════════════════════════════════
     * @param index 索引值
     */
    public void setIndex(int index) {
        this.index = index;
    }

    /**
     * 获取显示文本
     * ══════════════════════════════════════════════════════════════════════════════
     * @return 显示文本
     */
    public String getText() {
        return text;
    }

    /**
     * 设置显示文本
     * ══════════════════════════════════════════════════════════════════════════════
     * @param text 显示文本
     */
    public void setText(String text) {
        this.text = text;
    }

    /**
     * 是否可见
     * ══════════════════════════════════════════════════════════════════════════════
     * @return true可见，false不可见
     */
    public boolean isVisible() {
        return visible;
    }

    /**
     * 设置可见性
     * ══════════════════════════════════════════════════════════════════════════════
     * @param visible 可见性
     */
    public void setVisible(boolean visible) {
        this.visible = visible;
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // toString方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 返回字符串表示
     * ══════════════════════════════════════════════════════════════════════════════
     * @return 字符串表示
     */
    @Override
    public String toString() {
        return "TopAllBeanTitle{" +
                "index=" + index +
                ", text='" + text + '\'' +
                ", visible=" + visible +
                '}';
    }
}
