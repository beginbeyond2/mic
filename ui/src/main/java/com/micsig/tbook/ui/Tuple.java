package com.micsig.tbook.ui; // UI组件库根包，包含示波器自定义UI控件

/**
 * ╔══════════════════════════════════════════════════════════════════════════════╗
 * ║                            Tuple - 二元组工具类                                ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【模块定位】                                                                  ║
 * ║   UI组件库 > 工具类 > 数据结构                                                 ║
 * ║   MHO系列示波器软件通用数据结构                                                ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【核心职责】                                                                  ║
 * ║   1. 提供泛型二元组数据结构，用于存储两个相关联的值                              ║
 * ║   2. 支持任意类型的键值对存储                                                 ║
 * ║   3. 简化多返回值场景的代码实现                                                ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【架构设计】                                                                  ║
 * ║   类型参数: K - 第一个元素的类型                                               ║
 * ║            V - 第二个元素的类型                                               ║
 * ║   不可变性: 成员变量为final，创建后不可修改                                    ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【使用场景】                                                                  ║
 * ║   1. 方法需要返回两个值时                                                     ║
 * ║   2. 需要存储键值对但不需要Map的完整功能                                       ║
 * ║   3. 临时数据传递                                                            ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【使用示例】                                                                  ║
 * ║   // 创建二元组                                                              ║
 * ║   Tuple<String, Integer> tuple = new Tuple<>("年龄", 25);                     ║
 * ║   // 获取值                                                                  ║
 * ║   String key = tuple.getFirst();  // "年龄"                                   ║
 * ║   Integer value = tuple.getSecond();  // 25                                   ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【注意事项】                                                                  ║
 * ║   1. 元素创建后不可修改，如需修改请创建新实例                                   ║
 * ║   2. 未实现equals和hashCode方法，不适合用作Map的键                             ║
 * ╚══════════════════════════════════════════════════════════════════════════════╝
 * 
 * @author Liwb
 * @date 2022/3/9 11:25
 * @version 1.0
 * @param <K> 第一个元素的类型
 * @param <V> 第二个元素的类型
 */
public class Tuple<K,V> {
    // ================================ 成员变量定义 ================================
    
    private K first; // 第一个元素，类型为K
    private V second; // 第二个元素，类型为V
    
    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 构造方法
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     *   创建一个包含两个元素的二元组
     * 
     * 【参数说明】
     *   @param first 第一个元素
     *   @param second 第二个元素
     */
    public Tuple(K first, V second) { // 构造方法：传入两个元素
        this.first = first; // 初始化第一个元素
        this.second = second; // 初始化第二个元素
    }

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 方法：getFirst
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     *   获取二元组的第一个元素
     * 
     * 【返回值】
     *   @return 第一个元素，类型为K
     */
    public K getFirst() { // 获取第一个元素
        return first; // 返回第一个元素
    }

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 方法：getSecond
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     *   获取二元组的第二个元素
     * 
     * 【返回值】
     *   @return 第二个元素，类型为V
     */
    public V getSecond() { // 获取第二个元素
        return second; // 返回第二个元素
    }
}
