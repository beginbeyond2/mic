package com.micsig.tbook.ui.top.view.title;

import com.micsig.tbook.ui.bean.RxMsgSelect;

/**
 * ╔══════════════════════════════════════════════════════════════════════════════╗
 * ║                          TopShowBeanTitle                                    ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 模块定位: 顶部标题栏显示项数据模型                                            ║
 * ║ 核心职责: 封装标题栏中可见项的索引和文本信息                                   ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 架构设计:                                                                    ║
 * ║   ┌──────────────────┐                                                      ║
 * ║   │  TopShowBeanTitle │ ──继承──▶ RxMsgSelect                               ║
 * ║   └──────────────────┘                                                      ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 数据流向:                                                                    ║
 * ║   TopViewTitle.setData() ──▶ 创建TopShowBeanTitle ──▶ showList列表          ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 依赖关系:                                                                    ║
 * ║   - RxMsgSelect: 基类，提供选择状态管理                                      ║
 * ║   - TopViewTitle: 使用此类管理显示项                                         ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 使用示例:                                                                    ║
 * ║   TopShowBeanTitle bean = new TopShowBeanTitle(0, 1, "设置");               ║
 * ║   String text = bean.getText(); // 获取文本                                  ║
 * ║   int index = bean.getIndexAll(); // 获取在全部列表中的索引                  ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 注意事项:                                                                    ║
 * ║   1. indexShow是显示列表中的索引                                             ║
 * ║   2. indexAll是全部列表中的索引，用于关联TopAllBeanTitle                     ║
 * ║   3. 支持克隆操作                                                            ║
 * ╚══════════════════════════════════════════════════════════════════════════════╝
 */

public class TopShowBeanTitle extends RxMsgSelect implements Cloneable {
    // ═══════════════════════════════════════════════════════════════════════════════
    // 成员变量
    // ═══════════════════════════════════════════════════════════════════════════════

    /** 在显示列表中的索引 */
    private int indexShow;

    /** 在全部列表中的索引（关联TopAllBeanTitle） */
    private int indexAll;

    /** 显示文本 */
    private String text;

    // ═══════════════════════════════════════════════════════════════════════════════
    // 克隆方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 克隆对象
     * ══════════════════════════════════════════════════════════════════════════════
     * @return 克隆的对象
     * @throws CloneNotSupportedException 不支持克隆异常
     */
    @Override
    public Object clone() throws CloneNotSupportedException {
        return super.clone();
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 构造方法
     * ══════════════════════════════════════════════════════════════════════════════
     * @param indexShow 在显示列表中的索引
     * @param indexAll  在全部列表中的索引
     * @param text      显示文本
     */
    public TopShowBeanTitle(int indexShow, int indexAll, String text) {
        this.indexShow = indexShow;
        this.indexAll = indexAll;
        this.text = text;
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // Getter/Setter方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 获取显示列表索引
     * ══════════════════════════════════════════════════════════════════════════════
     * @return 显示列表索引
     */
    public int getIndexShow() {
        return indexShow;
    }

    /**
     * 设置显示列表索引
     * ══════════════════════════════════════════════════════════════════════════════
     * @param indexShow 显示列表索引
     */
    public void setIndexShow(int indexShow) {
        this.indexShow = indexShow;
    }

    /**
     * 获取全部列表索引
     * ══════════════════════════════════════════════════════════════════════════════
     * @return 全部列表索引
     */
    public int getIndexAll() {
        return indexAll;
    }

    /**
     * 设置全部列表索引
     * ══════════════════════════════════════════════════════════════════════════════
     * @param indexAll 全部列表索引
     */
    public void setIndexAll(int indexAll) {
        this.indexAll = indexAll;
    }

    /**
     * 获取显示文本
     * ══════════════════════════════════════════════════════════════════════════════
     * @return 显示文本
     */
    public String getText() {
        return text;
    }

    /**
     * 设置显示文本
     * ══════════════════════════════════════════════════════════════════════════════
     * @param text 显示文本
     */
    public void setText(String text) {
        this.text = text;
    }

    // ═══════════════════════════════════════════════════════════════════════════════
    // toString方法
    // ═══════════════════════════════════════════════════════════════════════════════

    /**
     * 返回字符串表示
     * ══════════════════════════════════════════════════════════════════════════════
     * @return 字符串表示
     */
    @Override
    public String toString() {
        return "TopShowBeanTitle{" +
                "indexShow=" + indexShow +
                ", indexAll=" + indexAll +
                ", text='" + text + '\'' +
                '}';
    }
}
