package com.micsig.tbook.ui.main;

import android.content.Context;
import android.util.AttributeSet;
import android.view.View;
import android.view.ViewGroup;

import androidx.annotation.Nullable;

/**
 * ╔══════════════════════════════════════════════════════════════════════════════╗
 *                              动画视图控件
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * 【模块定位】
 * 外部按键动画显示框控件，支持属性动画操作，用于显示按键操作的视觉反馈动画。
 *
 * 【核心职责】
 * 1. 提供可动画化的位置属性（aX, aY）
 * 2. 提供可动画化的尺寸属性（aWidth, aHeight）
 * 3. 支持ObjectAnimator进行属性动画
 * 4. 自动同步View的位置和尺寸
 *
 * 【架构设计】
 * ┌─────────────────────────────────────────────────────────────────┐
 * │                      AnimationView                              │
 * ├─────────────────────────────────────────────────────────────────┤
 * │  位置属性: aX / aY (支持属性动画)                              │
 * │  尺寸属性: aWidth / aHeight (支持属性动画)                     │
 * │  同步方法: setAX / setAY / setAWidth / setAHeight              │
 * └─────────────────────────────────────────────────────────────────┘
 *
 * 【数据流向】
 * ObjectAnimator → setAX/setAY/setAWidth/setAHeight → 更新View属性 → 渲染
 *
 * 【属性动画支持】
 * ┌──────────────────────────────────────────────────────────────────┐
 * │  属性名     │  对应方法    │  功能说明                          │
 * ├──────────────────────────────────────────────────────────────────┤
 * │  aX        │  setAX()     │  设置X坐标，同步调用setX()         │
 * │  aY        │  setAY()     │  设置Y坐标，同步调用setY()         │
 * │  aWidth    │  setAWidth() │  设置宽度，更新LayoutParams        │
 * │  aHeight   │  setAHeight()│  设置高度，更新LayoutParams        │
 * └──────────────────────────────────────────────────────────────────┘
 *
 * 【依赖关系】
 * ┌──────────────┐     ┌──────────────────────┐
 * │ ObjectAnimator│────>│ 属性动画支持         │
 * └──────────────┘     └──────────────────────┘
 *
 * 【使用示例】
 * // 创建动画视图
 * AnimationView animView = new AnimationView(context);
 *
 * // 使用属性动画移动位置
 * ObjectAnimator animX = ObjectAnimator.ofFloat(animView, "aX", 0, 100);
 * ObjectAnimator animY = ObjectAnimator.ofFloat(animView, "aY", 0, 200);
 * AnimatorSet set = new AnimatorSet();
 * set.playTogether(animX, animY);
 * set.start();
 *
 * // 使用属性动画改变尺寸
 * ObjectAnimator animW = ObjectAnimator.ofFloat(animView, "aWidth", 50, 200);
 * animW.start();
 *
 * 【注意事项】
 * 1. 属性名以小写'a'开头，避免与View原生属性冲突
 * 2. 设置尺寸时会更新LayoutParams，需要确保View已添加到父容器
 * 3. 适用于外部按键操作的视觉反馈动画
 *
 * @author liwb
 * @since 2017/12/14
 * ╚══════════════════════════════════════════════════════════════════════════════╝
 */
public class AnimationView extends View {

    // ═════════════════════════════════════════════════════════════════════════════
    // 可动画化属性
    // ═════════════════════════════════════════════════════════════════════════════

    /** 动画X坐标 */
    private float aX;

    /** 动画Y坐标 */
    private float aY;

    /** 动画宽度 */
    private float aWidth;

    /** 动画高度 */
    private float aHeight;

    // ═════════════════════════════════════════════════════════════════════════════
    // 属性Getter/Setter方法
    // ═════════════════════════════════════════════════════════════════════════════

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 获取动画X坐标
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     * 获取当前动画X坐标值。
     *
     * 【返回值】
     * @return 动画X坐标
     * ═══════════════════════════════════════════════════════════════════════════
     */
    public float getaX() {
        return aX;
    }

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 设置动画X坐标
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     * 设置动画X坐标，并同步更新View的实际位置。
     * 此方法支持ObjectAnimator属性动画调用。
     *
     * 【参数说明】
     * @param aX X坐标值
     *
     * 【同步操作】
     * 调用setX(aX)同步更新View位置
     * ═══════════════════════════════════════════════════════════════════════════
     */
    public void setAX(float aX) {
        // 保存动画X坐标
        this.aX = aX;
        // 同步更新View的实际X位置
        setX(aX);
    }

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 获取动画Y坐标
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     * 获取当前动画Y坐标值。
     *
     * 【返回值】
     * @return 动画Y坐标
     * ═══════════════════════════════════════════════════════════════════════════
     */
    public float getaY() {
        return aY;
    }

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 设置动画Y坐标
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     * 设置动画Y坐标，并同步更新View的实际位置。
     * 此方法支持ObjectAnimator属性动画调用。
     *
     * 【参数说明】
     * @param aY Y坐标值
     *
     * 【同步操作】
     * 调用setY(aY)同步更新View位置
     * ═══════════════════════════════════════════════════════════════════════════
     */
    public void setAY(float aY) {
        // 保存动画Y坐标
        this.aY = aY;
        // 同步更新View的实际Y位置
        setY(aY);
    }

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 获取动画宽度
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     * 获取当前动画宽度值。
     *
     * 【返回值】
     * @return 动画宽度
     * ═══════════════════════════════════════════════════════════════════════════
     */
    public float getaWidth() {
        return aWidth;
    }

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 设置动画宽度
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     * 设置动画宽度，并同步更新View的实际尺寸。
     * 此方法支持ObjectAnimator属性动画调用。
     *
     * 【参数说明】
     * @param aWidth 宽度值
     *
     * 【同步操作】
     * 更新LayoutParams的width属性并重新设置LayoutParams
     *
     * 【注意事项】
     * 需要确保View已添加到父容器，否则getLayoutParams()可能返回null
     * ═══════════════════════════════════════════════════════════════════════════
     */
    public void setAWidth(float aWidth) {
        // 保存动画宽度
        this.aWidth = aWidth;
        // 获取当前LayoutParams
        ViewGroup.LayoutParams layoutParams=this.getLayoutParams();
        // 更新宽度
        layoutParams.width=(int)aWidth;
        // 重新设置LayoutParams
        this.setLayoutParams(layoutParams);
    }

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 获取动画高度
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     * 获取当前动画高度值。
     *
     * 【返回值】
     * @return 动画高度
     * ═══════════════════════════════════════════════════════════════════════════
     */
    public float getaHeight() {
        return aHeight;
    }

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 设置动画高度
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     * 设置动画高度，并同步更新View的实际尺寸。
     * 此方法支持ObjectAnimator属性动画调用。
     *
     * 【参数说明】
     * @param aHeight 高度值
     *
     * 【同步操作】
     * 更新LayoutParams的height属性并重新设置LayoutParams
     *
     * 【注意事项】
     * 需要确保View已添加到父容器，否则getLayoutParams()可能返回null
     * ═══════════════════════════════════════════════════════════════════════════
     */
    public void setAHeight(float aHeight) {
        // 保存动画高度
        this.aHeight = aHeight;
        // 获取当前LayoutParams
        ViewGroup.LayoutParams layoutParams=this.getLayoutParams();
        // 更新高度
        layoutParams.height=(int) aHeight;
        // 重新设置LayoutParams
        this.setLayoutParams(layoutParams);
    }

    // ═════════════════════════════════════════════════════════════════════════════
    // 构造方法
    // ═════════════════════════════════════════════════════════════════════════════

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 构造方法 - 单参数
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     * 创建AnimationView实例，仅传入Context。
     *
     * 【参数说明】
     * @param context 上下文对象
     * ═══════════════════════════════════════════════════════════════════════════
     */
    public AnimationView(Context context) {
        this(context, null);
    }

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 构造方法 - XML属性
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     * 创建AnimationView实例，支持XML属性。
     *
     * 【参数说明】
     * @param context 上下文对象
     * @param attrs   XML属性集
     * ═══════════════════════════════════════════════════════════════════════════
     */
    public AnimationView(Context context, @Nullable AttributeSet attrs) {
        this(context, attrs, 0);
    }

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 构造方法 - 完整参数
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     * 创建AnimationView实例，支持完整参数。
     *
     * 【参数说明】
     * @param context      上下文对象
     * @param attrs        XML属性集
     * @param defStyleAttr 默认样式属性
     * ═══════════════════════════════════════════════════════════════════════════
     */
    public AnimationView(Context context, @Nullable AttributeSet attrs, int defStyleAttr) {
        super(context, attrs, defStyleAttr);
    }



}
