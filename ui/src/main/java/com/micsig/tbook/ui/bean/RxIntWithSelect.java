package com.micsig.tbook.ui.bean; // UI组件库bean子包，包含数据模型类

/**
 * ╔══════════════════════════════════════════════════════════════════════════════╗
 * ║                    RxIntWithSelect - 整数值选择数据模型                        ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【模块定位】                                                                  ║
 * ║   UI组件库 > bean > 数据模型 > 响应式数据                                      ║
 * ║   MHO系列示波器软件数据模型之一                                                ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【核心职责】                                                                  ║
 * ║   1. 存储整数值和选择状态的组合                                               ║
 * ║   2. 继承RxMsgSelect的选择状态管理能力                                        ║
 * ║   3. 用于需要整数值和选择状态同时管理的场景                                    ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【架构设计】                                                                  ║
 * ║   继承关系: RxIntWithSelect extends RxMsgSelect                               ║
 * ║   设计模式: 值对象模式，封装整数值和选择状态                                    ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【使用场景】                                                                  ║
 * ║   1. 示波器通道索引选择                                                       ║
 * ║   2. 数值选项的选中状态管理                                                   ║
 * ║   3. 需要同时管理整数值和选择状态的场景                                        ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【使用示例】                                                                  ║
 * ║   RxIntWithSelect intSelect = new RxIntWithSelect(5);                         ║
 * ║   intSelect.setRxMsgSelect(true);                                             ║
 * ║   int currentValue = intSelect.getValue();                                     ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║ 【注意事项】                                                                  ║
 * ║   1. 继承自RxMsgSelect，拥有选择状态管理能力                                  ║
 * ║   2. 整数值可以为任意int范围                                                  ║
 * ╚══════════════════════════════════════════════════════════════════════════════╝
 * 
 * @author yangj
 * @date 2017/5/22
 * @version 1.0
 */

public class RxIntWithSelect extends RxMsgSelect { // 整数值选择数据模型，继承自RxMsgSelect
    // ================================ 成员变量定义 ================================
    
    /**
     * 整数值
     * 存储实际的整数状态
     */
    private int value; // 整数值

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 方法：getValue
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     *   获取整数值
     * 
     * 【返回值】
     *   @return 当前整数值
     */
    public int getValue() { // 获取整数值
        return value; // 返回整数值
    }

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 方法：setValue
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     *   设置整数值
     * 
     * 【参数说明】
     *   @param value 新的整数值
     */
    public void setValue(int value) { // 设置整数值
        this.value = value; // 更新整数值
    }

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 构造方法
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     *   创建RxIntWithSelect实例
     * 
     * 【参数说明】
     *   @param value 初始整数值
     */
    public RxIntWithSelect(int value) { // 构造方法
        this.value = value; // 设置初始整数值
    }

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 方法：toString
     * ═══════════════════════════════════════════════════════════════════════════
     * 【功能说明】
     *   返回对象的字符串表示，用于调试和日志输出
     * 
     * 【返回值】
     *   @return 格式化的字符串表示
     */
    @Override
    public String toString() { // 转换为字符串
        return "RxIntWithSelect{" + // 类名
                "value=" + value + // 整数值
                "rxMsgSelect=" + rxMsgSelect + // 选择状态
                '}'; // 结束括号
    }
}
