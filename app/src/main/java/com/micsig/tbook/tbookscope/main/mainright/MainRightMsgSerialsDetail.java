package com.micsig.tbook.tbookscope.main.mainright;

import com.micsig.tbook.tbookscope.rightslipmenu.serials.RightMsgSerials;

/**
 * +=============================================================================================+
 * |  模块定位：示波器主界面右侧面板 - 串口总线详细信息消息类                                     |
 * +=============================================================================================+
 * |  核心职责：                                                                                  |
 * |  1. 封装串口总线（UART/SPI/I2C/CAN等）的详细信息消息                                        |
 * |  2. 承载RightMsgSerials数据对象，用于模块间传递                                            |
 * |  3. 控制触发电平打开状态标志                                                                |
 * |  4. 标识消息来源（EventBus或UI操作）                                                        |
 * +=============================================================================================+
 * |  架构设计：                                                                                  |
 * |  - 数据传输对象（DTO）模式                                                                  |
 * |  - 消息封装类，用于RxJava/EventBus消息传递                                                 |
 * |  - 轻量级数据容器，不包含复杂业务逻辑                                                       |
 * +=============================================================================================+
 * |  数据流向：                                                                                  |
 * |  输入：RightMsgSerials对象 + 触发电平标志 + 来源标识                                       |
 * |  流向：串口配置模块 → EventBus/RxJava → 主界面右侧面板 → 硬件控制层                        |
 * +=============================================================================================+
 * |  依赖关系：                                                                                  |
 * |  - RightMsgSerials：串口总线配置消息类                                                     |
 * |  - 被MainRightLayoutItemSerialsMaster等UI组件使用                                         |
 * |  - 通过RxJava在模块间传递                                                                  |
 * +=============================================================================================+
 * |  使用场景：                                                                                  |
 * |  1. 串口总线配置变更时封装消息数据                                                          |
 * |  2. 主界面右侧串口面板更新时传递配置信息                                                    |
 * |  3. RxJava消息传递的载体对象                                                               |
 * +=============================================================================================+
 * Created by yangj on 2017/9/15.
 */

public class MainRightMsgSerialsDetail {
    // ==================== 成员变量定义 ====================
    private RightMsgSerials rightMsgSerials; // 串口总线配置消息对象，包含总线类型、通道号、波特率等信息
    private boolean unOpenTriggerLevel = false; // 触发电平未打开标志（true=未打开，false=已打开）
    private boolean isFromEventBus = false; // 消息来源标识（true=来自EventBus，false=来自UI操作）

    /**
     * +-----------------------------------------------------------------------------------------+
     * |  方法：isFromEventBus - 获取消息来源标识                                                 |
     * +-----------------------------------------------------------------------------------------+
     * |  功能描述：                                                                              |
     * |  返回当前消息是否来自EventBus发送                                                        |
     * +-----------------------------------------------------------------------------------------+
     * |  返回值：                                                                                |
     * |  @return boolean  true=来自EventBus，false=来自UI操作                                    |
     * +-----------------------------------------------------------------------------------------+
     */
    public boolean isFromEventBus() {
        return isFromEventBus; // 返回消息来源标识
    }

    /**
     * +-----------------------------------------------------------------------------------------+
     * |  方法：setFromEventBus - 设置消息来源标识                                                 |
     * +-----------------------------------------------------------------------------------------+
     * |  功能描述：                                                                              |
     * |  设置当前消息是否来自EventBus发送                                                        |
     * +-----------------------------------------------------------------------------------------+
     * |  参数说明：                                                                              |
     * |  @param fromEventBus  消息来源标识                                                       |
     * +-----------------------------------------------------------------------------------------+
     */
    public void setFromEventBus(boolean fromEventBus) {
        isFromEventBus = fromEventBus; // 设置消息来源标识
    }

    /**
     * +-----------------------------------------------------------------------------------------+
     * |  方法：getRightMsgSerials - 获取串口总线配置消息                                         |
     * +-----------------------------------------------------------------------------------------+
     * |  功能描述：                                                                              |
     * |  返回封装的RightMsgSerials对象，包含串口总线详细配置                                      |
     * +-----------------------------------------------------------------------------------------+
     * |  返回值：                                                                                |
     * |  @return RightMsgSerials  串口总线配置消息对象                                           |
     * +-----------------------------------------------------------------------------------------+
     */
    public RightMsgSerials getRightMsgSerials() {
        return rightMsgSerials; // 返回串口总线配置消息
    }

    /**
     * +-----------------------------------------------------------------------------------------+
     * |  方法：setRightMsgSerials - 设置串口总线配置消息                                         |
     * +-----------------------------------------------------------------------------------------+
     * |  功能描述：                                                                              |
     * |  设置封装的RightMsgSerials对象                                                           |
     * +-----------------------------------------------------------------------------------------+
     * |  参数说明：                                                                              |
     * |  @param rightMsgSerials  串口总线配置消息对象                                            |
     * +-----------------------------------------------------------------------------------------+
     */
    public void setRightMsgSerials(RightMsgSerials rightMsgSerials) {
        this.rightMsgSerials = rightMsgSerials; // 设置串口总线配置消息
    }

    /**
     * +-----------------------------------------------------------------------------------------+
     * |  方法：isUnOpenTriggerLevel - 获取触发电平未打开标志                                     |
     * +-----------------------------------------------------------------------------------------+
     * |  功能描述：                                                                              |
     * |  返回触发电平是否处于未打开状态                                                           |
     * +-----------------------------------------------------------------------------------------+
     * |  返回值：                                                                                |
     * |  @return boolean  true=未打开，false=已打开                                              |
     * +-----------------------------------------------------------------------------------------+
     */
    public boolean isUnOpenTriggerLevel() {
        return unOpenTriggerLevel; // 返回触发电平未打开标志
    }

    /**
     * +-----------------------------------------------------------------------------------------+
     * |  方法：setUnOpenTriggerLevel - 设置触发电平未打开标志                                     |
     * +-----------------------------------------------------------------------------------------+
     * |  功能描述：                                                                              |
     * |  设置触发电平是否处于未打开状态                                                           |
     * +-----------------------------------------------------------------------------------------+
     * |  参数说明：                                                                              |
     * |  @param unOpenTriggerLevel  触发电平未打开标志                                           |
     * +-----------------------------------------------------------------------------------------+
     */
    public void setUnOpenTriggerLevel(boolean unOpenTriggerLevel) {
        this.unOpenTriggerLevel = unOpenTriggerLevel; // 设置触发电平未打开标志
    }
}