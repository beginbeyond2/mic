package com.micsig.tbook.tbookscope.top.layout.trigger; // 触发器配置模块所在包 //

import com.micsig.tbook.ui.top.view.channel.TopBeanChannel; // 通道配置Bean类 //

/**
 * ╔══════════════════════════════════════════════════════════════════════════════╗
 * ║                       触发器详情接口                                         ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║  模块名称: ITriggerDetail                                                    ║
 * ║  所属模块: 触发器配置模块                                                      ║
 * ║  创建时间: 2017/5/17                                                         ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║  核心职责:                                                                    ║
 * ║  1. 定义触发器详情的通用接口                                                   ║
 * ║  2. 提供获取触发源的方法                                                       ║
 * ║  3. 支持克隆操作，用于触发器配置的深拷贝                                       ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║  架构设计:                                                                    ║
 * ║                                                                              ║
 * ║    ┌─────────────────────────────────────────────────────────────────┐       ║
 * ║    │                    ITriggerDetail (接口)                        │       ║
 * ║    │                   extends Cloneable                             │       ║
 * ║    └─────────────────────────────────────────────────────────────────┘       ║
 * ║                                   │                                          ║
 * ║              ┌────────────────────┼────────────────────┐                     ║
 * ║              │                    │                    │                     ║
 * ║              ▼                    ▼                    ▼                     ║
 * ║    ┌──────────────┐    ┌──────────────┐    ┌──────────────┐                 ║
 * ║    │TopMsgTrigger │    │TopMsgTrigger │    │TopMsgTrigger │                 ║
 * ║    │    Edge      │    │    Logic     │    │    Common    │                 ║
 * ║    └──────────────┘    └──────────────┘    └──────────────┘                 ║
 * ║                                                                              ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║  数据流向:                                                                    ║
 * ║                                                                              ║
 * ║  TopLayoutTrigger → TopMsgTrigger → ITriggerDetail.getTriggerSource()        ║
 * ║                                                                              ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║  依赖关系:                                                                    ║
 * ║  • Cloneable            - Java标准克隆接口                                   ║
 * ║  • TopBeanChannel       - 通道配置数据Bean                                   ║
 * ║                                                                              ║
 * ╠══════════════════════════════════════════════════════════════════════════════╣
 * ║  使用场景:                                                                    ║
 * ║  1. 各触发器详情数据模型实现此接口                                             ║
 * ║  2. 通过克隆接口实现触发器配置的深拷贝                                         ║
 * ║  3. 通过getTriggerSource统一获取触发源通道信息                                ║
 * ║                                                                              ║
 * ╚══════════════════════════════════════════════════════════════════════════════╝
 */
public interface ITriggerDetail extends Cloneable { // 触发器详情接口，继承克隆接口 //

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 方法名称: getTriggerSource
     * 方法描述: 获取触发源通道信息
     *
     * @return TopBeanChannel 触发源通道配置Bean
     *
     * 功能说明:
     * 返回当前触发器配置的触发源通道信息，包括通道名称、索引等
     *
     * 使用场景: 外部需要获取触发源通道配置时调用
     * ═══════════════════════════════════════════════════════════════════════════
     */
    TopBeanChannel getTriggerSource(); // 获取触发源通道信息 //

    /**
     * ═══════════════════════════════════════════════════════════════════════════
     * 方法名称: clone
     * 方法描述: 克隆当前触发器详情对象
     *
     * @return Object 克隆后的触发器详情对象
     * @throws CloneNotSupportedException 如果对象不支持克隆
     *
     * 功能说明:
     * 创建当前触发器详情对象的深拷贝，用于配置的复制和备份
     *
     * 使用场景: 需要复制触发器配置时调用
     * ═══════════════════════════════════════════════════════════════════════════
     */
    Object clone() throws CloneNotSupportedException; // 克隆当前对象 //
}
